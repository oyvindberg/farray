    */
  private def fromBoxedK(k: Kind, expr: String): String = prims.find(_.name == k.name) match
    case Some(p) => s"((${p.boxed}) $expr).${p.jt}Value()"
    case None    => expr

  /** All reduce traversers (input-kind × Z-acc × direction) plus the fold function types. The prim self-kind unboxed fold (`${I}To${I}Fold`) is emitted only
    * for primitive input kinds; every input kind gets the generic ref-acc fold (`${I}ToRefFold<Z>`).
    */
  private def traversersJava: String = {
    val e = new Emit()
    e.line("package farray;")
    e.blank()
    e.line("// GENERATED by GenCores — do not edit. Hybrid-traversal Java: fold function types + the shared")
    e.line("// Reduce push traversers (design §3/§4). foldLeft/foldRight over ALL input kinds. The surface")
    e.line("// (FArrayOps.foldLeftImpl) peels Empty/One/top-leaf inline; only genuine TREES reach here.")
    e.open("public final class Traversers")
    e.line("private Traversers() {}")
    e.blank()
    e.line("// --- fold function types (§3): specialized on the accumulator kind Z so a prim Z stays unboxed ---")
    opKinds.foreach { k =>
      val je = jelem(k)
      if k.isPrim then {
        e.open(s"public interface ${k.name}To${k.name}Fold")
        e.line(s"$je apply($je acc, $je v);")
        e.close()
      }
      e.open(s"public interface ${k.name}ToRefFold<Z>")
      e.line(s"Z apply(Z acc, $je v);")
      e.close()
    }
    // CROSS-kind fold types: a Ref ELEMENT folded into a PRIMITIVE accumulator (e.g. xs.foldLeft(0)((n, s) =>
    // n + s.length) — sum/count over Strings). `acc` stays an unboxed primitive; the element is Object. This is
    // a COMMON reference workload, so it gets the unboxed acc (mirror of the prim-input + Ref-acc fold above).
    primKinds.foreach { p =>
      val jp = jelem(p)
      e.open(s"public interface RefTo${p.name}Fold")
      e.line(s"$jp apply($jp acc, Object v);")
      e.close()
    }
    e.blank()
    // --- Build (map) function types (§3): one per COVERED (input I, output O) pair. Primitive O is unboxed
    //     (a primitive return); a Ref OUTPUT is a type param RO. A Ref INPUT element is plain `Object` (NOT a
    //     type param) — exactly like the RefToRefFold SAM — so the Java traverser, whose Ref leaf arrays are
    //     Object[], passes elements with NO cast; the Scala lambda casts Object->A itself. ---
    e.line("// --- build (map) function types (§3): I->O element transforms; prim O unboxed, Ref OUTPUT is a tparam ---")
    buildPairs.foreach { case (ki, ko) =>
      val ji = jelem(ki) // Object for Ref input
      val jo = jelem(ko)
      (ki.isPrim, ko.isPrim) match {
        case (true, true) => // prim -> prim (self-kind only): fully unboxed
          e.open(s"public interface ${ki.name}To${ko.name}Fn")
          e.line(s"$jo apply($ji v);")
          e.close()
        case (true, false) => // prim -> Ref: output is a type param RO
          e.open(s"public interface ${ki.name}ToRefFn<RO>")
          e.line(s"RO apply($ji v);")
          e.close()
        case (false, true) => // Ref -> prim: input element is Object (no cast in Java)
          e.open(s"public interface RefTo${ko.name}Fn")
          e.line(s"$jo apply(Object v);")
          e.close()
        case (false, false) => // Ref -> Ref: input Object, output type param RO
          e.open("public interface RefToRefFn<RO>")
          e.line("RO apply(Object v);")
          e.close()
      }
    }
    e.blank()
    // --- consumer (foreach) function types (§3): one per INPUT kind. Prim input takes the unboxed prim; a Ref
    //     input element is plain Object (the Scala lambda casts Object->A itself), matching the Fold/Fn SAMs. ---
    e.line("// --- consumer (foreach) function types (§3): void apply(I v); Ref input takes Object ---")
    opKinds.foreach { k =>
      e.open(s"public interface ${k.name}Consumer")
      e.line(s"void apply(${jelem(k)} v);")
      e.close()
    }
    e.blank()
    // --- predicate (short-circuit) function types (§3): one per INPUT kind. `boolean apply(I v)`; the leaf scan
    //     stops on the FIRST element for which this returns true. Prim input takes the unboxed prim; a Ref input
    //     element is plain Object (the Scala lambda casts Object->A itself), matching the Consumer/Fold/Fn SAMs. ---
    e.line("// --- predicate (short-circuit) function types (§3): boolean apply(I v); Ref input takes Object ---")
    opKinds.foreach { k =>
      e.open(s"public interface ${k.name}Pred")
      e.line(s"boolean apply(${jelem(k)} v);")
      e.close()
    }
    e.blank()
    opKinds.foreach { k =>
      // prim input: unboxed self-kind acc traverser (fwd + bwd), then the generic ref-acc traverser (fwd + bwd).
      if k.isPrim then {
        reduceTraverser(e, k, backward = false, refAcc = false)
        e.blank()
        reduceTraverser(e, k, backward = true, refAcc = false)
        e.blank()
      }
      reduceTraverser(e, k, backward = false, refAcc = true)
      e.blank()
      reduceTraverser(e, k, backward = true, refAcc = true)
      e.blank()
    }
    // --- Build (map) traversers: ONE per covered (I, O) pair. Same lazy-stack walk skeleton as reduceFwd;
    //     writes out[o++] = f.apply(elem) ascending. A ReverseNode recurses into the BACKWARD build (still
    //     writing out[o++] ascending — only the READ order flips). Deep-base Slice/Pad/Updated keep the
    //     per-index applyBoxed read (a later pass replaces those). ---
    buildPairs.foreach { case (ki, ko) =>
      buildTraverser(e, ki, ko, backward = false)
      e.blank()
      buildTraverser(e, ki, ko, backward = true)
      e.blank()
    }
    // --- Build-filtered (filter) traversers: ONE pair (fwd + bwd) per INPUT kind. Same lazy-stack walk skeleton
    //     as buildFwd, but the write is predicate-GUARDED: `if (p.apply(elem)) out[o++] = elem`. The kept-count
    //     cursor `o` advances ascending in BOTH directions; a ReverseNode recurses into the OPPOSITE-direction
    //     filter so a reversed subtree's local source order flips (READ flips) while the WRITE stays forward
    //     out[o++] — keeping the SAME kept elements in REVERSED order, exactly as reverse.filter requires. Deep-base
    //     Slice/Pad/Updated keep the per-index applyBoxed read (a later pass replaces those). ---
    opKinds.foreach { k =>
      buildFilteredTraverser(e, k, backward = false)
      e.blank()
      buildFilteredTraverser(e, k, backward = true)
      e.blank()
    }
    // --- Partition traversers: ONE pair (fwd + bwd) per INPUT kind. The dual-output sibling of buildFiltered:
    //     each element is routed to outA[oa++] (predicate true) OR outB[ob++] (predicate false). Both cursors
    //     advance ascending in BOTH directions; a ReverseNode recurses into the OPPOSITE-direction partition so a
    //     reversed subtree's local source order flips (READ flips) while BOTH WRITES stay forward — keeping the
    //     SAME split in REVERSED order, exactly as reverse.partition requires. The two cursors are PACKED into the
    //     return long (oa low 32 bits, ob high 32 bits). Deep-base Slice/Pad/Updated read per-index via applyBoxed. ---
    opKinds.foreach { k =>
      partitionTraverser(e, k, backward = false)
      e.blank()
      partitionTraverser(e, k, backward = true)
      e.blank()
    }
    // --- Foreach traversers: ONE pair (fwd + bwd) per INPUT kind. Same lazy-stack walk skeleton; per element
    //     it just calls f.apply(elem). A ReverseNode recurses into the opposite-direction foreach so a reversed
    //     subtree's local order flips. No accumulator, no output — Unit. ---
    opKinds.foreach { k =>
      foreachTraverser(e, k, backward = false)
      e.blank()
      foreachTraverser(e, k, backward = true)
      e.blank()
    }
    // --- Scan traversers: FOUR per covered (I, Z) pair (same coverage as Reduce: prim self + Ref acc). The WRITE
    //     convention is FIXED per op (scanLeft writes fwd out[o-1]; scanRight writes bwd out[o+1]); the VISIT
    //     direction is INDEPENDENT and flips at each ReverseNode. So each op gets a MAIN entry (visit == write
    //     dir, the shared-leaf forwarder) PLUS a reverse-arm variant (visit != write dir) it bounces to at a
    //     ReverseNode. scanLeft: scanFwd…(visit fwd) <-> scanFwdRev…(visit bwd); scanRight: scanBwd…(visit bwd)
    //     <-> scanBwdRev…(visit fwd). The running acc is ALWAYS the previously-written slot (out[o-1]/out[o+1]),
    //     so no acc threading; each method returns the advanced cursor `o`. ---
    opKinds.foreach { k =>
      def four(refAcc: Boolean): Unit = {
        // scanLeft (write fwd): MAIN visit-fwd + reverse-arm visit-bwd.
        scanTraverser(e, k, writeBackward = false, visitBackward = false, refAcc = refAcc); e.blank()
        scanTraverser(e, k, writeBackward = false, visitBackward = true, refAcc = refAcc); e.blank()
        // scanRight (write bwd): MAIN visit-bwd + reverse-arm visit-fwd.
        scanTraverser(e, k, writeBackward = true, visitBackward = true, refAcc = refAcc); e.blank()
        scanTraverser(e, k, writeBackward = true, visitBackward = false, refAcc = refAcc); e.blank()
      }
      if k.isPrim then four(refAcc = false)
      four(refAcc = true)
    }
    // --- Short-circuit traversers: ONE pair (fwd + bwd) per INPUT kind. Same lazy-stack walk skeleton, BUT each
    //     method returns the FIRST-HIT global index (and ABANDONS the lazy stack via the method return on a hit)
    //     instead of running to completion. It threads a cumulative element count `cum` to map a local leaf hit to
    //     a global index, and `from`/`end` clamps each leaf's window (whole leaves below `from`/above `end` are
    //     skipped without testing). A ReverseNode recurses into the opposite direction; the recursion encodes
    //     "no hit, new cum = C" as ~C (a negative) and a hit as the global index (>= 0). ---
    opKinds.foreach { k =>
      scTraverser(e, k, backward = false)
      e.blank()
      scTraverser(e, k, backward = true)
      e.blank()
    }
    e.close()
    e.result
  }

  /** The COVERED (input I, output O) pairs for the Build shape (map/mapConserve): primitive self-kind (Int->Int, Long->Long, Double->Double) plus every kind
    * paired with Ref (prim->Ref, Ref->prim, Ref->Ref). Cross-prim pairs (e.g. Int->Long) are NOT here — the surface routes them through a generic per-element
    * boxed `applyAtImpl` path that preserves the output kind O.
    */
  private def buildPairs: List[(Kind, Kind)] = {
    val refK = opKinds.find(_.name == "Ref").get
    val primSelf = opKinds.filter(_.isPrim).map(k => (k, k))
    val toRef = opKinds.filter(_.isPrim).map(k => (k, refK))
    val fromRef = opKinds.filter(_.isPrim).map(k => (refK, k))
    primSelf ++ toRef ++ fromRef ++ List((refK, refK))
  }

  /** The Java type name of the Build SAM for (I, O): prim-self `${I}To${O}Fn`, prim->Ref `${I}ToRefFn<RO>`, Ref->prim `RefTo${O}Fn`, Ref->Ref `RefToRefFn<RO>`.
    * A Ref INPUT element is plain Object (no tparam); only a Ref OUTPUT carries the `roArg` type param (e.g. "B & AnyRef" Scala-side, erased Java-side).
    */
  private def buildFnType(ki: Kind, ko: Kind, roArg: String): String =
    (ki.isPrim, ko.isPrim) match {
      case (true, true)   => s"${ki.name}To${ko.name}Fn"
      case (true, false)  => s"${ki.name}ToRefFn<$roArg>"
      case (false, true)  => s"RefTo${ko.name}Fn"
      case (false, false) => s"RefToRefFn<$roArg>"
    }

  /** The per-traverser pieces a WINDOWED sub-traverse needs. The windowed method `<name>(node, skip, take, …)` walks `node` but APPLIES the per-element action
    * ONLY to the elements in the window [skip, skip+take) (in THIS direction's frame — a Bwd window counts skip from the END), early-returning the instant
    * `take` hits 0. It is RECURSIVE (one call per structural child, with a child-local sub-window computed from the child's KNOWN length) rather than
    * stack-iterative, so skip/take never need threading back across a call boundary — each parent recomputes its own skip/take from `child.length`. A
    * ReverseNode recurses into the OPPOSITE direction windowed method with the SAME skip/take (the window then lands in the reversed frame). This is the
    * (skip,take)-windowed replacement for the deep-base applyBoxed loops in every traverser shape.
    */
  private final case class WinSpec(
      name: String, // this method's name (e.g. reduceWindowFwdIntInt)
      other: String, // opposite-direction windowed method (for the ReverseNode arm)
      tparam: String, // "<Z> " / "<RO> " / ""
      retT: String, // return type (Z / int / long / void / je)
      retState: String, // the value to `return` (e.g. "acc" / "o" / "(((long)ob)<<32)|…" / "" for void)
      stateDecl: String, // state+fn params AFTER "FBase node, int skip, int take" (e.g. ", Z acc, …Fold f")
      stateArgs: String, // the state arg(s) passed on a recursive call BETWEEN skip/take and the fn (", acc")
      fnArgs: String, // the fn arg(s) to pass on a recursive call (e.g. ", f")  (state captured separately)
      backward: Boolean,
      capture: (String) => String, // wrap a recursive sub-call returning the new state, e.g. v => s"acc = $v;"
      act: String => Unit, // per-element action on an element expr (mutates the state locals)
      actRun: (String, String, String) => Unit, // leaf sub-run over arr[start, start±count) in THIS direction
      isInt: Boolean, // emit the RangeNode arm (Int input only)
      prelude: () => Unit = () => () // method-body lines BEFORE the take<=0 guard (e.g. unpack packed cursors)
  ) {

    /** the recursive-call expression into THIS direction's windowed method (state captured by `capture`). */
    def recurExpr(child: String, cskip: String, ctake: String, stateArgs: String): String =
      s"$name($child, $cskip, $ctake$stateArgs$fnArgs)"
    def recurOtherExpr(child: String, cskip: String, ctake: String, stateArgs: String): String =
      s"$other($child, $cskip, $ctake$stateArgs$fnArgs)"
  }

  /** Emit ONE windowed sub-traverser from a WinSpec — the (skip,take)-windowed replacement for the deep-base applyBoxed loops.
    * `<ret> name(FBase node, int skip, int take <state>)` walks `node` but APPLIES the per-element action ONLY to the elements in [skip, skip+take) in THIS
    * direction's frame (a Bwd window counts `skip` from the END), early-RETURNING the instant `take` hits 0. It is RECURSIVE: each composite node is decomposed
    * into ordered SEGMENTS (visit order: fwd left→right / base→fillers / prefix→elem→suffix; bwd mirrors), each segment being a recursion (known child length),
    * a single element, or a filler run; a shared per-segment routine consumes `skip`/`take` so children get child-local sub-windows and `skip` collapses to 0
    * after the first partially-covered segment. ReverseNode recurses into the OPPOSITE-direction window with the SAME skip/take (the window then lands in the
    * reversed frame). Slice/Updated express their base as a SLICE of the base (base position `off+p` fwd / `baseLen-off-sn+p` bwd) so the one composition rule
    * covers them.
    */
  private def emitWindow(e: Emit, k: Kind, s: WinSpec): Unit = {
    val K = k.name
    val je = jelem(k)
    val backward = s.backward
    val voidRet = s.retT == "void"
    def doReturn(): Unit = e.line(if voidRet then "return;" else s"return ${s.retState};")

    // --- per-segment window consumers (mutate skip/take; `act`/recurse only on the in-window slice). ---
    // a recursion segment: the child node `child`, of length `clen`, occupies the next `clen` visit positions.
    // We recurse into it with a child-LOCAL sub-window. `childWin(segSkip, segTake)` yields the (cskip, ctake)
    // pair IN THE CHILD'S OWN this-direction frame (identity for a plain child; offset for a slice-of-base).
    def recurSeg(child: String, clen: String, childCSkip: (String) => String): Unit = e.scope {
      e.line(s"int _L = $clen;")
      e.line("int _ss = skip < _L ? skip : _L;")
      e.line("int _st = _L - _ss; if (_st > take) _st = take;")
      e.open("if (_st > 0)")
      e.line(s"${s.capture(s.recurExpr(child, childCSkip("_ss"), "_st", s.stateArgs))}")
      e.close()
      e.line("skip -= _ss; take -= _st;")
    }
    // a single-element segment.
    def elemSeg(elem: String): Unit = {
      e.open("if (skip > 0)")
      e.line("skip -= 1;")
      e.closeOpen("else")
      s.act(elem)
      e.line("take -= 1;")
      e.close()
    }
    // a filler run of length `flen` (all the same value `filler`).
    def fillerSeg(filler: String, flen: String): Unit = e.scope {
      e.line(s"int _fL = $flen;")
      e.line("int _fs = skip < _fL ? skip : _fL;")
      e.line("int _ft = _fL - _fs; if (_ft > take) _ft = take;")
      e.open("if (_ft > 0)")
      e.line("int _fj = 0;")
      e.open("while (_fj < _ft)")
      s.act(filler)
      e.line("_fj += 1;")
      e.close()
      e.close()
      e.line("skip -= _fs; take -= _ft;")
    }
    // guard each segment after the first with an early-out once take hits 0.
    def stop(): Unit = { e.open("if (take <= 0)"); doReturn(); e.close() }

    e.open(s"static ${s.tparam}${s.retT} ${s.name}(FBase node, int skip, int take${s.stateDecl})")
    s.prelude()
    e.line("if (take <= 0)" + (if voidRet then " return;" else s" return ${s.retState};"))
    e.line("FBase cur = node;")
    // leaf
    e.open(s"if (cur instanceof ${K}Arr leaf)")
    e.line(s"$je[] wa = leaf.arr;")
    e.line("int c = leaf.length;")
    e.open("if (skip < c)")
    e.line("int avail = c - skip;")
    e.line("int n = take < avail ? take : avail;")
    if !backward then s.actRun("wa", "skip", "n")
    else s.actRun("wa", "c - 1 - skip", "n")
    e.close()
    // flatMap node: flatten to one contiguous leaf, then the SAME windowed leaf run (a window may span segments).
    e.closeOpen(s"else if (cur instanceof ${K}FlatMap fm)")
    e.line(s"${K}Arr ffl = fm.flat();")
    e.line(s"$je[] wa = ffl.arr;")
    e.line("int c = ffl.length;")
    e.open("if (skip < c)")
    e.line("int avail = c - skip;")
    e.line("int n = take < avail ? take : avail;")
    if !backward then s.actRun("wa", "skip", "n")
    else s.actRun("wa", "c - 1 - skip", "n")
    e.close()
    // one
    e.closeOpen(s"else if (cur instanceof ${K}One one)")
    e.open("if (skip == 0)")
    s.act("one.elem")
    e.close()
    // prepend: fwd = elem then base; bwd = base then elem
    e.closeOpen(s"else if (cur instanceof ${K}Prepend pp)")
    if !backward then {
      elemSeg("pp.elem"); stop()
      recurSeg("pp.base", "pp.base.length", identity)
    } else {
      recurSeg("pp.base", "pp.base.length", identity); stop()
      elemSeg("pp.elem")
    }
    // append: fwd = base then elem; bwd = elem then base
    e.closeOpen(s"else if (cur instanceof ${K}Append ap)")
    if !backward then {
      recurSeg("ap.base", "ap.base.length", identity); stop()
      elemSeg("ap.elem")
    } else {
      elemSeg("ap.elem"); stop()
      recurSeg("ap.base", "ap.base.length", identity)
    }
    // concat: fwd = left then right; bwd = right then left
    e.closeOpen("else if (cur instanceof Concat cc)")
    if !backward then {
      recurSeg("cc.left", "cc.left.length", identity); stop()
      recurSeg("cc.right", "cc.right.length", identity)
    } else {
      recurSeg("cc.right", "cc.right.length", identity); stop()
      recurSeg("cc.left", "cc.left.length", identity)
    }
    // reverse: recurse into the OPPOSITE-direction window with the SAME skip/take (window lands in reversed frame)
    e.closeOpen("else if (cur instanceof ReverseNode rev)")
    e.line(s"${s.capture(s.recurOtherExpr("rev.base", "skip", "take", s.stateArgs))}")
    // slice: leaf base = a direct clipped run; deep base = a windowed sub-traverse of the base restricted to
    // base[so, so+sn). The slice's THIS-direction position p maps to base index so+p (fwd) / so+sn-1-p (bwd);
    // expressed as a base this-direction window: fwd base-pos so+skip; bwd base reversed-pos (baseLen-so-sn)+skip.
    e.closeOpen("else if (cur instanceof SliceNode sl)")
    e.line("int so = sl.offset;")
    e.line("int sn = sl.length;")
    e.open(s"if (sl.base instanceof ${K}Arr lf)")
    e.line(s"$je[] wa = lf.arr;")
    e.open("if (skip < sn)")
    e.line("int avail = sn - skip;")
    e.line("int n = take < avail ? take : avail;")
    if !backward then s.actRun("wa", "so + skip", "n")
    else s.actRun("wa", "so + sn - 1 - skip", "n")
    e.close()
    e.closeOpen("else")
    sliceDeep(e, s, "sl.base", "so", "sn")
    e.close()
    // pad: fwd = base then fillers; bwd = fillers then base
    e.closeOpen(s"else if (cur instanceof ${K}Pad pad)")
    e.line("int bl = pad.base.length;")
    e.line(s"$je pf = pad.filler;")
    e.line("int fillCount = pad.length - bl;")
    if !backward then {
      recurSeg("pad.base", "bl", identity); stop()
      fillerSeg("pf", "fillCount")
    } else {
      fillerSeg("pf", "fillCount"); stop()
      recurSeg("pad.base", "bl", identity)
    }
    // updated: fwd = prefix base[0,ui) , elem , suffix base[ui+1,ul) ; bwd = suffix(rev) , elem , prefix(rev).
    // Prefix/suffix are SLICES of base, windowed via sliceDeep (or a leaf run if base is a leaf).
    e.closeOpen(s"else if (cur instanceof ${K}Updated u)")
    e.line("int ui = u.index;")
    e.line("int ul = u.length;")
    e.open(s"if (u.base instanceof ${K}Arr lf2)")
    e.line(s"$je[] la = lf2.arr;")
    if !backward then {
      updLeafRun(e, s, "la", "0", "ui"); stop()
      elemSeg("u.elem"); stop()
      updLeafRun(e, s, "la", "ui + 1", "ul - ui - 1")
    } else {
      updLeafRun(e, s, "la", "ui + 1", "ul - ui - 1"); stop()
      elemSeg("u.elem"); stop()
      updLeafRun(e, s, "la", "0", "ui")
    }
    e.closeOpen("else")
    if !backward then {
      sliceDeep(e, s, "u.base", "0", "ui"); stop()
      elemSeg("u.elem"); stop()
      sliceDeep(e, s, "u.base", "ui + 1", "ul - ui - 1")
    } else {
      sliceDeep(e, s, "u.base", "ui + 1", "ul - ui - 1"); stop()
      elemSeg("u.elem"); stop()
      sliceDeep(e, s, "u.base", "0", "ui")
    }
    e.close()
    if s.isInt then {
      e.closeOpen("else if (cur instanceof RangeNode rng)")
      e.line("int rn = rng.length;")
      e.open("if (skip < rn)")
      e.line("int avail = rn - skip;")
      e.line("int n = take < avail ? take : avail;")
      if !backward then {
        e.line("int ri = skip;")
        e.line("int re = skip + n;")
        e.open("while (ri < re)")
        s.act("rng.start + ri * rng.step")
        e.line("ri += 1;")
        e.close()
      } else {
        e.line("int ri = rn - 1 - skip;")
        e.line("int re = ri - n;")
        e.open("while (ri > re)")
        s.act("rng.start + ri * rng.step")
        e.line("ri -= 1;")
        e.close()
      }
      e.close()
    }
    e.close() // node-kind dispatch
    if !voidRet then e.line(s"return ${s.retState};")
    e.close() // method
  }

  /** Emit a windowed sub-traverse of `base` RESTRICTED to base[off, off+len) — i.e. a deep SliceNode base or an Updated prefix/suffix. Composes a base
    * this-direction window: fwd base-position `off+skip`; bwd base reversed-position `(baseLen-off-len)+skip`. `skip`/`take` are this-segment-local on entry;
    * on exit they are decremented by the consumed amounts (so a following segment continues correctly).
    */
  private def sliceDeep(e: Emit, s: WinSpec, base: String, off: String, len: String): Unit = e.scope {
    e.line(s"int _so = $off;")
    e.line(s"int _sn = $len;")
    e.line("int _ss = skip < _sn ? skip : _sn;")
    e.line("int _st = _sn - _ss; if (_st > take) _st = take;")
    e.open("if (_st > 0)")
    if !s.backward then e.line(s"${s.capture(s.recurExpr(base, "_so + _ss", "_st", s.stateArgs))}")
    else {
      e.line(s"int _bl = $base.length;")
      e.line(s"${s.capture(s.recurExpr(base, "_bl - _so - _sn + _ss", "_st", s.stateArgs))}")
    }
    e.close()
    e.line("skip -= _ss; take -= _st;")
  }

  /** Emit a leaf-array run for an Updated prefix/suffix (base IS a leaf): act on arr[start, start+len) clipped to the window, consuming skip/take.
    * Direction-aware (fwd ascending / bwd descending mirror).
    */
  private def updLeafRun(e: Emit, s: WinSpec, arr: String, start: String, len: String): Unit = e.scope {
    e.line(s"int _rs = $start;")
    e.line(s"int _rn = $len;")
    e.line("int _ss = skip < _rn ? skip : _rn;")
    e.line("int _st = _rn - _ss; if (_st > take) _st = take;")
    e.open("if (_st > 0)")
    if !s.backward then s.actRun(arr, "_rs + _ss", "_st")
    else s.actRun(arr, "_rs + _rn - 1 - _ss", "_st")
    e.close()
    e.line("skip -= _ss; take -= _st;")
  }

  /** The per-traverser pieces the ITERATIVE MAIN WALK needs — the single-source spec for the un-windowed, explicit-stack lowering (the mirror of `WinSpec`,
    * which single-sources the recursive windowed lowering). The main walk is the node-arm ORDER (leaf / One / Prepend / Append / Concat / Reverse / Slice / Pad
    * / Updated / Range) over one lazy `FBase[] stack`; a deferred Append/Prepend element rides as a `${K}One` pushed on that same stack (no separate tail
    * array). ONLY ReverseNode recurses (into the opposite-direction method). Per-shape knobs are callbacks (`act`, `runLeaf`, `run`, `winCall`, the ReverseNode
    * recursion). The HOOKS the OTHER shapes will need are designed in now (Reduce uses the no-op versions):
    *   - `cursorDecl`/`cursorArgs` — an OPTIONAL output-cursor threaded BESIDE the state (Build/Partition write an out[] and return the bumped cursor `o`;
    *     Reduce has none, both empty).
    *   - `earlyStopAfterAct` — an OPTIONAL early-stop hook emitted after a per-element action (ShortCircuit breaks out of the walk; Reduce no-ops).
    *   - the WRITE direction is intentionally NOT baked into `backward`: a shape whose write-cursor advances opposite the visit direction (Scan) supplies its
    *     own `runLeaf`/`run`/`act` that write in the write frame while the walk visits in `backward`. Reduce's write dir == visit dir, so its callbacks just
    *     use `backward`.
    */
  private final case class WalkSpec(
      sig: String, // the method signature (e.g. "static <Z> Z reduceFwdRefRef(FBase root, Z acc, …f)")
      K: String, // input kind name (node-class prefix: ${K}Arr / ${K}One / …)
      isInt: Boolean, // emit the RangeNode arm (Int input only)
      retState: String, // the value to `return` at the end (e.g. "acc" / "o"); "" for a void walk
      recurReverse: String => Unit, // the ReverseNode arm: given "rev.base", emit the opposite-direction call
      act: String => Unit, // per-element action on an element expr (One/Prepend·elem/Append·elem/Pad·filler/Range)
      runLeaf: (String, String) => Unit, // a WHOLE-leaf run over the named array up to the leaf's LOGICAL length (may be < arr.length; slack)
      run: (String, String, String) => Unit, // a leaf window run over arr[start, start±count) (Slice/Pad/Updated)
      winCall: (String, String, String) => Unit, // deep-base window hand-off: node[lo, lo+len) in THIS direction
      cursorDecl: String = "", // OPTIONAL output-cursor decl, threaded beside the state (Build/Partition); "" here
      cursorArgs: String = "", // OPTIONAL output-cursor arg(s) on a recursive call; "" here
      earlyStopAfterAct: () => Unit = () => (), // OPTIONAL early-stop hook after a per-element act (ShortCircuit); no-op here
      prelude: () => Unit = () => (), // OPTIONAL extra local decls emitted after `int sp = 0;` (Partition's two int cursors); no-op here
      prependVar: String = "p" // the Prepend arm's pattern-binding name; "pr" for shapes whose fn param is named `p` (Filter/Partition)
  )

  /** Emit the ITERATIVE MAIN WALK from a `WalkSpec` — the single source for the un-windowed, explicit-stack lowering (generalizing `reduceTraverser`'s former
    * body verbatim: same node arms, same lazy `FBase[]`/ `tail[]`/`isTail[]` stack, same `winCall` hand-off, same pop loop). `backward` mirrors the VISIT
    * order. This emits the method from its `sig` through its closing brace; the caller emits any trailing windowed traverser.
    */
  private def emitMainWalk(e: Emit, spec: WalkSpec, backward: Boolean): Unit = {
    val K = spec.K
    // the Java element type for this input kind (Pad filler / Updated leaf-array element type): each primitive's
    // jt; Ref -> Object.
    val je = prims.find(_.name == K).map(_.jt).getOrElse("Object")
    // grow the single FBase[] child stack; a deferred Append/Prepend element rides as a ${K}One node.
    def ensureStack(): Unit = {
      e.open("if (stack == null)")
      e.line("stack = new FBase[16];")
      e.closeOpen("else if (sp == stack.length)")
      e.line("int nl = sp * 2;")
      e.line("stack = java.util.Arrays.copyOf(stack, nl);")
      e.close()
    }
    def pushOne(elem: String): Unit = {
      ensureStack()
      e.line(s"stack[sp] = new ${K}One($elem);")
      e.line("sp += 1;")
    }
    def pushChild(child: String): Unit = {
      ensureStack()
      e.line(s"stack[sp] = $child;")
      e.line("sp += 1;")
    }

    e.open(spec.sig)
    e.line("FBase cur = root;")
    e.line("FBase[] stack = null;")
    e.line("int sp = 0;")
    spec.prelude()
    e.open("while (cur != null)")
    e.line("FBase next = null;")
    // leaf
    e.open(s"if (cur instanceof ${K}Arr leaf)")
    spec.runLeaf("leaf.arr", "leaf.length")
    // flatMap node: run each inner segment as a whole-leaf run. runLeaf emits the per-segment loop in `backward`
    // direction; only the segment iteration order flips. `foffs[i+1]-foffs[i]` = segment i's LOGICAL length.
    e.closeOpen(s"else if (cur instanceof ${K}FlatMap fm)")
    e.line(s"$je[][] fsegs = fm.segs;")
    e.line("int[] foffs = fm.offs;")
    if !backward then e.open("for (int fsi = 0; fsi < fsegs.length; fsi++)")
    else e.open("for (int fsi = fsegs.length - 1; fsi >= 0; fsi--)")
    spec.runLeaf("fsegs[fsi]", "foffs[fsi + 1] - foffs[fsi]")
    e.close()
    // one
    e.closeOpen(s"else if (cur instanceof ${K}One one)")
    spec.act("one.elem")
    // prepend: fwd elem-then-base; bwd defer elem, descend base first
    val pv = spec.prependVar
    e.closeOpen(s"else if (cur instanceof ${K}Prepend $pv)")
    if !backward then {
      spec.act(s"$pv.elem")
      e.line(s"next = $pv.base;")
    } else {
      pushOne(s"$pv.elem")
      e.line(s"next = $pv.base;")
    }
    // append: fwd defer elem, descend base first; bwd elem-then-base
    e.closeOpen(s"else if (cur instanceof ${K}Append ap)")
    if !backward then {
      pushOne("ap.elem")
      e.line("next = ap.base;")
    } else {
      spec.act("ap.elem")
      e.line("next = ap.base;")
    }
    // concat: fwd left-then-right; bwd right-then-left
    e.closeOpen("else if (cur instanceof Concat c)")
    if !backward then {
      pushChild("c.right")
      e.line("next = c.left;")
    } else {
      pushChild("c.left")
      e.line("next = c.right;")
    }
    // reverse: the ONLY recursion — flip direction across the JVM call boundary
    e.closeOpen("else if (cur instanceof ReverseNode rev)")
    spec.recurReverse("rev.base")
    // slice: leaf-base window fast path; deep base via the windowed sub-traverse
    e.closeOpen("else if (cur instanceof SliceNode s)")
    e.line("int so = s.offset;")
    e.line("int sn = s.length;")
    e.open(s"if (s.base instanceof ${K}Arr lf)")
    if !backward then spec.run("lf.arr", "so", "sn")
    else spec.run("lf.arr", "so + sn - 1", "sn")
    e.closeOpen("else")
    spec.winCall("s.base", "so", "sn")
    e.close()
    // pad: fwd base-then-fillers; bwd fillers-then-base
    e.closeOpen(s"else if (cur instanceof ${K}Pad pad)")
    e.line("int bl = pad.base.length;")
    e.line(s"$je pf = pad.filler;")
    e.line("int pn = pad.length;")
    if !backward then {
      e.open(s"if (pad.base instanceof ${K}Arr lf)")
      spec.run("lf.arr", "0", "bl")
      e.closeOpen("else")
      spec.winCall("pad.base", "0", "bl")
      e.close()
      e.line("int pj = bl;")
      e.open("while (pj < pn)")
      spec.act("pf")
      e.line("pj += 1;")
      e.close()
    } else {
      e.line("int pj = pn;")
      e.open("while (pj > bl)")
      spec.act("pf")
      e.line("pj -= 1;")
      e.close()
      e.open(s"if (pad.base instanceof ${K}Arr lf)")
      spec.run("lf.arr", "bl - 1", "bl")
      e.closeOpen("else")
      spec.winCall("pad.base", "0", "bl")
      e.close()
    }
    // updated: fwd [0,ui) elem (ui,len); bwd mirror. leaf-base fast path; deep base via the windowed sub-traverse.
    e.closeOpen(s"else if (cur instanceof ${K}Updated u)")
    e.line("int ui = u.index;")
    e.line("int ul = u.length;")
    e.open(s"if (u.base instanceof ${K}Arr lf)")
    e.line(s"$je[] la = lf.arr;")
    if !backward then {
      spec.run("la", "0", "ui")
      spec.act("u.elem")
      spec.run("la", "ui + 1", "ul - ui - 1")
    } else {
      spec.run("la", "ul - 1", "ul - ui - 1")
      spec.act("u.elem")
      spec.run("la", "ui - 1", "ui")
    }
    e.closeOpen("else")
    if !backward then {
      spec.winCall("u.base", "0", "ui")
      spec.act("u.elem")
      spec.winCall("u.base", "ui + 1", "ul - ui - 1")
    } else {
      spec.winCall("u.base", "ui + 1", "ul - ui - 1")
      spec.act("u.elem")
      spec.winCall("u.base", "0", "ui")
    }
    e.close()
    // range (Int-only structural node — emitted only for Int input)
    if spec.isInt then {
      e.closeOpen("else if (cur instanceof RangeNode rng)")
      e.line("int rn = rng.length;")
      if !backward then {
        e.line("int ri = 0;")
        e.open("while (ri < rn)")
        spec.act("rng.start + ri * rng.step")
        e.line("ri += 1;")
        e.close()
      } else {
        e.line("int ri = rn - 1;")
        e.open("while (ri >= 0)")
        spec.act("rng.start + ri * rng.step")
        e.line("ri -= 1;")
        e.close()
      }
    }
    e.close()
    // advance: descend into `next`, else pop the explicit stack (a popped ${K}One acts as an ordinary node).
    e.open("if (next != null)")
    e.line("cur = next;")
    e.closeOpen("else")
    e.line("cur = null;")
    e.open("while (sp > 0 && cur == null)")
    e.line("sp -= 1;")
    e.line("cur = stack[sp];")
    e.close()
    e.close()
    e.close() // while (cur != null)
    if spec.retState.nonEmpty then e.line(s"return ${spec.retState};")
    e.close() // method
  }

  /** Emit one Reduce push traverser over input kind `k`. `refAcc` picks the function type (Z=Ref generic vs Z=${k} unboxed) and the acc type; `backward`
    * mirrors visit order. Per §4.1: Concat/Append/Prepend deferrals + deep Slice/Pad/Updated bases ride a LOCAL explicit stack (lazily allocated — null until
    * the first defer); a bare-leaf walk never touches it. ONLY ReverseNode recurses — fwd calls bwd on rev.base (and vice versa), so the direction flip rides
    * the JVM call boundary. (Deep bases over a NON-leaf use applyBoxed — minimal but correct; the leaf-base fast path is unboxed.)
    */
  private def reduceTraverser(e: Emit, k: Kind, backward: Boolean, refAcc: Boolean): Unit = {
    val K = k.name
    val je = jelem(k) // Java element type: int/long/double/Object
    val isRefInput = K == "Ref"
    val dir = if backward then "Bwd" else "Fwd"
    val other = if backward then "Fwd" else "Bwd"
    val foldT = if refAcc then s"${K}ToRefFold<Z>" else s"${K}To${K}Fold"
    val sig =
      if refAcc then s"static <Z> Z reduce${dir}${K}Ref(FBase root, Z acc, $foldT f)"
      else s"static $je reduce${dir}${K}${K}(FBase root, $je acc, $foldT f)"
    val recur = if refAcc then s"reduce${other}${K}Ref" else s"reduce${other}${K}${K}"
    // Read a single boxed Object from a deep (non-leaf) base, cast to the element type. For prims the cast
    // unboxes via the boxed wrapper's value method; for Ref the element IS Object (pass-through).
    def fromBoxed(expr: String): String = fromBoxedK(k, expr)

    // a WHOLE-leaf run over `arr[0, len)` where `len` is the leaf's LOGICAL length (may be < arr.length — leaves
    // can carry slack so structural ops skip the trim copy). MEASURED (ArrLenFoldBenchmark, GraalVM + C2): the
    // `len` field bound over `a` ties the `a.length` baseline — loop-predication hoists `len <= a.length` out once
    // and the body stays a vectorized counted loop. (The compound `i < a.length && i < len` form defeats
    // vectorization — 0.48× on GraalVM — never use it.)
    def runLeaf(arr: String, len: String): Unit = e.scope {
      e.line(s"$je[] a = $arr;")
      if !backward then {
        e.line("int i = 0;")
        e.line(s"int end = $len;")
        e.open("while (i < end)")
        e.line("acc = f.apply(acc, a[i]);")
        e.line("i += 1;")
        e.close()
      } else {
        e.line(s"int i = ($len) - 1;")
        e.open("while (i >= 0)")
        e.line("acc = f.apply(acc, a[i]);")
        e.line("i -= 1;")
        e.close()
      }
    }
    // a forward array run over a[start, start+count); a backward run over a[start, start-count) descending.
    def run(arr: String, start: String, count: String): Unit = e.scope {
      e.line(s"$je[] a = $arr;")
      if !backward then {
        e.line(s"int i = $start;")
        e.line(s"int end = i + ($count);")
        e.open("while (i < end)")
        e.line("acc = f.apply(acc, a[i]);")
        e.line("i += 1;")
        e.close()
      } else {
        e.line(s"int i = $start;")
        e.line(s"int end = i - ($count);")
        e.open("while (i > end)")
        e.line("acc = f.apply(acc, a[i]);")
        e.line("i -= 1;")
        e.close()
      }
    }
    // deep (non-leaf) base read, per element, via applyBoxed (cast/unbox to the element type).
    def runBoxed(base: String, start: String, count: String): Unit = e.scope {
      if !backward then {
        e.line(s"int bi = $start;")
        e.line(s"int bend = bi + ($count);")
        e.open("while (bi < bend)")
        e.line(s"acc = f.apply(acc, ${fromBoxed(s"$base.applyBoxed(bi)")});")
        e.line("bi += 1;")
        e.close()
      } else {
        e.line(s"int bi = $start;")
        e.line(s"int bend = bi - ($count);")
        e.open("while (bi > bend)")
        e.line(s"acc = f.apply(acc, ${fromBoxed(s"$base.applyBoxed(bi)")});")
        e.line("bi -= 1;")
        e.close()
      }
    }

    // --- the (skip,take)-windowed sub-traverser for deep (non-leaf) Slice/Pad/Updated bases (replaces runBoxed). ---
    val accT = if refAcc then "Z" else je
    val winName = s"reduceWindow${dir}${K}${if refAcc then "Ref" else K}"
    val winOther = s"reduceWindow${other}${K}${if refAcc then "Ref" else K}"
    val win = WinSpec(
      name = winName,
      other = winOther,
      tparam = if refAcc then "<Z> " else "",
      retT = accT,
      retState = "acc",
      stateDecl = s", $accT acc, $foldT f",
      stateArgs = ", acc",
      fnArgs = ", f",
      backward = backward,
      capture = v => s"acc = $v;",
      act = el => e.line(s"acc = f.apply(acc, $el);"),
      actRun = (arr, start, count) => run(arr, start, count),
      isInt = K == "Int"
    )
    // a deep-base window call from the MAIN traverser: act on `node`[lo, lo+len) (node's index space), in THIS
    // direction. fwd window skip = lo; bwd window skip = node.length - lo - len (the reversed-frame offset).
    def winCall(node: String, lo: String, len: String): Unit =
      if !backward then e.line(win.capture(win.recurExpr(node, lo, len, ", acc")))
      else e.line(win.capture(win.recurExpr(node, s"$node.length - ($lo) - ($len)", len, ", acc")))

    // Build a Reduce WalkSpec and emit the iterative main walk from it (the single-sourced emitter). Reduce's
    // per-element action is `acc = f.apply(acc, e)`; state is `acc`; the ReverseNode arm recurses into the
    // opposite-direction reduce method; a deferred Append/Prepend element rides as a ${K}One on the stack and is
    // acted by the One arm. No output-cursor, no early-stop, and write dir == visit dir (the run/runLeaf/act
    // callbacks all use `backward` directly).
    val spec = WalkSpec(
      sig = sig,
      K = K,
      isInt = K == "Int",
      retState = "acc",
      recurReverse = base => e.line(s"acc = $recur($base, acc, f);"),
      act = el => e.line(s"acc = f.apply(acc, $el);"),
      runLeaf = (arr, len) => runLeaf(arr, len),
      run = (arr, start, count) => run(arr, start, count),
      winCall = (node, lo, len) => winCall(node, lo, len)
    )
    emitMainWalk(e, spec, backward)
    e.blank()
    emitWindow(e, k, win)
  }

  /** Emit one Build (map) push traverser over (input kind `ki`, output kind `ko`). The SAME lazy-stack walk skeleton as `reduceTraverser`, but instead of
    * threading an accumulator it WRITES `out[o] = f.apply(elem)` and bumps `o`; it returns the final cursor `o`. `backward` mirrors the READ order (Concat
    * r-then-l, runs descending, …) yet the cursor `o` still advances ascending — so a ReverseNode visited from the Fwd build calls the Bwd build (reading
    * last-to-first), which fills out[o..] in reversed-source order, exactly as `reverse.map` requires. Deep-base Slice/Pad/Updated read per-index via
    * applyBoxed (later pass replaces those — do NOT change here). `out` is `O[]` (prim) / `Object[]` (Ref); `f` is the (I,O) Build SAM.
    */
  private def buildTraverser(e: Emit, ki: Kind, ko: Kind, backward: Boolean): Unit = {
    val KI = ki.name
    val jiE = jelem(ki) // input Java element type: int/long/double/Object
    val joE = jelem(ko) // output Java element type: int/long/double/Object
    val dir = if backward then "Bwd" else "Fwd"
    val other = if backward then "Fwd" else "Bwd"
    // Only a Ref OUTPUT carries a type param (RO); a Ref input element is plain Object.
    val roArg = "RO"
    val fnT = buildFnType(ki, ko, roArg)
    val tparams = if !ko.isPrim then List("RO") else Nil
    val tpClause = if tparams.isEmpty then "" else s"<${tparams.mkString(", ")}> "
    val outT = if ko.isPrim then s"$joE[]" else "Object[]"
    val name = s"build${dir}${KI}${ko.name}"
    // recursion into the opposite-direction build (the only recursion). RO is inferred from `out`/`f`.
    val recur = s"build${other}${KI}${ko.name}"
    val sig = s"static ${tpClause}int $name(FBase root, $outT out, int o, $fnT f)"
    // write one element: out[o] = f.apply(<elem>); o += 1.  For a Ref OUTPUT the SAM returns RO (a generic
    // erased to Object) — store straight into the Object[]. For a Ref INPUT the leaf array IS Object[] and
    // `f.apply` takes RI (erased to Object), so the read is a pass-through.
    def writeElem(elem: String): Unit = {
      e.line(s"out[o] = f.apply($elem);")
      e.line("o += 1;")
    }
    def fromBoxed(expr: String): String = fromBoxedK(ki, expr)
    // a WHOLE-leaf run over `arr[0, len)` (LOGICAL length; may be < arr.length — slack). The cursor `o`
    // advances ascending in BOTH directions; only the READ index walks descending for the Bwd build.
    def runLeaf(arr: String, len: String): Unit = e.scope {
      e.line(s"$jiE[] a = $arr;")
      if !backward then {
        e.line("int i = 0;")
        e.line(s"int end = $len;")
        e.open("while (i < end)")
        writeElem("a[i]")
        e.line("i += 1;")
        e.close()
      } else {
        e.line(s"int i = ($len) - 1;")
        e.open("while (i >= 0)")
        writeElem("a[i]")
        e.line("i -= 1;")
        e.close()
      }
    }
    // a leaf window over a[start, start±count).
    def run(arr: String, start: String, count: String): Unit = e.scope {
      e.line(s"$jiE[] a = $arr;")
      if !backward then {
        e.line(s"int i = $start;")
        e.line(s"int end = i + ($count);")
        e.open("while (i < end)")
        writeElem("a[i]")
        e.line("i += 1;")
        e.close()
      } else {
        e.line(s"int i = $start;")
        e.line(s"int end = i - ($count);")
        e.open("while (i > end)")
        writeElem("a[i]")
        e.line("i -= 1;")
        e.close()
      }
    }
    // --- the (skip,take)-windowed sub-traverser for deep Slice/Pad/Updated bases (replaces runBoxed). ---
    val winName = s"buildWindow${dir}${KI}${ko.name}"
    val winOther = s"buildWindow${other}${KI}${ko.name}"
    val win = WinSpec(
      name = winName,
      other = winOther,
      tparam = tpClause,
      retT = "int",
      retState = "o",
      stateDecl = s", $outT out, int o, $fnT f",
      stateArgs = ", out, o",
      fnArgs = ", f",
      backward = backward,
      capture = v => s"o = $v;",
      act = el => writeElem(el),
      actRun = (arr, start, count) => run(arr, start, count),
      isInt = KI == "Int"
    )
    def winCall(node: String, lo: String, len: String): Unit =
      if !backward then e.line(win.capture(win.recurExpr(node, lo, len, ", out, o")))
      else e.line(win.capture(win.recurExpr(node, s"$node.length - ($lo) - ($len)", len, ", out, o")))

    // Build a Build WalkSpec and emit the iterative main walk from it (the shared emitter). Per-element action is
    // `out[o] = f.apply(e); o += 1`; state returned is the cursor `o`; the ReverseNode arm recurses into the
    // opposite-direction build threading `o`; a deferred Append/Prepend element rides as a ${KI}One on the stack
    // and is written by the One arm. Write dir == visit dir (the run/runLeaf/writeElem callbacks all use `backward`
    // directly).
    val spec = WalkSpec(
      sig = sig,
      K = KI,
      isInt = KI == "Int",
      retState = "o",
      recurReverse = base => e.line(s"o = $recur($base, out, o, f);"),
      act = el => writeElem(el),
      runLeaf = (arr, len) => runLeaf(arr, len),
      run = (arr, start, count) => run(arr, start, count),
      winCall = (node, lo, len) => winCall(node, lo, len)
    )
    emitMainWalk(e, spec, backward)
    e.blank()
    emitWindow(e, ki, win)
  }

  /** Emit one Build-filtered (filter) push traverser over input kind `k`. The SAME lazy-stack walk skeleton as `buildTraverser`, but the write is
    * predicate-GUARDED: `if (p.apply(elem)) { out[o] = elem; o += 1; }`. The kept-count cursor `o` advances ascending in BOTH directions; `backward` mirrors
    * the READ order only. The ReverseNode arm is the ONLY recursion: it bounces into the OPPOSITE-direction filter (Fwd <-> Bwd), so a reversed subtree is READ
    * last-to-first while the WRITE stays forward out[o++] — keeping the SAME kept elements in REVERSED order, exactly as `reverse.filter` requires (the
    * scan-style decoupling: visit dir flips at the ReverseNode, write dir is fixed forward; filter has no per-slot acc so it is simpler — only the read order
    * changes). Deep-base Slice/Pad/Updated read per-index via applyBoxed (later pass replaces those — do NOT change here). `out` is the `${K}[]` max-size
    * array; `p` is the `${K}Pred` SAM. Fwd is the public entry `buildFiltered${K}`; Bwd is the reverse-arm helper `buildFilteredBwd${K}`. Returns the
    * kept-count cursor.
    */
  private def buildFilteredTraverser(e: Emit, k: Kind, backward: Boolean): Unit = {
    val K = k.name
    val je = jelem(k) // input Java element type: int/long/double/Object
    val name = if backward then s"buildFilteredBwd${K}" else s"buildFiltered${K}"
    val recur = if backward then s"buildFiltered${K}" else s"buildFilteredBwd${K}"
    val sig = s"static int $name(FBase root, $je[] out, int o, ${K}Pred p)"
    // predicate-guarded write: keep + advance only on a hit. The cursor `o` advances ascending in BOTH directions.
    def writeElem(elem: String): Unit = e.scope {
      e.line(s"$je _e = $elem;")
      e.open("if (p.apply(_e))")
      e.line("out[o] = _e;")
      e.line("o += 1;")
      e.close()
    }
    def fromBoxed(expr: String): String = fromBoxedK(k, expr)
    // a WHOLE-leaf run over the LOGICAL length `len` (may be < arr.length — slack). The READ index walks descending for the Bwd filter; the cursor advances asc.
    def runLeaf(arr: String, len: String): Unit = e.scope {
      e.line(s"$je[] a = $arr;")
      if !backward then {
        e.line("int i = 0;")
        e.line(s"int end = $len;")
        e.open("while (i < end)")
        writeElem("a[i]")
        e.line("i += 1;")
        e.close()
      } else {
        e.line(s"int i = ($len) - 1;")
        e.open("while (i >= 0)")
        writeElem("a[i]")
        e.line("i -= 1;")
        e.close()
      }
    }
    // a leaf window over a[start, start±count).
    def run(arr: String, start: String, count: String): Unit = e.scope {
      e.line(s"$je[] a = $arr;")
      if !backward then {
        e.line(s"int i = $start;")
        e.line(s"int end = i + ($count);")
        e.open("while (i < end)")
        writeElem("a[i]")
        e.line("i += 1;")
        e.close()
      } else {
        e.line(s"int i = $start;")
        e.line(s"int end = i - ($count);")
        e.open("while (i > end)")
        writeElem("a[i]")
        e.line("i -= 1;")
        e.close()
      }
    }
    // --- the (skip,take)-windowed sub-traverser for deep Slice/Pad/Updated bases (replaces runBoxed). ---
    val winName = if backward then s"buildFilteredWindowBwd${K}" else s"buildFilteredWindow${K}"
    val winOther = if backward then s"buildFilteredWindow${K}" else s"buildFilteredWindowBwd${K}"
    val win = WinSpec(
      name = winName,
      other = winOther,
      tparam = "",
      retT = "int",
      retState = "o",
      stateDecl = s", $je[] out, int o, ${K}Pred p",
      stateArgs = ", out, o",
      fnArgs = ", p",
      backward = backward,
      capture = v => s"o = $v;",
      act = el => writeElem(el),
      actRun = (arr, start, count) => run(arr, start, count),
      isInt = K == "Int"
    )
    def winCall(node: String, lo: String, len: String): Unit =
      if !backward then e.line(win.capture(win.recurExpr(node, lo, len, ", out, o")))
      else e.line(win.capture(win.recurExpr(node, s"$node.length - ($lo) - ($len)", len, ", out, o")))

    // Build a BuildFiltered WalkSpec and emit the iterative main walk from it (the shared emitter). Per-element
    // action is the predicate-guarded keep `if (p.apply(e)) { out[o] = e; o += 1; }`; state returned is the kept
    // cursor `o`; the ReverseNode arm recurses into the opposite-direction filter threading `o` and `p`; a
    // deferred Append/Prepend element rides as a ${K}One on the stack and is written (guarded) by the One arm.
    // Write dir == visit dir.
    val spec = WalkSpec(
      sig = sig,
      K = K,
      isInt = K == "Int",
      retState = "o",
      recurReverse = base => e.line(s"o = $recur($base, out, o, p);"),
      act = el => writeElem(el),
      runLeaf = (arr, len) => runLeaf(arr, len),
      run = (arr, start, count) => run(arr, start, count),
      winCall = (node, lo, len) => winCall(node, lo, len),
      prependVar = "pr" // the fn param is named `p` (the predicate), so the Prepend binding is `pr` to avoid shadowing
    )
    emitMainWalk(e, spec, backward)
    e.blank()
    emitWindow(e, k, win)
  }

  /** Emit one Partition push traverser over input kind `k` — the DUAL-output sibling of `buildFilteredTraverser`. SAME lazy-stack walk skeleton, but each
    * element routes to outA[oa++] (predicate true) or outB[ob++] (false); BOTH cursors advance ascending in BOTH directions. `backward` mirrors the READ order
    * only. The ReverseNode arm is the ONLY recursion: it bounces into the OPPOSITE-direction partition (Fwd <-> Bwd), threading BOTH cursors via a PACKED long
    * (oa in the low 32 bits, ob in the high 32 bits), so a reversed subtree is READ last-to-first while BOTH WRITES stay forward — keeping the SAME split in
    * REVERSED order, exactly as `reverse.partition` requires. Returns the packed (oa, ob) long. Fwd entry `partitionFwd${K}` (Scala passes oa = ob = 0); Bwd is
    * the reverse-arm helper `partitionBwd${K}`. Deep-base Slice/Pad/Updated read per-index via applyBoxed (later pass).
    */
  private def partitionTraverser(e: Emit, k: Kind, backward: Boolean): Unit = {
    val K = k.name
    val je = jelem(k)
    val name = if backward then s"partitionBwd${K}" else s"partitionFwd${K}"
    val recur = if backward then s"partitionFwd${K}" else s"partitionBwd${K}"
    val sig = s"static long $name(FBase root, $je[] outA, $je[] outB, long oa0, long ob0, ${K}Pred p)"
    // dual predicate-routed write: keep into A on a hit, into B on a miss; the matching cursor advances ascending.
    def writeElem(elem: String): Unit = e.scope {
      e.line(s"$je _e = $elem;")
      e.open("if (p.apply(_e))")
      e.line("outA[oa] = _e;")
      e.line("oa += 1;")
      e.closeOpen("else")
      e.line("outB[ob] = _e;")
      e.line("ob += 1;")
      e.close()
    }
    def fromBoxed(expr: String): String = fromBoxedK(k, expr)
    def runLeaf(arr: String, len: String): Unit = e.scope {
      e.line(s"$je[] a = $arr;")
      if !backward then {
        e.line("int i = 0;")
        e.line(s"int end = $len;")
        e.open("while (i < end)")
        writeElem("a[i]")
        e.line("i += 1;")
        e.close()
      } else {
        e.line(s"int i = ($len) - 1;")
        e.open("while (i >= 0)")
        writeElem("a[i]")
        e.line("i -= 1;")
        e.close()
      }
    }
    def run(arr: String, start: String, count: String): Unit = e.scope {
      e.line(s"$je[] a = $arr;")
      if !backward then {
        e.line(s"int i = $start;")
        e.line(s"int end = i + ($count);")
        e.open("while (i < end)")
        writeElem("a[i]")
        e.line("i += 1;")
        e.close()
      } else {
        e.line(s"int i = $start;")
        e.line(s"int end = i - ($count);")
        e.open("while (i > end)")
        writeElem("a[i]")
        e.line("i -= 1;")
        e.close()
      }
    }
    // --- the (skip,take)-windowed sub-traverser for deep Slice/Pad/Updated bases (replaces runBoxed). ---
    val packedRet = "(((long) ob) << 32) | (((long) oa) & 0xffffffffL)"
    val winName = if backward then s"partitionWindowBwd${K}" else s"partitionWindowFwd${K}"
    val winOther = if backward then s"partitionWindowFwd${K}" else s"partitionWindowBwd${K}"
    val win = WinSpec(
      name = winName,
      other = winOther,
      tparam = "",
      retT = "long",
      retState = packedRet,
      stateDecl = s", $je[] outA, $je[] outB, long oa0, long ob0, ${K}Pred p",
      stateArgs = ", outA, outB, (long) oa, (long) ob",
      fnArgs = ", p",
      backward = backward,
      capture = v => s"{ long _pk = $v; oa = (int) (_pk & 0xffffffffL); ob = (int) (_pk >>> 32); }",
      act = el => writeElem(el),
      actRun = (arr, start, count) => run(arr, start, count),
      isInt = K == "Int",
      prelude = () => { e.line("int oa = (int) oa0;"); e.line("int ob = (int) ob0;") }
    )
    def winCall(node: String, lo: String, len: String): Unit =
      if !backward then e.line(win.capture(win.recurExpr(node, lo, len, ", outA, outB, (long) oa, (long) ob")))
      else e.line(win.capture(win.recurExpr(node, s"$node.length - ($lo) - ($len)", len, ", outA, outB, (long) oa, (long) ob")))

    // Build a Partition WalkSpec and emit the iterative main walk from it (the shared emitter). The prelude declares
    // the two int cursors `oa`/`ob` (from the packed long params) BESIDE the stack; per-element action routes the
    // element to outA/outB (the dual-write writeElem); state returned is the repacked (oa, ob) long; the ReverseNode
    // arm recurses into the opposite-direction partition threading BOTH cursors via the packed long; a deferred
    // Append/Prepend element rides as a ${K}One on the stack and is dual-written by the One arm. The fn param is
    // named `p` so the Prepend binding is `pr`. Write dir == visit dir.
    val spec = WalkSpec(
      sig = sig,
      K = K,
      isInt = K == "Int",
      retState = packedRet,
      recurReverse = base => {
        e.line(s"long _pk = $recur($base, outA, outB, (long) oa, (long) ob, p);")
        e.line("oa = (int) (_pk & 0xffffffffL);")
        e.line("ob = (int) (_pk >>> 32);")
      },
      act = el => writeElem(el),
      runLeaf = (arr, len) => runLeaf(arr, len),
      run = (arr, start, count) => run(arr, start, count),
      winCall = (node, lo, len) => winCall(node, lo, len),
      prelude = () => { e.line("int oa = (int) oa0;"); e.line("int ob = (int) ob0;") },
      prependVar = "pr"
    )
    emitMainWalk(e, spec, backward)
    e.blank()
    emitWindow(e, k, win)
  }

  /** Emit one Foreach push traverser over input kind `k`. The SAME lazy-stack walk skeleton as `reduceTraverser`, but it threads NO accumulator and produces NO
    * output — each element just calls `f.apply(elem)`. `backward` mirrors visit order (needed because a ReverseNode flips a subtree's local order — the Fwd
    * traverser recurses into the Bwd one). Deep-base Slice/Pad/Updated keep the per-index applyBoxed read (a later pass replaces those — do NOT change here).
    * The deferred-element stack mirrors reduceTraverser (Prepend fwd-defer / Append bwd-defer); a deferred tail slot APPLIES `f` to the deferred element on
    * pop. Returns void.
    */
  private def foreachTraverser(e: Emit, k: Kind, backward: Boolean): Unit = {
    val K = k.name
    val je = jelem(k) // int/long/double/Object
    val dir = if backward then "Bwd" else "Fwd"
    val other = if backward then "Fwd" else "Bwd"
    val sig = s"static void foreach${dir}${K}(FBase root, ${K}Consumer f)"
    val recur = s"foreach${other}${K}"
    def fromBoxed(expr: String): String = fromBoxedK(k, expr)
    // a WHOLE-leaf run over the LOGICAL length `len` (may be < arr.length — slack; see the reduce leaf note).
    def runLeaf(arr: String, len: String): Unit = e.scope {
      e.line(s"$je[] a = $arr;")
      if !backward then {
        e.line("int i = 0;")
        e.line(s"int end = $len;")
        e.open("while (i < end)")
        e.line("f.apply(a[i]);")
        e.line("i += 1;")
        e.close()
      } else {
