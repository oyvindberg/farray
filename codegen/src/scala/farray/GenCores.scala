package farray

import bleep.*
import bleep.internal.FileUtils

import java.nio.file.Files

/** Generates the sealed FArray core hierarchy: `FBase` (abstract storage-shuffling API + a concrete O(1) `concat` that builds a `Concat` tree node) + one
  * `<Name>Arr` leaf per primitive + the `Object[]`-backed `RefArr` leaf + the `Concat` tree node. Wired as `farray`'s sourcegen.
  *
  * Element-touching combinators and element-insert ops (`:+`/`+:`/`updated`) live in Scala (inline, specialized) — NOT here. Adding a primitive = one row in
  * `prims`.
  */
object GenCores extends BleepCodegenScript("GenCores") {

  final case class Prim(name: String, jt: String, boxed: String)

  val prims: List[Prim] = List(
    Prim("Int", "int", "Integer"),
    Prim("Long", "long", "Long"),
    Prim("Double", "double", "Double"),
    Prim("Float", "float", "Float"),
    Prim("Short", "short", "Short"),
    Prim("Byte", "byte", "Byte"),
    Prim("Char", "char", "Character"),
    Prim("Boolean", "boolean", "Boolean")
  )

  override def run(started: Started, commands: Commands, targets: List[GenCores.Target], args: List[String]): Unit =
    targets.foreach { target =>
      val dir = target.sources.resolve("farray")
      FileUtils.deleteDirectory(target.sources)
      Files.createDirectories(dir)
      Files.writeString(dir.resolve("FBase.java"), fbase)
      Files.writeString(dir.resolve("RefArr.java"), refArr)
      Files.writeString(dir.resolve("RefAppend.java"), refUnary("Append"))
      Files.writeString(dir.resolve("RefPrepend.java"), refUnary("Prepend"))
      Files.writeString(dir.resolve("Concat.java"), concat)
      Files.writeString(dir.resolve("RangeNode.java"), rangeNode)
      Files.writeString(dir.resolve("ReverseNode.java"), reverseNode)
      Files.writeString(dir.resolve("SliceNode.java"), sliceNode)
      Files.writeString(dir.resolve("Hashing.java"), hashing)
      Files.writeString(dir.resolve("Empty.java"), emptyNode)
      padKinds.foreach { case (name, jt, boxed) => Files.writeString(dir.resolve(s"${name}Pad.java"), padNode(name, jt, boxed)) }
      padKinds.foreach { case (name, jt, boxed) => Files.writeString(dir.resolve(s"${name}Updated.java"), updatedNode(name, jt, boxed)) }
      // ${K}One (array-free singleton) for EVERY prim kind + Ref — so any length-1 leaf/node canonicalizes to
      // its own-kind One (no boxing), never a generic fallback.
      oneKinds.foreach { case (name, jt, boxed) => Files.writeString(dir.resolve(s"${name}One.java"), oneNode(name, jt, boxed)) }
      prims.foreach { p =>
        Files.writeString(dir.resolve(s"${p.name}Arr.java"), primCore(p))
        Files.writeString(dir.resolve(s"${p.name}Append.java"), primAppend(p))
        Files.writeString(dir.resolve(s"${p.name}Prepend.java"), primPrepend(p))
      }
      Files.writeString(dir.resolve("Repr.scala"), reprSrc)
      Files.writeString(dir.resolve("FArrayOps.scala"), farrayOps)
    }

  /** Unified array-representation evidence — subsumes primitive specialization, value-class/opaque unwrapping (Prim + inline wrap/unwrap), and reference
    * ClassTag (RefRepr.ct). One per concrete A; absent for abstract A ⇒ summonFrom fails ⇒ compile error (specialize-or-fail).
    */
  private def reprSrc: String = {
    val subtraits = opKinds
      .map { k =>
        if k.name == "Ref" then "trait RefRepr[A] extends Repr[A] { type Prim = A }"
        else s"trait ${k.name}Repr[A] extends Repr[A] { type Prim = ${k.arr} }"
      }
      .mkString("\n")
    val givens = opKinds
      .map { k =>
        if k.name == "Ref" then "  given refRepr[A <: AnyRef]: RefRepr[A] with { inline def unwrap(a: A): A = a; inline def wrap(p: A): A = p }"
        else s"  given ${k.lc}Repr: ${k.name}Repr[${k.arr}] with { inline def unwrap(a: ${k.arr}): ${k.arr} = a; inline def wrap(p: ${k.arr}): ${k.arr} = p }"
      }
      .mkString("\n")
    s"""package farray
       |
       |// GENERATED by GenCores — do not edit.
       |sealed trait Repr[A] {
       |  type Prim
       |  inline def unwrap(a: A): Prim
       |  inline def wrap(p: Prim): A
       |}
       |$subtraits
       |
       |object Repr {
       |$givens
       |}
       |""".stripMargin
  }

  // ===== generated per-kind Scala combinators (the read×write matrix lives here, not hand-written) =====

  /** name=core prefix (Int/Long/Double/Ref); arr=Scala array elem type; pat=erasedValue pattern; dflt=zero. */
  /** the primitive boxing facet of a Kind (absent ⇒ a reference): the boxed wrapper type plus how to box (`to`) and unbox (`from`) the underlying `prim` value.
    */
  final case class Box(wrapper: String, prim: String) {
    def to(v: String): String = s"$wrapper.valueOf($v)" // box:   prim -> Object
    def from(v: String): String = s"scala.runtime.BoxesRunTime.unboxTo$prim($v)" // unbox: Object -> prim
  }

  /** specCh: the JVM erasure descriptor char — I/J/D for primitives, L (object) for references — which is also the char in stdlib @specialized class names
    * (Tuple2$mcII$sp etc.). prim: present iff a primitive.
    */
  final case class Kind(name: String, arr: String, pat: String, dflt: String, specCh: String, prim: Option[Box]) {
    def lc = name.toLowerCase
    def isPrim: Boolean = prim.isDefined

    /** box a `$arr`-typed value to an Object (pass-through for a reference). */
    def box(v: String): String = prim match { case Some(b) => b.to(v); case None => v }

    /** unbox an Object expression to the element type (auto-unboxes for prims; cast for references). */
    def unbox(v: String): String = prim match { case Some(b) => b.from(v); case None => s"($v).asInstanceOf[$arr]" }
  }

  // Start with the common numeric kinds + reference; add rows here to cover more primitives.
  val opKinds: List[Kind] = List(
    Kind("Int", "Int", "Int", "0", "I", Some(Box("java.lang.Integer", "Int"))),
    Kind("Long", "Long", "Long", "0L", "J", Some(Box("java.lang.Long", "Long"))),
    Kind("Double", "Double", "Double", "0.0", "D", Some(Box("java.lang.Double", "Double"))),
    Kind("Ref", "Object", "AnyRef", "null", "L", None)
  )

  private val grow =
    "if (sp + 2 > stack.length) { val ns = stack.length * 2; stack = java.util.Arrays.copyOf(stack, ns); tail = java.util.Arrays.copyOf(tail, ns); isTail = java.util.Arrays.copyOf(isTail, ns) }"

  // ===== the codegen-only traversal AST (design §A) =====
  // These case classes / enums live ONLY in the generator. They are the reification of the
  // (direction, holes, accumulator, output, early-exit) tuple that `dfsBody`'s holes carry implicitly.
  // `lower(spec)` consumes a `Trav` and emits the op-method body; nothing here ever appears in generated code.

  enum Dir { case Fwd, Bwd }

  /** The per-element work, expressed ONCE as a code template. `perElem(e)` reads the current element via the
    * code fragment `e` (already wrapped through `read(k)` unless `rawArray`), and reads/writes the accumulator
    * locals directly. `rawArray = true` reads `a(i)` unwrapped (sum/product). */
  final case class Body(perElem: String => String, rawArray: Boolean = false)

  /** An accumulator threaded as a plain `var` LOCAL (a JVM register), not a heap field. */
  final case class Acc(name: String, tpe: String, init: String)

  /** The early-exit shape, which also selects the leaf run-loop shape (design §A.2):
    *   Never        -> the runMethods full-run loop (process every element)
    *   OnPredInCond -> the scanB empty-body predicate-in-condition loop (Wave 2; structured here, unused now) */
  enum Exit {
    case Never
    case OnPredInCond(cont: String => String, onStop: String, onOne: String)
  }

  /** Output construction (design §A). Wave 1 needs NoOut (foreach) and Scalar (fold/sum/product/count).
    * PrimArray / ResultFArray are reserved for Wave 3 (map/filter/scanLeft) and are not consumed by `lower` yet. */
  enum Out {
    case NoOut
    case Scalar(read: String)
    case PrimArray(elemKind: Kind, write: (String, String) => String)
    case ResultFArray(elemKind: Kind, sizeHint: SizeHint, write: (String, String) => String, fin: Finalize)
  }

  enum SizeHint { case Exact(expr: String); case AtMost(expr: String) }
  enum Finalize { case LeafExact; case LeafTrim; case FilterIdentity }

  /** One traversal, fully described. `k` is the element Kind (the row dispatchA/dispatchB resolved to). */
  final case class Trav(k: Kind, dir: Dir, body: Body, acc: List[Acc], exit: Exit, out: Out)

  // The traversal body for ONE direction (forward when backward=false, reverse when backward=true), parameterized
  // by how an array-backed RUN (onRF forward / onRB backward) and a single element (onO) are consumed. The two
  // directions are exact mirrors and MUTUALLY RECURSIVE: a ReverseNode is visited by CALLING the opposite-direction
  // traversal on its base (the JVM stack carries that nesting — only literal reverse-of-reverse recurses, which the
  // smart constructor collapses, so depth is ~0-1). Each direction keeps its OWN local explicit stack purely for
  // its own Concat/Append/Prepend deferrals (a Reverse subtree is self-contained, so it never spills into the
  // caller's stack). This kills the materialize/kindAt fallbacks for backward ops over trees.
  //   forward : Concat l-then-r · Append defer-elem · Prepend elem-then-base · runs ascending · Pad base-then-fill
  //   backward: Concat r-then-l · Append elem-then-base · Prepend defer-elem · runs descending · Pad fill-then-base
  // `onRev` fills the ReverseNode arm. Default = today's driver-recursive form (the non-inline dfsC pair carries
  // the opposite direction on the JVM stack). The inline `lower` emitter passes a self-contained form instead
  // (reverse-of-leaf = an opposite-direction window; reverse-of-non-leaf = materialize then opposite-direction
  // run), because once inlined there is no `c` and no driver to call (design §B.6).
  private def dfsBody(
      k: Kind,
      backward: Boolean,
      onRF: (String, String, String) => String,
      onRB: (String, String, String) => String,
      onO: String => String,
      onRev: ((String, String, String) => String, (String, String, String) => String, String => String) => String = null
  ): String = {
    // a forward run over a leaf window emits onRF(arr, start, count); a backward run emits onRB(arr, start, count)
    // where start is the FIRST index visited (high end for backward).
    def runFwd(a: String, start: String, count: String): String = onRF(a, start, count)
    def runBwd(a: String, start: String, count: String): String = onRB(a, start, count)
    val other = if backward then "Fwd" else "Back"
    val rngCase =
      if k.name != "Int" then ""
      else if !backward then
        s"\n       |        case rng: RangeNode => { val rn = rng.length; var ri = 0; while (ri < rn) { ${onO("rng.start + ri * rng.step")}; ri += 1 } }"
      else
        s"\n       |        case rng: RangeNode => { val rn = rng.length; var ri = rn - 1; while (ri >= 0) { ${onO("rng.start + ri * rng.step")}; ri -= 1 } }"
    val padRead = if k.name == "Ref" then "pad.base.applyBoxed(pi)" else s"${k.lc}At(pad.base, pi)"
    // The explicit stack holds deferred Concat children (the common case) and Append/Prepend tail elements
    // (rare). `stack` carries the FBase children; `tail`/`isTail` are allocated LAZILY only once a tail element
    // is actually deferred — so a pure Concat tree (the overwhelmingly common shape) pays ONE small array, not
    // three. The pop loop reads a slot as a tail element only when `isTail` exists and the flag is set.
    // ensureStack: room for one more `stack` slot, growing all live arrays in lockstep so indices stay aligned.
    val ensureStack =
      s"if (stack == null) stack = new Array[FBase](16) else if (sp == stack.length) { val nl = sp * 2; stack = java.util.Arrays.copyOf(stack, nl); if (isTail != null) { tail = java.util.Arrays.copyOf(tail, nl); isTail = java.util.Arrays.copyOf(isTail, nl) } }"
    // ensureTail: lazily materialize `tail`/`isTail` to match `stack.length` the first time a tail is deferred.
    val ensureTail = s"if (isTail == null) { tail = new Array[${k.arr}](stack.length); isTail = new Array[Boolean](stack.length) }"
    val pushTail = (e: String) => s"$ensureStack; $ensureTail; tail(sp) = $e; isTail(sp) = true; sp += 1"
    // --- per-node arms (mirrored by `backward`) ---
    val oneCase = s"case o: ${k.name}One => ${onO("o.elem")}"
    // pattern var is `pp` (not `p`): once `lower` splices an op body inline, a bare `p` would collide with the
    // user predicate `p` (count/filter). `pp` can't collide with any op-body lambda name. Local & order-preserving.
    val prependCase =
      if !backward then s"case pp: ${k.name}Prepend => { ${onO("pp.elem")}; cont = pp.base }"
      else s"case pp: ${k.name}Prepend => { ${pushTail("pp.elem")}; cont = pp.base }"
    val appendCase =
      if !backward then s"case a: ${k.name}Append => { ${pushTail("a.elem")}; cont = a.base }"
      else s"case a: ${k.name}Append => { ${onO("a.elem")}; cont = a.base }"
    // Concat pushes an FBase child (never a tail). If a tail array exists, clear the stale flag at this slot —
    // a popped tail can leave `isTail(sp)` set, and we must not misread this Concat child as a tail element.
    val clearTail = "if (isTail != null) isTail(sp) = false"
    val concatCase =
      if !backward then s"case c: Concat => { $ensureStack; $clearTail; stack(sp) = c.right; sp += 1; cont = c.left }"
      else s"case c: Concat => { $ensureStack; $clearTail; stack(sp) = c.left; sp += 1; cont = c.right }"
    // ReverseNode: descend into base in the OPPOSITE direction via a plain call (JVM stack carries it). If the
    // sub-walk set `stop` (short-circuit), bail the whole walk too. (Default, used by the non-inline dfsC pair.)
    // When `onRev` is supplied (the inline `lower` emitter), the arm becomes self-contained — no `c`, no driver.
    val reverseCase =
      if onRev == null then s"case rev: ReverseNode => { dfsC${k.name}$other(rev.base, c); if (c.stop) return }"
      else s"case rev: ReverseNode => { ${onRev(onRF, onRB, onO)} }"
    // leaf / slice / pad-base / updated-segments: array-backed runs (forward asc, backward desc).
    val leafCase =
      if !backward then s"case leaf: ${k.name}Arr => ${runFwd("leaf.arr", "0", "leaf.length")}"
      else s"case leaf: ${k.name}Arr => ${runBwd("leaf.arr", "leaf.length - 1", "leaf.length")}"
    val sliceCase =
      if !backward then
        s"case s: SliceNode => { val sn = s.length; val so = s.offset; s.base match { case lf: ${k.name}Arr => ${runFwd("lf.arr", "so", "sn")}; case _ => { var si = 0; while (si < sn) { ${onO(s"${k.lc}At(s.base, so + si)")}; si += 1 } } } }"
      else
        s"case s: SliceNode => { val sn = s.length; val so = s.offset; s.base match { case lf: ${k.name}Arr => ${runBwd("lf.arr", "so + sn - 1", "sn")}; case _ => { var si = sn - 1; while (si >= 0) { ${onO(s"${k.lc}At(s.base, so + si)")}; si -= 1 } } } }"
    // Pad: forward = base then fillers; backward = fillers then base.
    val padCase =
      if !backward then
        s"case pad: ${k.name}Pad => { val bl = pad.base.length; pad.base match { case lf: ${k.name}Arr => ${runFwd("lf.arr", "0", "bl")}; case _ => { var pi = 0; while (pi < bl) { ${onO(padRead)}; pi += 1 } } }; val pf = pad.filler; var pj = bl; val pn = pad.length; while (pj < pn) { ${onO("pf")}; pj += 1 } }"
      else
        s"case pad: ${k.name}Pad => { val bl = pad.base.length; val pf = pad.filler; var pj = pad.length; while (pj > bl) { ${onO("pf")}; pj -= 1 }; pad.base match { case lf: ${k.name}Arr => ${runBwd("lf.arr", "bl - 1", "bl")}; case _ => { var pi = bl - 1; while (pi >= 0) { ${onO(padRead)}; pi -= 1 } } } }"
    // Updated: forward = [0,ui) , elem , (ui,len) ; backward mirrors.
    val updatedCase =
      if !backward then
        s"case u: ${k.name}Updated => { u.base match { case lf: ${k.name}Arr => { val la = lf.arr; val ui = u.index; ${runFwd("la", "0", "ui")}; ${onO("u.elem")}; ${runFwd("la", "ui + 1", "u.length - ui - 1")} }; case _ => { val mout = materialize${k.name}(u); ${runFwd("mout", "0", "mout.length")} } } }"
      else
        s"case u: ${k.name}Updated => { u.base match { case lf: ${k.name}Arr => { val la = lf.arr; val ui = u.index; ${runBwd("la", "u.length - 1", "u.length - ui - 1")}; ${onO("u.elem")}; ${runBwd("la", "ui - 1", "ui")} }; case _ => { val mout = materialize${k.name}(u); ${runBwd("mout", "mout.length - 1", "mout.length")} } } }"
    val cases = List(leafCase, oneCase, prependCase, appendCase, concatCase, reverseCase, padCase, updatedCase, sliceCase)
      .map(c => s"        $c")
      .mkString("\n       |") + rngCase
    s"""    var cur: FBase = root
       |    var stack: Array[FBase] = null; var tail: Array[${k.arr}] = null; var isTail: Array[Boolean] = null; var sp = 0
       |    while (cur != null) {
       |      var cont: FBase = null
       |      cur match {
       |$cases
       |        case _ => ()
       |      }
       |      if (cont != null) cur = cont
       |      else { cur = null; while (sp > 0 && cur == null) { sp -= 1; if (isTail != null && isTail(sp)) ${onO("tail(sp)")} else cur = stack(sp) } }
       |    }""".stripMargin
  }
  // ONE direction-aware DFS pair. An op that wants to stop early sets the consumer's `stop` flag (inside its run
  // loop), and the driver bails after each run/element. onRunF / onRunB are the forward/backward array-run handlers
  // (empty-body, constant-stride — the JIT-friendly shape); onOne handles genuine singletons.
  private def dfsConsumer(k: Kind): String =
    s"abstract class ${k.name}Dfs { var stop = false; def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit; def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit; def onOne(v: ${k.arr}): Unit }"
  private def dfsCDef(k: Kind): String = {
    def body(backward: Boolean): String =
      dfsBody(
        k,
        backward,
        (a, st, cnt) => s"{ c.onRunF($a, $st, $cnt); if (c.stop) return }",
        (a, st, cnt) => s"{ c.onRunB($a, $st, $cnt); if (c.stop) return }",
        args => s"{ c.onOne($args); if (c.stop) return }"
      )
    s"""  def dfsC${k.name}Fwd(root: FBase, c: ${k.name}Dfs): Unit = {
       |${body(false)}
       |  }
       |  def dfsC${k.name}Back(root: FBase, c: ${k.name}Dfs): Unit = {
       |${body(true)}
       |  }
       |  inline def dfsC${k.name}(root: FBase, c: ${k.name}Dfs): Unit = dfsC${k.name}Fwd(root, c)""".stripMargin
  }

  // The iterator is a trivial flat-array cursor (2 fields → escape analysis scalar-replaces it when consumed
  // inline → foreach speed). A leaf hands its backing array straight in (no copy, lazy); a tree is flattened
  // ONCE via the dfs we already have (materialize). So the tree-walk lives only in dfsBody — no second walker.
  private def cursorClass(k: Kind): String = {
    val nextBoxed = if k.name == "Ref" then s"next${k.name}()" else k.box(s"next${k.name}()")
    s"""final class ${k.name}Cursor(a: Array[${k.arr}], len: Int) extends scala.collection.AbstractIterator[Any] {
       |  private var pos = 0
       |  def hasNext: Boolean = pos < len
       |  override def knownSize: Int = len - pos
       |  def next${k.name}(): ${k.arr} = { val r = a(pos); pos += 1; r }
       |  def next(): Any = $nextBoxed
       |}""".stripMargin
  }

  // Per-kind growable, unboxed accumulator for groupBy/groupMap. One instance per group; `add` appends an
  // already-unwrapped Prim, doubling the backing array on demand (starts tiny: most groups are small). `toLeaf`
  // wraps the buffer into a leaf node WITHOUT a second per-element pass — it reuses the backing array verbatim
  // when full, else trims with a single Arrays.copyOf. This is what kills the old double-build: elements land
  // unboxed in their final array on the first (only) pass; finalisation is O(#groups), not O(n).
  private def groupBufClass(k: Kind): String = {
    val arrT = if k.name == "Ref" then "Object" else k.arr
    s"""final class ${k.name}Group {
       |  var arr: Array[$arrT] = new Array[$arrT](4)
       |  var size: Int = 0
       |  def add(v: $arrT): Unit = { if (size == arr.length) arr = java.util.Arrays.copyOf(arr, size * 2); arr(size) = v; size += 1 }
       |  def toLeaf: FBase = if (size == 0) Empty.INSTANCE else if (size == 1) new ${k.name}One(arr(0)) else new ${k.name}Arr(if (size == arr.length) arr else java.util.Arrays.copyOf(arr, size), size)
       |}""".stripMargin
  }

  private def atDef(k: Kind): String = {
    val rngCase = if k.name == "Int" then "\n       |    case rng: RangeNode => rng.start + i * rng.step" else ""
    val padBase = if k.name == "Ref" then "pad.base.applyBoxed(i)" else s"${k.lc}At(pad.base, i)"
    s"""  def ${k.lc}At(node: FBase, i: Int): ${k.arr} = node match {
       |    case leaf: ${k.name}Arr => leaf.arr(i)
       |    case o: ${k.name}One => o.elem
       |    case c: Concat => if (i < c.left.length) ${k.lc}At(c.left, i) else ${k.lc}At(c.right, i - c.left.length)
       |    case a: ${k.name}Append => if (i < a.base.length) ${k.lc}At(a.base, i) else a.elem
       |    case p: ${k.name}Prepend => if (i == 0) p.elem else ${k.lc}At(p.base, i - 1)
       |    case rev: ReverseNode => ${k.lc}At(rev.base, rev.base.length - 1 - i)
       |    case pad: ${k.name}Pad => if (i < pad.base.length) $padBase else pad.filler
       |    case u: ${k.name}Updated => if (i == u.index) u.elem else ${if k.name == "Ref" then "u.base.applyBoxed(i)" else s"${k.lc}At(u.base, i)"}
       |    case s: SliceNode => ${k.lc}At(s.base, s.offset + i)$rngCase
       |    case _ => ${k.dflt}
       |  }""".stripMargin
  }

  private def farrayOps: String = {
    val dfsConsumers = opKinds.map(dfsConsumer).mkString("\n")
    val cursors = opKinds.map(cursorClass).mkString("\n")
    val groupBufs = opKinds.map(groupBufClass).mkString("\n")
    val dfsC = opKinds.map(dfsCDef).mkString("\n")
    val ats = opKinds.map(atDef).mkString("\n")
    // Flatten a (possibly deep) Updated chain to a fresh leaf array, ONCE, so dfs can emit it tight via
    // onRunF instead of walking the chain per element. Recursion applies overrides bottom-up => outer wins.
    val mat = opKinds
      .map(k =>
        s"""  def materialize${k.name}(node: FBase): Array[${k.arr}] = node match {
         |    case u: ${k.name}Updated => { val out = materialize${k.name}(u.base); out(u.index) = u.elem; out }
         |    case _ => { val out = new Array[${k.arr}](node.length); var o = 0; dfsC${k.name}(node, new ${k.name}Dfs { def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { System.arraycopy(a, start, out, o, count); o += count }; def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e) { out(o) = a(i); o += 1; i -= 1 } }; def onOne(v: ${k.arr}): Unit = { out(o) = v; o += 1 } }); out }
         |  }""".stripMargin
      )
      .mkString("\n")
    // Stable bottom-up mergesort directly on a kind array, ping-ponging between two buffers (no per-level
    // copy-back). `less(x, y)` = x sorts strictly before y. Returns the sorted buffer (may be the input or temp).
    // non-inline per-kind flatMap result builder: dfs+System.arraycopy each inner into one flat array (dfs
    // inlined ONCE here, not at every flatMap call site). Ref takes the ClassTag so the array is typed.
    // non-inline flatMap helpers (compiled once, not re-inlined at every call site): grow the output array,
    // and copy ONE non-leaf inner into out (dfs).
    val flatMapCopyOne = opKinds
      .map(k =>
        s"""  def ensureCap${k.name}(out: Array[${k.arr}], needed: Int): Array[${k.arr}] =
         |    if (needed <= out.length) out else { var nc = out.length * 2; if (nc < needed) nc = needed; java.util.Arrays.copyOf(out, nc) }
         |  def flatMapCopyOne${k.name}(node: FBase, out: Array[${k.arr}], off0: Int): Unit = {
         |    var o = off0; dfsC${k.name}(node, new ${k.name}Dfs { def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { System.arraycopy(a, start, out, o, count); o += count }; def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e) { out(o) = a(i); o += 1; i -= 1 } }; def onOne(v: ${k.arr}): Unit = { out(o) = v; o += 1 } })
         |  }""".stripMargin
      )
      .mkString("\n")
    // Natural (run-adaptive) bottom-up mergesort. NON-inline (compiled once, not dumped at every sort site,
    // so multiple sorts in one method can't blow past the JIT method-size limit) with an unboxed primitive
    // comparator (${k.name}Less SAM — no boxing). Detects ascending runs and reverses strictly-descending
    // ones in place (already/reverse-sorted -> O(n)), then merges runs pairwise. Stable (merge prefers left;
    // ascending detection non-strict, descending strict). Ref sorts use java.util.Arrays.sort instead.
    val sortKinds = opKinds.filter(_.name != "Ref")
    val lessTraits = sortKinds.map(k => s"trait ${k.name}Less { def lt(a: ${k.arr}, b: ${k.arr}): Boolean }").mkString("\n")
    val sortArr = sortKinds
      .map(k =>
        s"""  def sort${k.name}(a: Array[${k.arr}], n: Int, less: ${k.name}Less): Array[${k.arr}] = {
         |    if (n < 2) a else {
         |      var sruns = new Array[Int](n + 1); var nr = 0; var i = 0
         |      while (i < n) {
         |        sruns(nr) = i; nr += 1; var j = i + 1
         |        if (j < n) {
         |          if (less.lt(a(j), a(i))) { j += 1; while (j < n && less.lt(a(j), a(j - 1))) j += 1; var lo = i; var hi = j - 1; while (lo < hi) { val t = a(lo); a(lo) = a(hi); a(hi) = t; lo += 1; hi -= 1 } }
         |          else { j += 1; while (j < n && !less.lt(a(j), a(j - 1))) j += 1 }
         |        }
         |        i = j
         |      }
         |      sruns(nr) = n
         |      var src = a; var dst = new Array[${k.arr}](n); var druns = new Array[Int](nr + 1)
         |      while (nr > 1) {
         |        var r = 0; var w = 0
         |        while (r < nr) {
         |          druns(w) = sruns(r); w += 1
         |          if (r + 1 < nr) {
         |            val lo = sruns(r); val mid = sruns(r + 1); val hi = sruns(r + 2)
         |            var x = lo; var y = mid; var k = lo
         |            while (x < mid && y < hi) { if (less.lt(src(y), src(x))) { dst(k) = src(y); y += 1 } else { dst(k) = src(x); x += 1 }; k += 1 }
         |            while (x < mid) { dst(k) = src(x); x += 1; k += 1 }
         |            while (y < hi) { dst(k) = src(y); y += 1; k += 1 }
         |            r += 2
         |          } else { val lo = sruns(r); System.arraycopy(src, lo, dst, lo, n - lo); r += 1 }
         |        }
         |        druns(w) = n
         |        val ts = src; src = dst; dst = ts; val tr = sruns; sruns = druns; druns = tr; nr = w
         |      }
         |      src
         |    }
         |  }""".stripMargin
      )
      .mkString("\n")

    // Reference reads cast the leaf array to Array[A] (checkcast hoisted out of the loop at the
    // concrete inline call site; empty leaves never evaluate it). Primitive arrays can't cast to
    // Array[A] (erases to Object[]), so they read directly.
    // Ref reads cast the ELEMENT (Object -> A), valid for any backing array — an Array[A] (i.e. Array[String])
    // cast would CCE on an Object[] backing (which materialize/sort produce, having no ClassTag).
    def read(k: Kind): String = if k.name == "Ref" then "r.wrap(a(i).asInstanceOf[A])" else "r.wrap(a(i))"
    def readOne(k: Kind): String = if k.name == "Ref" then "r.wrap(v.asInstanceOf[A])" else "r.wrap(v)"
    def alloc(k: Kind, rv: String): String =
      if k.name == "Ref" then s"new Array[Object](n)" else s"new Array[${k.arr}](n)"
    // a ref write stores r.Prim (= A, runtime <: Object) into an Object[] — cast it (no-op); prims store directly.
    def wr(k: Kind, e: String): String = if k.name == "Ref" then s"($e).asInstanceOf[Object]" else e
    // wrap a raw kind-array element back to A (for an inline user comparator); plain kind-array alloc
    def readVal(k: Kind, e: String): String = if k.name == "Ref" then s"r.wrap($e.asInstanceOf[A])" else s"r.wrap($e)"
    def allocPlain(k: Kind): String = s"new Array[${k.arr}](n)"
    // Canonicalizing leaf builder: a result array `arr` of length `len` becomes Empty (0) / ${K}One (1) / a
    // ${K}Arr leaf (>=2), upholding the size-0/1 invariant at every Scala build site. `len` is evaluated once.
    def leaf(k: Kind, arr: String, len: String): String =
      s"{ val _l = $len; if (_l == 0) Empty.INSTANCE else if (_l == 1) new ${k.name}One($arr(0)) else new ${k.name}Arr($arr, _l) }"
    // ---- run-method builders: emit the onRunF/onRunB pair for a consumer from ONE per-element body. `body`
    // reads the wrapped element via `read(k)` (which references `a(i)`); `i` runs start..start+count (fwd) or
    // start..start-count (bwd, descending). Two constant-stride loops keep the JIT-friendly shape.
    // fullRun: process EVERY element (no early stop). rawA=true reads a(i) directly (sum/product), else read(k).
    def runMethods(k: Kind, body: String => String, rawA: Boolean = false): String = {
      val rd = if rawA then "a(i)" else read(k)
      val fwd = s"def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start + count; while (i < e) { ${body(rd)}; i += 1 } }"
      val bwd = s"def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e) { ${body(rd)}; i -= 1 } }"
      s"$fwd; $bwd"
    }
    def dispatchA(body: Kind => String): String =
      "summonFrom {\n" + opKinds.map(k => s"      case r: ${k.name}Repr[A] => ${body(k)}").mkString("\n") + "\n    }"
    def dispatchB(body: Kind => String): String =
      "summonFrom {\n" + opKinds.map(k => s"      case r: ${k.name}Repr[B] => ${body(k)}").mkString("\n") + "\n    }"

    // wrap an arbitrary RAW kind-array-typed expression `e` back to A (mirrors readOne, which hardcodes `v`).
    // `rawArray` ops (sum/product) read the prim directly; Ref always casts the Object element to A then wraps.
    def wrapRaw(k: Kind, e: String, rawArray: Boolean): String =
      if rawArray then e
      else if k.name == "Ref" then s"r.wrap(($e).asInstanceOf[A])"
      else s"r.wrap($e)"

    // ===== the PUSH emitter (design §B): splice dfsBody INLINE with the op body in its holes =====
    // Accumulators become plain `var` LOCALS (no consumer object, no heap field); the per-element body is
    // spliced straight into the leaf/run loops (no onRunF call, no megamorphism); the ReverseNode arm and the
    // deep-base Slice/Pad/Updated arms are handled self-contained (reverse-of-leaf inline, else materialize).
    // Wave 1: Exit.Never + Out.Scalar / Out.NoOut only.
    def lower(spec: Trav): String = {
      val k = spec.k
      val backward = spec.dir == Dir.Bwd
      val rawA = spec.body.rawArray
      val rd = if rawA then "a(i)" else read(k) // the element read inside a constant-stride run loop (reads `a(i)`)
      spec.exit match {
        case Exit.Never =>
          // onRF/onRB = the runMethods full-run loops with `spec.body.perElem` spliced; onO = the single form.
          // The run array hole arrives as an arbitrary expression (`leaf.arr`, `lf.arr`, `la`, `mout`, …); bind
          // it to a local `a` so the `read(k)`/`a(i)` element fragment resolves to the right array.
          // `start`/`count` arrive as arbitrary expressions (e.g. `u.length - ui - 1`); parenthesize so the
          // `start + count` / `start - count` end-bound keeps the intended precedence (the old runMethods passed
          // them as plain method params `start`/`count`, so it never needed this).
          val onRF = (arr: String, start: String, count: String) =>
            s"{ val a = $arr; var i = $start; val e = ($start) + ($count); while (i < e) { ${spec.body.perElem(rd)}; i += 1 } }"
          val onRB = (arr: String, start: String, count: String) =>
            s"{ val a = $arr; var i = $start; val e = ($start) - ($count); while (i > e) { ${spec.body.perElem(rd)}; i -= 1 } }"
          // ALWAYS brace the single-element body: it splices into the pop loop's `if (isTail(sp)) <onO> else
          // cur = stack(sp)`, so a brace-less body (e.g. count's bare `if (p(..)) n += 1`) would steal that
          // trailing `else` (dangling-else) and drop the popped Concat child. Braces make the `else` bind right.
          val onO = (e: String) => s"{ ${spec.body.perElem(wrapRaw(k, e, rawA))} }"
          // ReverseNode arm, self-contained (design §B.6): the base is visited in the OPPOSITE direction.
          //   walk dir Fwd  -> base Bwd: reverse-of-leaf = a descending window; else materialize then run Bwd.
          //   walk dir Bwd  -> base Fwd: reverse-of-leaf = an ascending window;  else materialize then run Fwd.
          val onRev = (rf: (String, String, String) => String, rb: (String, String, String) => String, _o: String => String) =>
            if !backward then
              s"rev.base match { case lf: ${k.name}Arr => ${rb("lf.arr", "lf.length - 1", "lf.length")}; case _ => { val mout = materialize${k.name}(rev.base); ${rb("mout", "mout.length - 1", "mout.length")} } }"
            else
              s"rev.base match { case lf: ${k.name}Arr => ${rf("lf.arr", "0", "lf.length")}; case _ => { val mout = materialize${k.name}(rev.base); ${rf("mout", "0", "mout.length")} } }"
          val walk = dfsBody(k, backward, onRF, onRB, onO, onRev)
          // (3) accumulators as plain `var` locals, declared OUTSIDE the walk; (4) the Out epilogue.
          val accDecls = spec.acc.map(a => s"var ${a.name}: ${a.tpe} = ${a.init}").mkString("; ")
          val epilogue = spec.out match {
            case Out.Scalar(res) => s"; $res"
            case Out.NoOut       => ""
            case other           => sys.error(s"lower: Wave 1 does not support Out $other")
          }
          val accPart = if accDecls.isEmpty then "" else s"$accDecls\n"
          // `dfsBody`'s template starts at `root`; the op method's array is `xs`, so bind the alias once.
          s"{ val root: FBase = xs\n$accPart$walk$epilogue }"
        case other => sys.error(s"lower: Wave 1 does not support Exit $other")
      }
    }

    // ===== Wave 1 traversal specs (design §B.1). Each op delegates: dispatchA(k => lower(spec(k))). =====
    def foldLeftSpec(k: Kind): Trav =
      Trav(k, Dir.Fwd, Body(e => s"acc = op(acc, $e)"), List(Acc("acc", "Z", "z")), Exit.Never, Out.Scalar("acc"))
    // foldRight is the SAME spec with direction flipped and the op argument order swapped — a declaration, not a
    // hand-written mirror (the direction-mirroring dfsBody already encodes is now one field).
    def foldRightSpec(k: Kind): Trav =
      foldLeftSpec(k).copy(dir = Dir.Bwd, body = Body(e => s"acc = op($e, acc)"))
    def foreachSpec(k: Kind): Trav =
      Trav(k, Dir.Fwd, Body(e => s"f($e)"), Nil, Exit.Never, Out.NoOut)
    def countSpec(k: Kind): Trav =
      Trav(k, Dir.Fwd, Body(e => s"if (p($e)) n += 1"), List(Acc("n", "Int", "0")), Exit.Never, Out.Scalar("n"))
    def sumSpec(k: Kind): Trav =
      if k.prim.isDefined then
        Trav(k, Dir.Fwd, Body(e => s"acc += $e", rawArray = true), List(Acc("acc", k.arr, k.dflt)), Exit.Never, Out.Scalar("acc.asInstanceOf[B]"))
      else
        Trav(k, Dir.Fwd, Body(e => s"acc = num.plus(acc, ($e).asInstanceOf[B])", rawArray = true), List(Acc("acc", "B", "num.zero")), Exit.Never, Out.Scalar("acc"))
    def productSpec(k: Kind): Trav = {
      val one = k.name match { case "Long" => "1L"; case "Double" => "1.0"; case _ => "1" }
      if k.prim.isDefined then
        Trav(k, Dir.Fwd, Body(e => s"acc *= $e", rawArray = true), List(Acc("acc", k.arr, one)), Exit.Never, Out.Scalar("acc.asInstanceOf[B]"))
      else
        Trav(k, Dir.Fwd, Body(e => s"acc = num.times(acc, ($e).asInstanceOf[B])", rawArray = true), List(Acc("acc", "B", "num.one")), Exit.Never, Out.Scalar("acc"))
    }

    // Wave 1 (design §E): foldLeft/foreach now lower the Trav spec inline — no ${K}Dfs object, acc is a local.
    val foldLeft = dispatchA(k => lower(foldLeftSpec(k)))
    val foreach = dispatchA(k => lower(foreachSpec(k)))
    // Short-circuiting scan over the unified dfsC: each leaf-run scans tight; setting `stop` (the consumer flag)
    // ends both the run and the whole tree walk. O(n) + early-exit on EVERY shape, no kindAt.
    // EMPTY-BODY, predicate-in-condition scan: the per-element loop is just `i += 1` with the predicate in the
    // while condition, then we inspect WHY it stopped once, after the loop. No mid-loop res/stop write -> no
    // loop-carried dependency -> the only shape HotSpot optimizes to ~iarray speed (a body write is ~1.45x slower,
    // measured). `gi` is the run's global start; the leaf scans local i, so the found global index is gi + i.
    def scanB(k: Kind, init: String, cont: String => String, hit: (String, String) => String, oneStep: String, result: String): String = {
      val fwd = s"def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start + count; while (i < e && ${cont(read(k))}) i += 1; if (i < e) { val a0 = ${read(k)}; ${hit("a0", "gi + (i - start)")}; stop = true }; gi += count }"
      val bwd = s"def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e && ${cont(read(k))}) i -= 1; if (i > e) { val a0 = ${read(k)}; ${hit("a0", "gi + (start - i)")}; stop = true }; gi += count }"
      s"{ var gi = 0; $init; val c = new ${k.name}Dfs { $fwd; $bwd; def onOne(v: ${k.arr}): Unit = { val a0 = ${readOne(k)}; $oneStep; gi += 1 } }; dfsC${k.name}(xs, c); $result }"
    }
    val existsV = dispatchA(k => scanB(k, "var res = false", a0 => s"!p($a0)", (a0, _) => "res = true", "if (p(a0)) { res = true; stop = true }", "res"))
    val forallV = dispatchA(k => scanB(k, "var res = true", a0 => s"p($a0)", (a0, _) => "res = false", "if (!p(a0)) { res = false; stop = true }", "res"))
    val findV = dispatchA(k => scanB(k, "var res: Option[A] = None", a0 => s"!p($a0)", (a0, _) => s"res = Some($a0)", "if (p(a0)) { res = Some(a0); stop = true }", "res"))
    val indexWhereV = dispatchA(k => scanB(k, "var res = -1", a0 => s"!p($a0)", (_, gidx) => s"res = $gidx", "if (p(a0)) { res = gi; stop = true }", "res"))
    val indexOfV = dispatchA(k => scanB(k, "var res = -1", a0 => s"!($a0 == elem)", (_, gidx) => s"res = $gidx", "if (a0 == elem) { res = gi; stop = true }", "res"))
    val collectFirstV = dispatchA(k => scanB(k, "var res: Option[B] = None", a0 => s"!pf.isDefinedAt($a0)", (a0, _) => s"res = Some(pf($a0))", "if (pf.isDefinedAt(a0)) { res = Some(pf(a0)); stop = true }", "res"))
    // prefixLength accumulates (res += matched count) rather than setting an index, so it scans empty-body but
    // doesn't fit hit(): res += i after each run, stop only when a run has a non-match (i < len).
    val prefixLenV = dispatchA(k =>
      s"{ var res = 0; val c = new ${k.name}Dfs { def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start + count; while (i < e && p(${read(k)})) i += 1; res += (i - start); if (i < e) stop = true }; def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e && p(${read(k)})) i -= 1; res += (start - i); if (i > e) stop = true }; def onOne(v: ${k.arr}): Unit = { if (p(${readOne(k)})) res += 1 else stop = true } }; dfsC${k.name}(xs, c); res }"
    )
    // lastIndexWhere/lastIndexOf scan BACKWARD via dfsCBack — visiting elements last-to-first, so the FIRST hit is
    // the last occurrence. ZERO allocation on EVERY shape (no materialize): the direction-aware traversal turns a
    // reverse-of-tree into a forward walk, a tree into a real backward walk, etc. `gi` counts the global index
    // DOWN from length-1; a backward run starting at run-position `start` (high) maps run-local steps to gi - j.
    def lastBackwardScan(k: Kind, cont: String => String): String = {
      // onRunB drives backward array runs (start = highest index visited). onRunF can fire too (reverse-of-tree
      // flips a backward walk into forward sub-walks): there a forward run of `count` elements still consumes the
      // next `count` global indices counting DOWN, but visited LOW-to-HIGH, so the first forward hit is at the
      // LOWEST global index of that run -> gi - (count-1) + (i - start). We compute the hit's global index and,
      // because backward order means earlier-found == larger global index, just take it (stop on first hit).
      val rb = s"def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e && ${cont(read(k))}) i -= 1; if (i > e) { res = gi - (start - i); stop = true }; gi -= count }"
      val rf = s"def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start + count; while (i < e && ${cont(read(k))}) i += 1; if (i < e) { res = gi - (i - start); stop = true }; gi -= count }"
      s"{ var res = -1; var gi = xs.length - 1; val c = new ${k.name}Dfs { $rb; $rf; def onOne(v: ${k.arr}): Unit = { val a0 = ${readOne(k)}; if (!(${cont("a0")})) { res = gi; stop = true }; gi -= 1 } }; dfsC${k.name}Back(xs, c); res }"
    }
    val lastIndexWhereV = dispatchA(k => lastBackwardScan(k, a0 => s"!p($a0)"))
    val lastIndexOfV = dispatchA(k => lastBackwardScan(k, a0 => s"$a0 != elem"))
    // count/sum/product: Wave 1 — full forward accumulate, lowered inline. O(n) on EVERY shape, acc a local.
    val countV = dispatchA(k => lower(countSpec(k)))
    val sumV = dispatchA(k => lower(sumSpec(k)))
    val productV = dispatchA(k => lower(productSpec(k)))
    // scanLeft: out[0..n] with a running accumulator, unboxed B-array storage (mirrors mapM's dispatchA x dispatchB).
    val scanLeftV = "summonFrom {\n" + opKinds.map { ka =>
      val inner = "summonFrom {\n" + opKinds.map { kb =>
        s"          case rb: ${kb.name}Repr[B] => { val out = new Array[${kb.arr}](n + 1); var acc: B = z; out(0) = ${wr(kb, "rb.unwrap(z)")}; var o = 1; val c = new ${ka.name}Dfs { ${runMethods(ka, e => s"acc = op(acc, $e); out(o) = ${wr(kb, "rb.unwrap(acc)")}; o += 1")}; def onOne(v: ${ka.arr}): Unit = { acc = op(acc, ${readOne(ka)}); out(o) = ${wr(kb, "rb.unwrap(acc)")}; o += 1 } }; dfsC${ka.name}(xs, c); new ${kb.name}Arr(out, n + 1) }"
      }.mkString("\n") + "\n        }"
      s"      case r: ${ka.name}Repr[A] => $inner"
    }.mkString("\n") + "\n    }"
    // scanLeft size-0/1 short-circuit (no dfs): n==0 -> [z] (a ${B}One); n==1 -> [z, op(z, a0)] (a 2-leaf).
    // List-identical. Dispatched on B's repr so the storage stays unboxed.
    val scanLeftSmallV = "summonFrom {\n" + opKinds.map { kb =>
      val zEl = wr(kb, "rb.unwrap(z)")
      val one = s"new ${kb.name}One($zEl)"
      val alloc2 = if kb.name == "Ref" then "new Array[Object](2)" else s"new Array[${kb.arr}](2)"
      val two = s"{ val a1 = op(z, applyAtImpl[A](xs, 0)); val out = $alloc2; out(0) = $zEl; out(1) = ${wr(kb, "rb.unwrap(a1)")}; new ${kb.name}Arr(out, 2) }"
      s"      case rb: ${kb.name}Repr[B] => if (n == 0) $one else $two"
    }.mkString("\n") + "\n    }"
    val iteratorV = dispatchA(k =>
      s"xs match { case leaf: ${k.name}Arr => new ${k.name}Cursor(leaf.arr, leaf.length); case _ => new ${k.name}Cursor(materialize${k.name}(xs), xs.length) }"
    )
    val foreachWhileV = dispatchA(k =>
      s"{ val c = new ${k.name}Dfs { def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start + count; while (i < e && f(${read(k)})) i += 1; if (i < e) stop = true }; def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e && f(${read(k)})) i -= 1; if (i > e) stop = true }; def onOne(v: ${k.arr}): Unit = { if (!f(${readOne(k)})) stop = true } }; dfsC${k.name}(xs, c) }"
    ) // f returns false to stop
    // foldRight: Wave 1 — the SAME lowered walk with dir = Bwd (a BACKWARD walk hands elements last-to-first,
    // exactly foldRight order, applying op(elem, acc)). ZERO allocation on every shape, acc a local.
    val foldRightV = dispatchA(k => lower(foldRightSpec(k)))
    val mapM = "summonFrom {\n" + opKinds
      .map { ka =>
        val inner = "summonFrom {\n" + opKinds
          .map { kb =>
            s"          case rb: ${kb.name}Repr[B] => { val out = ${alloc(kb, "rb")}; var o = 0; val c = new ${ka.name}Dfs { ${runMethods(ka, e => s"out(o) = ${wr(kb, s"rb.unwrap(f($e))")}; o += 1")}; def onOne(v: ${ka.arr}): Unit = { out(o) = ${wr(kb, s"rb.unwrap(f(${readOne(ka)}))")}; o += 1 } }; dfsC${ka.name}(xs, c); new ${kb.name}Arr(out, n) }"
          }
          .mkString("\n") + "\n        }"
        s"      case r: ${ka.name}Repr[A] => $inner"
      }
