  /** Unboxed, order-dependent hash: dispatch on each node's runtime kind and hash raw primitives. Replaces the per-node applyBoxed loops (which boxed every
    * element). `rev` flips order for ReverseNode.
    */
  private def hashing: String = {
    // the element-hash expression for a kind by NAME (the single source of truth for every node shape). Matches
    // scala.runtime.Statics.##: Int/Short/Byte/Char hash to their widened value; Long/Double/Float/Boolean/Ref
    // use the dedicated hashers.
    def ehName(name: String, e: String): String = name match
      case "Long"    => s"scala.runtime.Statics.longHash($e)"
      case "Double"  => s"scala.runtime.Statics.doubleHash($e)"
      case "Float"   => s"scala.runtime.Statics.floatHash($e)"
      case "Boolean" => s"($e ? 1231 : 1237)"
      case "Ref"     => s"scala.runtime.Statics.anyHash($e)"
      case _         => e // Int/Short/Byte/Char: ## is the (widened) value
    def eh(p: Prim, e: String): String = ehName(p.name, e)
    val leafFill = prims
      .map(p =>
        s"""       |        if (node instanceof ${p.name}Arr a) { if (rev) for (int i = a.length - 1; i >= 0; i--) hs[pos++] = ${eh(
            p,
            "a.arr[i]"
          )}; else for (int i = 0; i < a.length; i++) hs[pos++] = ${eh(p, "a.arr[i]")}; return pos; }"""
      )
      .mkString("\n")
    val appendFill = prims
      .map(p =>
        s"""       |        if (node instanceof ${p.name}Append ap) { if (rev) { hs[pos++] = ${eh(
            p,
            "ap.elem"
          )}; return fill(ap.base, true, hs, pos); } else { pos = fill(ap.base, false, hs, pos); hs[pos++] = ${eh(p, "ap.elem")}; return pos; } }"""
      )
      .mkString("\n")
    val prependFill = prims
      .map(p =>
        s"""       |        if (node instanceof ${p.name}Prepend pp) { if (rev) { pos = fill(pp.base, true, hs, pos); hs[pos++] = ${eh(
            p,
            "pp.elem"
          )}; return pos; } else { hs[pos++] = ${eh(p, "pp.elem")}; return fill(pp.base, false, hs, pos); } }"""
      )
      .mkString("\n")
    val padFill = padKinds
      .map { case (name, _, _) =>
        val fh = ehName(name, "pad.filler")
        s"""       |        if (node instanceof ${name}Pad pad) { int extra = pad.length - pad.base.length; if (rev) { for (int i = 0; i < extra; i++) hs[pos++] = $fh; return fill(pad.base, true, hs, pos); } else { pos = fill(pad.base, false, hs, pos); for (int i = 0; i < extra; i++) hs[pos++] = $fh; return pos; } }"""
      }
      .mkString("\n")
    val updatedFill = padKinds
      .map { case (name, _, _) =>
        val eh2 = ehName(name, "u.elem")
        s"""       |        if (node instanceof ${name}Updated u) { int start = pos; pos = fill(u.base, rev, hs, pos); hs[rev ? (start + u.length - 1 - u.index) : (start + u.index)] = $eh2; return pos; }"""
      }
      .mkString("\n")
    val oneFill = oneKinds
      .map { case (name, _, _) =>
        val eh2 = ehName(name, "o.elem")
        s"""       |        if (node instanceof ${name}One o) { hs[pos] = $eh2; return pos + 1; }"""
      }
      .mkString("\n")
    val flatMapFill = padKinds
      .map { case (name, _, _) =>
        val ehE = ehName(name, "fm.segs[s][j]")
        s"""       |        if (node instanceof ${name}FlatMap fm) { int ns = fm.segs.length; if (rev) { for (int s = ns - 1; s >= 0; s--) { int sl = fm.offs[s + 1] - fm.offs[s]; for (int j = sl - 1; j >= 0; j--) hs[pos++] = $ehE; } } else { for (int s = 0; s < ns; s++) { int sl = fm.offs[s + 1] - fm.offs[s]; for (int j = 0; j < sl; j++) hs[pos++] = $ehE; } } return pos; }"""
      }
      .mkString("\n")
    s"""package farray;
       |
       |// GENERATED by GenCores — do not edit. Hash byte-for-byte equal to Scala Seq.hashCode (List/Vector):
       |// collect each element's ## (unboxed, via scala.runtime.Statics) into an int[], then run MurmurHash3's
       |// ordered hash INCLUDING its arithmetic-progression optimisation (a constant-stride hash sequence ->
       |// rangeHash). A standalone RangeNode hashes in O(1) via rangeHash (no million-element walk). The int[]
       |// holds primitive element-hashes — no element is ever boxed.
       |final class Hashing {
       |    private static final int SEED = "Seq".hashCode();
       |    static int hashOf(FBase node) {
       |        if (node instanceof RangeNode rg) {
       |            if (rg.length == 0) return finalizeHash(SEED, 0);
       |            if (rg.length == 1) return finalizeHash(mix(SEED, rg.start), 1);
       |            return rangeHash(rg.start, rg.step, rg.start + (rg.length - 1) * rg.step, SEED);
       |        }
       |        int[] hs = new int[node.length];
       |        fill(node, false, hs, 0);
       |        return ordered(hs);
       |    }
       |    private static int mix(int h, int d) { return scala.util.hashing.MurmurHash3$$.MODULE$$.mix(h, d); }
       |    private static int finalizeHash(int h, int n) { return scala.util.hashing.MurmurHash3$$.MODULE$$.finalizeHash(h, n); }
       |    private static int rangeHash(int start, int step, int last, int seed) { return scala.util.hashing.MurmurHash3$$.MODULE$$.rangeHash(start, step, last, seed); }
       |    /** Exactly scala.util.hashing.MurmurHash3.orderedHash, hoisted to an int[] of element-hashes. */
       |    private static int ordered(int[] h) {
