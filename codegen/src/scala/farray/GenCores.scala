package farray

import bleep.*
import bleep.internal.FileUtils

import java.nio.file.Files

/** Generates the sealed FArray core hierarchy: `FBase` (abstract storage-shuffling API + a concrete O(1) `concat` that builds a `Concat` tree node) + one
  * `<Name>Arr` leaf per primitive + the `Object[]`-backed `RefArr` leaf + the `Concat` tree node. Wired as `farray`'s sourcegen.
  *
  * Element-touching combinators and element-insert ops (`:+`/`+:`/`updated`) live in Scala (inline, specialized) — NOT here. Adding a primitive = one row in
  * `prims`.
  */
object GenCores extends BleepCodegenScript("GenCores") {

  final case class Prim(name: String, jt: String, boxed: String)

  val prims: List[Prim] = List(
    Prim("Int", "int", "Integer"),
    Prim("Long", "long", "Long"),
    Prim("Double", "double", "Double"),
    Prim("Float", "float", "Float"),
    Prim("Short", "short", "Short"),
    Prim("Byte", "byte", "Byte"),
    Prim("Char", "char", "Character"),
    Prim("Boolean", "boolean", "Boolean")
  )

  override def run(started: Started, commands: Commands, targets: List[GenCores.Target], args: List[String]): Unit =
    targets.foreach { target =>
      val dir = target.sources.resolve("farray")
      FileUtils.deleteDirectory(target.sources)
      Files.createDirectories(dir)
      Files.writeString(dir.resolve("FBase.java"), fbase)
      Files.writeString(dir.resolve("RefArr.java"), refArr)
      Files.writeString(dir.resolve("RefAppend.java"), refUnary("Append"))
      Files.writeString(dir.resolve("RefPrepend.java"), refUnary("Prepend"))
      Files.writeString(dir.resolve("Concat.java"), concat)
      Files.writeString(dir.resolve("RangeNode.java"), rangeNode)
      Files.writeString(dir.resolve("ReverseNode.java"), reverseNode)
      Files.writeString(dir.resolve("SliceNode.java"), sliceNode)
      Files.writeString(dir.resolve("Hashing.java"), hashing)
      Files.writeString(dir.resolve("Empty.java"), emptyNode)
      padKinds.foreach { case (name, jt, boxed) => Files.writeString(dir.resolve(s"${name}Pad.java"), padNode(name, jt, boxed)) }
      padKinds.foreach { case (name, jt, boxed) => Files.writeString(dir.resolve(s"${name}Updated.java"), updatedNode(name, jt, boxed)) }
      // ${K}One (array-free singleton) for EVERY prim kind + Ref — so any length-1 leaf/node canonicalizes to
      // its own-kind One (no boxing), never a generic fallback.
      oneKinds.foreach { case (name, jt, boxed) => Files.writeString(dir.resolve(s"${name}One.java"), oneNode(name, jt, boxed)) }
      // ${K}FlatMap (array-of-arrays flatMap result) for every prim kind + Ref.
      padKinds.foreach { case (name, jt, boxed) => Files.writeString(dir.resolve(s"${name}FlatMap.java"), flatMapNode(name, jt, boxed)) }
      prims.foreach { p =>
        Files.writeString(dir.resolve(s"${p.name}Arr.java"), primCore(p))
        Files.writeString(dir.resolve(s"${p.name}Append.java"), primAppend(p))
        Files.writeString(dir.resolve(s"${p.name}Prepend.java"), primPrepend(p))
      }
      Files.writeString(dir.resolve("Traversers.java"), traversersJava)
      Files.writeString(dir.resolve("Repr.scala"), reprSrc)
      Files.writeString(dir.resolve("FArrayOps.scala"), farrayOps)
    }

  /** Unified array-representation evidence — subsumes primitive specialization, value-class/opaque unwrapping (Prim + inline wrap/unwrap), and reference
    * ClassTag (RefRepr.ct). One per concrete A; absent for abstract A ⇒ summonFrom fails ⇒ compile error (specialize-or-fail).
    */
  private def reprSrc: String = {
    val subtraits = opKinds
      .map { k =>
        if k.name == "Ref" then "trait RefRepr[A] extends Repr[A] { type Prim = A }"
        else s"trait ${k.name}Repr[A] extends Repr[A] { type Prim = ${k.arr} }"
      }
      .mkString("\n")
    val givens = opKinds
      .map { k =>
        if k.name == "Ref" then "  given refRepr[A <: AnyRef]: RefRepr[A] with { inline def unwrap(a: A): A = a; inline def wrap(p: A): A = p }"
        else s"  given ${k.lc}Repr: ${k.name}Repr[${k.arr}] with { inline def unwrap(a: ${k.arr}): ${k.arr} = a; inline def wrap(p: ${k.arr}): ${k.arr} = p }"
      }
      .mkString("\n")
    s"""package farray
       |
       |// GENERATED by GenCores — do not edit.
       |sealed trait Repr[A] {
       |  type Prim
       |  inline def unwrap(a: A): Prim
       |  inline def wrap(p: Prim): A
       |}
       |$subtraits
       |
       |object Repr {
       |$givens
       |}
       |""".stripMargin
  }

  // ===== generated per-kind Scala combinators (the read×write matrix lives here, not hand-written) =====

  /** name=core prefix (Int/Long/Double/Ref); arr=Scala array elem type; pat=erasedValue pattern; dflt=zero. */
  /** the primitive boxing facet of a Kind (absent ⇒ a reference): the boxed wrapper type plus how to box (`to`) and unbox (`from`) the underlying `prim` value.
    */
  final case class Box(wrapper: String, prim: String) {
    def to(v: String): String = s"$wrapper.valueOf($v)" // box:   prim -> Object
    def from(v: String): String = s"scala.runtime.BoxesRunTime.unboxTo$prim($v)" // unbox: Object -> prim
  }

  /** specCh: the JVM erasure descriptor char — I/J/D for primitives, L (object) for references — which is also the char in stdlib @specialized class names
    * (Tuple2$mcII$sp etc.). prim: present iff a primitive.
    */
  final case class Kind(name: String, arr: String, pat: String, dflt: String, specCh: String, prim: Option[Box]) {
    def lc = name.toLowerCase
    def isPrim: Boolean = prim.isDefined

    /** box a `$arr`-typed value to an Object (pass-through for a reference). */
    def box(v: String): String = prim match { case Some(b) => b.to(v); case None => v }

    /** unbox an Object expression to the element type (auto-unboxes for prims; cast for references). */
    def unbox(v: String): String = prim match { case Some(b) => b.from(v); case None => s"($v).asInstanceOf[$arr]" }
  }

  // Every JVM primitive kind + reference. The ops (map/foldLeft/filter/scan/…) specialize over ALL of these.
  val opKinds: List[Kind] = List(
    Kind("Int", "Int", "Int", "0", "I", Some(Box("java.lang.Integer", "Int"))),
    Kind("Long", "Long", "Long", "0L", "J", Some(Box("java.lang.Long", "Long"))),
    Kind("Double", "Double", "Double", "0.0", "D", Some(Box("java.lang.Double", "Double"))),
    Kind("Float", "Float", "Float", "0.0f", "F", Some(Box("java.lang.Float", "Float"))),
    Kind("Short", "Short", "Short", "0", "S", Some(Box("java.lang.Short", "Short"))),
    Kind("Byte", "Byte", "Byte", "0", "B", Some(Box("java.lang.Byte", "Byte"))),
    Kind("Char", "Char", "Char", "'\\u0000'", "C", Some(Box("java.lang.Character", "Char"))),
    Kind("Boolean", "Boolean", "Boolean", "false", "Z", Some(Box("java.lang.Boolean", "Boolean"))),
    Kind("Ref", "Object", "AnyRef", "null", "L", None)
  )

  /** the primitive kinds only — used e.g. for the Ref-element + primitive-accumulator folds. */
  val primKinds: List[Kind] = opKinds.filter(_.isPrim)

  /** primitive kinds that have a `scala.math.Numeric` (so sum/product/scan-of-numeric work). Char/Boolean do not. */
  val numericKinds: List[Kind] = opKinds.filter(k => k.isPrim && k.name != "Char" && k.name != "Boolean")

  private val grow =
    "if (sp + 2 > stack.length) { val ns = stack.length * 2; stack = java.util.Arrays.copyOf(stack, ns); tail = java.util.Arrays.copyOf(tail, ns); isTail = java.util.Arrays.copyOf(isTail, ns) }"

  // ===== Emit: indent-aware line emitter for generated code (design §6) =====
  // Tracks an indent level; `line` appends one statement on its own line; `open`/`close` bracket a
  // block (emitting the trailing `{` / a `}` and bumping the indent). HARD REQUIREMENT: every call
  // produces ONE statement per line at real indentation — never `a; b; c` crammed together.
  final class Emit(indentStr: String = "    ") {
    private val sb = new StringBuilder
    private var level = 0
    private def pad: String = indentStr * level
    def line(s: String): Emit = { sb.append(pad).append(s).append('\n'); this }

    /** Append a pre-rendered (possibly multi-line) block, re-indenting EVERY line to the current level so an already-Emit-formatted sub-expression keeps
      * one-statement-per-line at the right depth.
      */
    def lines(s: String): Emit = {
      s.stripLineEnd.split("\n", -1).foreach(l => if l.isEmpty then sb.append('\n') else sb.append(pad).append(l).append('\n'))
      this
    }
    def blank(): Emit = { sb.append('\n'); this }

    /** emit `head {`, indent the body, then emit `}` at the original level. */
    def open(head: String): Emit = { line(head + " {"); level += 1; this }
    def close(): Emit = { level -= 1; line("}"); this }
    def closeWith(suffix: String): Emit = { level -= 1; line("}" + suffix); this }

    /** close the current block and open a chained one on the same line: `} <head> {` (net indent 0). */
    def closeOpen(head: String): Emit = { level -= 1; line("} " + head + " {"); level += 1; this }

    /** emit `body` inside a fresh anonymous `{ ... }` block (its own variable scope). */
    def scope(body: => Unit): Emit = { line("{"); level += 1; body; level -= 1; line("}"); this }
    def result: String = sb.toString
  }

  // ===== the codegen-only traversal AST (design §A) =====
  // These case classes / enums live ONLY in the generator. They are the reification of the
  // (direction, holes, accumulator, output, early-exit) tuple that `dfsBody`'s holes carry implicitly.
  // `lower(spec)` consumes a `Trav` and emits the op-method body; nothing here ever appears in generated code.

  enum Dir { case Fwd, Bwd }

  /** The per-element work, expressed ONCE as a code template. `perElem(e)` reads the current element via the code fragment `e` (already wrapped through
    * `read(k)` unless `rawArray`), and reads/writes the accumulator locals directly. `rawArray = true` reads `a(i)` unwrapped (sum/product).
    */
  final case class Body(perElem: String => String, rawArray: Boolean = false)

  /** An accumulator threaded as a plain `var` LOCAL (a JVM register), not a heap field. */
  final case class Acc(name: String, tpe: String, init: String)

  /** The early-exit shape, which also selects the leaf run-loop shape (design §A.2): Never -> the runMethods full-run loop (process every element) OnPredInCond
    * -> the scanB empty-body predicate-in-condition loop (Wave 2; structured here, unused now)
    */
  enum Exit {
    case Never
    case OnPredInCond(cont: String => String, onStop: String, onOne: String)
  }

  /** Output construction (design §A). Wave 1 needs NoOut (foreach) and Scalar (fold/sum/product/count). PrimArray / ResultFArray are reserved for Wave 3
    * (map/filter/scanLeft) and are not consumed by `lower` yet.
    */
  enum Out {
    case NoOut
    case Scalar(read: String)
    case PrimArray(elemKind: Kind, write: (String, String) => String)
    case ResultFArray(elemKind: Kind, sizeHint: SizeHint, write: (String, String) => String, fin: Finalize)
  }

  enum SizeHint { case Exact(expr: String); case AtMost(expr: String) }
  enum Finalize { case LeafExact; case LeafTrim; case FilterIdentity }

  /** One traversal, fully described. `k` is the element Kind (the row dispatchA/dispatchB resolved to). */
  final case class Trav(k: Kind, dir: Dir, body: Body, acc: List[Acc], exit: Exit, out: Out)

  // The traversal body for ONE direction (forward when backward=false, reverse when backward=true), parameterized
  // by how an array-backed RUN (onRF forward / onRB backward) and a single element (onO) are consumed. The two
  // directions are exact mirrors and MUTUALLY RECURSIVE: a ReverseNode is visited by CALLING the opposite-direction
  // traversal on its base (the JVM stack carries that nesting — only literal reverse-of-reverse recurses, which the
  // smart constructor collapses, so depth is ~0-1). Each direction keeps its OWN local explicit stack purely for
  // its own Concat/Append/Prepend deferrals (a Reverse subtree is self-contained, so it never spills into the
  // caller's stack). This kills the materialize/kindAt fallbacks for backward ops over trees.
  //   forward : Concat l-then-r · Append defer-elem · Prepend elem-then-base · runs ascending · Pad base-then-fill
  //   backward: Concat r-then-l · Append elem-then-base · Prepend defer-elem · runs descending · Pad fill-then-base
  // `onRev` fills the ReverseNode arm. Default = today's driver-recursive form (the non-inline dfsC pair carries
  // the opposite direction on the JVM stack). The inline `lower` emitter passes a self-contained form instead
  // (reverse-of-leaf = an opposite-direction window; reverse-of-non-leaf = materialize then opposite-direction
  // run), because once inlined there is no `c` and no driver to call (design §B.6).
  private def dfsBody(
      k: Kind,
      backward: Boolean,
      onRF: (String, String, String) => String,
      onRB: (String, String, String) => String,
      onO: String => String,
      onRev: ((String, String, String) => String, (String, String, String) => String, String => String) => String = null
  ): String = {
    // a forward run over a leaf window emits onRF(arr, start, count); a backward run emits onRB(arr, start, count)
    // where start is the FIRST index visited (high end for backward).
    def runFwd(a: String, start: String, count: String): String = onRF(a, start, count)
    def runBwd(a: String, start: String, count: String): String = onRB(a, start, count)
    val other = if backward then "Fwd" else "Back"
    val rngCase =
      if k.name != "Int" then ""
      else if !backward then
        s"\n       |        case rng: RangeNode => { val rn = rng.length; var ri = 0; while (ri < rn) { ${onO("rng.start + ri * rng.step")}; ri += 1 } }"
      else
        s"\n       |        case rng: RangeNode => { val rn = rng.length; var ri = rn - 1; while (ri >= 0) { ${onO("rng.start + ri * rng.step")}; ri -= 1 } }"
    val padRead = if k.name == "Ref" then "pad.base.applyBoxed(pi)" else s"${k.lc}At(pad.base, pi)"
    // The explicit stack holds deferred Concat children (the common case) and Append/Prepend tail elements
    // (rare). `stack` carries the FBase children; `tail`/`isTail` are allocated LAZILY only once a tail element
    // is actually deferred — so a pure Concat tree (the overwhelmingly common shape) pays ONE small array, not
    // three. The pop loop reads a slot as a tail element only when `isTail` exists and the flag is set.
    // ensureStack: room for one more `stack` slot, growing all live arrays in lockstep so indices stay aligned.
    val ensureStack =
      s"if (stack == null) stack = new Array[FBase](16) else if (sp == stack.length) { val nl = sp * 2; stack = java.util.Arrays.copyOf(stack, nl); if (isTail != null) { tail = java.util.Arrays.copyOf(tail, nl); isTail = java.util.Arrays.copyOf(isTail, nl) } }"
    // ensureTail: lazily materialize `tail`/`isTail` to match `stack.length` the first time a tail is deferred.
    val ensureTail = s"if (isTail == null) { tail = new Array[${k.arr}](stack.length); isTail = new Array[Boolean](stack.length) }"
    val pushTail = (e: String) => s"$ensureStack; $ensureTail; tail(sp) = $e; isTail(sp) = true; sp += 1"
    // --- per-node arms (mirrored by `backward`) ---
    val oneCase = s"case o: ${k.name}One => ${onO("o.elem")}"
    // pattern var is `pp` (not `p`): once `lower` splices an op body inline, a bare `p` would collide with the
    // user predicate `p` (count/filter). `pp` can't collide with any op-body lambda name. Local & order-preserving.
    val prependCase =
      if !backward then s"case pp: ${k.name}Prepend => { ${onO("pp.elem")}; cont = pp.base }"
      else s"case pp: ${k.name}Prepend => { ${pushTail("pp.elem")}; cont = pp.base }"
    val appendCase =
      if !backward then s"case a: ${k.name}Append => { ${pushTail("a.elem")}; cont = a.base }"
      else s"case a: ${k.name}Append => { ${onO("a.elem")}; cont = a.base }"
    // Concat pushes an FBase child (never a tail). If a tail array exists, clear the stale flag at this slot —
    // a popped tail can leave `isTail(sp)` set, and we must not misread this Concat child as a tail element.
    val clearTail = "if (isTail != null) isTail(sp) = false"
    val concatCase =
      if !backward then s"case c: Concat => { $ensureStack; $clearTail; stack(sp) = c.right; sp += 1; cont = c.left }"
      else s"case c: Concat => { $ensureStack; $clearTail; stack(sp) = c.left; sp += 1; cont = c.right }"
    // ReverseNode: descend into base in the OPPOSITE direction via a plain call (JVM stack carries it). If the
    // sub-walk set `stop` (short-circuit), bail the whole walk too. (Default, used by the non-inline dfsC pair.)
    // When `onRev` is supplied (the inline `lower` emitter), the arm becomes self-contained — no `c`, no driver.
    val reverseCase =
      if onRev == null then s"case rev: ReverseNode => { dfsC${k.name}$other(rev.base, c); if (c.stop) return }"
      else s"case rev: ReverseNode => { ${onRev(onRF, onRB, onO)} }"
    // leaf / slice / pad-base / updated-segments: array-backed runs (forward asc, backward desc).
    val leafCase =
      if !backward then s"case leaf: ${k.name}Arr => ${runFwd("leaf.arr", "0", "leaf.length")}"
      else s"case leaf: ${k.name}Arr => ${runBwd("leaf.arr", "leaf.length - 1", "leaf.length")}"
    val sliceCase =
      if !backward then
        s"case s: SliceNode => { val sn = s.length; val so = s.offset; s.base match { case lf: ${k.name}Arr => ${runFwd("lf.arr", "so", "sn")}; case _ => { var si = 0; while (si < sn) { ${onO(s"${k.lc}At(s.base, so + si)")}; si += 1 } } } }"
      else
        s"case s: SliceNode => { val sn = s.length; val so = s.offset; s.base match { case lf: ${k.name}Arr => ${runBwd("lf.arr", "so + sn - 1", "sn")}; case _ => { var si = sn - 1; while (si >= 0) { ${onO(s"${k.lc}At(s.base, so + si)")}; si -= 1 } } } }"
    // Pad: forward = base then fillers; backward = fillers then base.
    val padCase =
      if !backward then
        s"case pad: ${k.name}Pad => { val bl = pad.base.length; pad.base match { case lf: ${k.name}Arr => ${runFwd("lf.arr", "0", "bl")}; case _ => { var pi = 0; while (pi < bl) { ${onO(padRead)}; pi += 1 } } }; val pf = pad.filler; var pj = bl; val pn = pad.length; while (pj < pn) { ${onO("pf")}; pj += 1 } }"
      else
        s"case pad: ${k.name}Pad => { val bl = pad.base.length; val pf = pad.filler; var pj = pad.length; while (pj > bl) { ${onO("pf")}; pj -= 1 }; pad.base match { case lf: ${k.name}Arr => ${runBwd("lf.arr", "bl - 1", "bl")}; case _ => { var pi = bl - 1; while (pi >= 0) { ${onO(padRead)}; pi -= 1 } } } }"
    // Updated: forward = [0,ui) , elem , (ui,len) ; backward mirrors.
    val updatedCase =
      if !backward then
        s"case u: ${k.name}Updated => { u.base match { case lf: ${k.name}Arr => { val la = lf.arr; val ui = u.index; ${runFwd("la", "0", "ui")}; ${onO("u.elem")}; ${runFwd("la", "ui + 1", "u.length - ui - 1")} }; case _ => { val mout = materialize${k.name}(u); ${runFwd("mout", "0", "mout.length")} } } }"
      else
        s"case u: ${k.name}Updated => { u.base match { case lf: ${k.name}Arr => { val la = lf.arr; val ui = u.index; ${runBwd("la", "u.length - 1", "u.length - ui - 1")}; ${onO("u.elem")}; ${runBwd("la", "ui - 1", "ui")} }; case _ => { val mout = materialize${k.name}(u); ${runBwd("mout", "mout.length - 1", "mout.length")} } } }"
    // flatMap node: run each inner segment as a leaf window (forward: seg 0..; backward: seg last.. each descending).
    // `offs(i+1)-offs(i)` is segment i's LOGICAL length (segs(i) may carry slack). A run may short-circuit (onRF's
    // `if (c.stop) return`), which bails the whole walk — correct across segments.
    val flatMapCase =
      if !backward then
        s"case fm: ${k.name}FlatMap => { val fsegs = fm.segs; val foffs = fm.offs; val fns = fsegs.length; var fsi = 0; while (fsi < fns) { ${runFwd("fsegs(fsi)", "0", "foffs(fsi + 1) - foffs(fsi)")}; fsi += 1 } }"
      else
        s"case fm: ${k.name}FlatMap => { val fsegs = fm.segs; val foffs = fm.offs; var fsi = fsegs.length - 1; while (fsi >= 0) { val fsl = foffs(fsi + 1) - foffs(fsi); ${runBwd("fsegs(fsi)", "fsl - 1", "fsl")}; fsi -= 1 } }"
    val cases = List(leafCase, oneCase, prependCase, appendCase, concatCase, reverseCase, padCase, updatedCase, sliceCase, flatMapCase)
      .map(c => s"        $c")
      .mkString("\n       |") + rngCase
    s"""    var cur: FBase = root
       |    var stack: Array[FBase] = null; var tail: Array[${k.arr}] = null; var isTail: Array[Boolean] = null; var sp = 0
       |    while (cur != null) {
       |      var cont: FBase = null
       |      cur match {
       |$cases
       |        case _ => ()
       |      }
       |      if (cont != null) cur = cont
       |      else { cur = null; while (sp > 0 && cur == null) { sp -= 1; if (isTail != null && isTail(sp)) ${onO("tail(sp)")} else cur = stack(sp) } }
       |    }""".stripMargin
  }
  // ONE direction-aware DFS pair. An op that wants to stop early sets the consumer's `stop` flag (inside its run
  // loop), and the driver bails after each run/element. onRunF / onRunB are the forward/backward array-run handlers
  // (empty-body, constant-stride — the JIT-friendly shape); onOne handles genuine singletons.
  private def dfsConsumer(k: Kind): String =
    s"abstract class ${k.name}Dfs { var stop = false; def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit; def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit; def onOne(v: ${k.arr}): Unit }"
  private def dfsCDef(k: Kind): String = {
    def body(backward: Boolean): String =
      dfsBody(
        k,
        backward,
        (a, st, cnt) => s"{ c.onRunF($a, $st, $cnt); if (c.stop) return }",
        (a, st, cnt) => s"{ c.onRunB($a, $st, $cnt); if (c.stop) return }",
        args => s"{ c.onOne($args); if (c.stop) return }"
      )
    s"""  def dfsC${k.name}Fwd(root: FBase, c: ${k.name}Dfs): Unit = {
       |${body(false)}
       |  }
       |  def dfsC${k.name}Back(root: FBase, c: ${k.name}Dfs): Unit = {
       |${body(true)}
       |  }
       |  inline def dfsC${k.name}(root: FBase, c: ${k.name}Dfs): Unit = dfsC${k.name}Fwd(root, c)""".stripMargin
  }

  // The iterator is a trivial flat-array cursor (2 fields → escape analysis scalar-replaces it when consumed
  // inline → foreach speed). A leaf hands its backing array straight in (no copy, lazy); a tree is flattened
  // ONCE via the dfs we already have (materialize). So the tree-walk lives only in dfsBody — no second walker.
  private def cursorClass(k: Kind): String = {
    val nextBoxed = if k.name == "Ref" then s"next${k.name}()" else k.box(s"next${k.name}()")
    s"""final class ${k.name}Cursor(a: Array[${k.arr}], len: Int) extends scala.collection.AbstractIterator[Any] {
       |  private var pos = 0
       |  def hasNext: Boolean = pos < len
       |  override def knownSize: Int = len - pos
       |  def next${k.name}(): ${k.arr} = { val r = a(pos); pos += 1; r }
       |  def next(): Any = $nextBoxed
       |}""".stripMargin
  }

  // Per-kind growable, unboxed accumulator for groupBy/groupMap. One instance per group; `add` appends an
  // already-unwrapped Prim, doubling the backing array on demand (starts tiny: most groups are small). `toLeaf`
  // wraps the buffer into a leaf node WITHOUT a second per-element pass — it reuses the backing array verbatim
  // when full, else trims with a single Arrays.copyOf. This is what kills the old double-build: elements land
  // unboxed in their final array on the first (only) pass; finalisation is O(#groups), not O(n).
  private def groupBufClass(k: Kind): String = {
    val arrT = if k.name == "Ref" then "Object" else k.arr
    s"""final class ${k.name}Group {
       |  var arr: Array[$arrT] = new Array[$arrT](4)
       |  var size: Int = 0
       |  def add(v: $arrT): Unit = { if (size == arr.length) arr = java.util.Arrays.copyOf(arr, size * 2); arr(size) = v; size += 1 }
       |  def toLeaf: FBase = if (size == 0) Empty.INSTANCE else if (size == 1) new ${k.name}One(arr(0)) else new ${k.name}Arr(arr, size)
       |}""".stripMargin
  }

  // Per-kind combinations/permutations iterators (replicating scala.collection.SeqOps.{CombinationsItr,
  // PermutationsItr} EXACTLY, but unboxed). The input is materialized ONCE into `src`; init() groups equal
  // elements by first-occurrence rank (a stable counting sort — same observable order as Scala's
  // `m.getOrElseUpdate(e, m.size)` + stable `sortBy(_._2)`); each next() writes a fresh ${k.arr} result array
  // and wraps it to a leaf, so a primitive input yields primitive leaves (NO boxing of the elements).
  // `elms` = sorted elements (groups of equals adjacent, in first-occurrence order); `cnts(g)` = group size;
  // `offs` = scanLeft(0)(_+_) of cnts (group start indices in `elms`); for permutations `idxs(p)` = group of
  // position p (== Scala's sorted weights `is`).
  private def combPermClasses(k: Kind): String = {
    val K = k.name
    val arrT = if k.name == "Ref" then "Object" else k.arr
    val boxKey = if k.name == "Ref" then "src(i)" else k.box("src(i)")
    // canonicalizing leaf builder (same as FArrayOps.leaf, inlined here since that local isn't in scope):
    // length 0 -> Empty, 1 -> ${K}One, else ${K}Arr. `total` is the result length.
    val leafOut = s"{ if (total == 0) Empty.INSTANCE else if (total == 1) new ${K}One(out(0)) else new ${K}Arr(out, total) }"
    // init: assign each position a first-occurrence group rank, then a stable counting sort into `elms`
    // (preserving original order within each equal-group). Returns elms/cnts/offs/idxs. Boxing here is one-time
    // (per combinations/permutations CALL), never per result — the hot next() path stays unboxed.
    val initBody =
      s"""    val len = src.length
         |    val w = new Array[Int](len)
         |    val m = new java.util.HashMap[$arrT, Integer]()
         |    var i = 0
         |    while (i < len) { var g = m.get($boxKey); if (g == null) { g = m.size; m.put($boxKey, g) }; w(i) = g; i += 1 }
         |    val ng = m.size
         |    val cnts = new Array[Int](ng)
         |    i = 0; while (i < len) { cnts(w(i)) += 1; i += 1 }
         |    val offs = new Array[Int](ng + 1)
         |    i = 0; while (i < ng) { offs(i + 1) = offs(i) + cnts(i); i += 1 }
         |    val elms = new Array[$arrT](len)
         |    val idxs = new Array[Int](len)
         |    val pos = java.util.Arrays.copyOf(offs, ng)
         |    i = 0; while (i < len) { val g = w(i); val p = pos(g); elms(p) = src(i); idxs(p) = g; pos(g) += 1; i += 1 }
         |    new ${K}CombPermInit(elms, cnts, offs, idxs)"""
    s"""final class ${K}CombPermInit(val elms: Array[$arrT], val cnts: Array[Int], val offs: Array[Int], val idxs: Array[Int])
       |object ${K}CombPerm {
       |  def init(src: Array[$arrT]): ${K}CombPermInit = {
       |$initBody
       |  }
       |  // combinations(total): nums(g) = how many to take from group g. Greedy init: r = total; nums(g) = min(r, cnts(g)).
       |  def combinations(src: Array[$arrT], total: Int): scala.collection.AbstractIterator[Any] = {
       |    val in = init(src)
       |    val cnts = in.cnts; val ng = cnts.length
       |    val nums = new Array[Int](ng)
       |    var r = total; var g = 0
       |    while (g < ng) { val t = if (r < cnts(g)) r else cnts(g); nums(g) = t; r -= t; g += 1 }
       |    new ${K}CombinationsItr(in.elms, in.offs, cnts, nums, total)
       |  }
       |  def permutations(src: Array[$arrT]): scala.collection.AbstractIterator[Any] =
       |    new ${K}PermutationsItr(init(src))
       |}
       |final class ${K}CombinationsItr(elms: Array[$arrT], offs: Array[Int], cnts: Array[Int], nums: Array[Int], total: Int) extends scala.collection.AbstractIterator[Any] {
       |  private val ng = nums.length
       |  private var _hasNext = true
       |  def hasNext: Boolean = _hasNext
       |  def next(): Any = {
       |    if (!_hasNext) Iterator.empty.next()
       |    // build this result: for each group k take nums(k) copies of its representative element
       |    val out = new Array[$arrT](total)
       |    var o = 0; var kk = 0
       |    while (kk < ng) { val base = offs(kk); val nk = nums(kk); var j = 0; while (j < nk) { out(o) = elms(base + j); o += 1; j += 1 }; kk += 1 }
       |    val res = $leafOut
       |    // advance nums to the next combination (mirrors Scala's CombinationsItr.next)
       |    var idx = ng - 1
       |    while (idx >= 0 && nums(idx) == cnts(idx)) idx -= 1
       |    idx -= 1
       |    while (idx >= 0 && nums(idx) <= 0) idx -= 1
       |    if (idx < 0) _hasNext = false
       |    else {
       |      var sum = 1; var i = idx + 1
       |      while (i < ng) { sum += nums(i); i += 1 }
       |      nums(idx) -= 1
       |      var kx = idx + 1
       |      while (kx < ng) { val t = if (sum < cnts(kx)) sum else cnts(kx); nums(kx) = t; sum -= t; kx += 1 }
       |    }
       |    res
       |  }
       |}
       |final class ${K}PermutationsItr(in: ${K}CombPermInit) extends scala.collection.AbstractIterator[Any] {
       |  private val elms: Array[$arrT] = java.util.Arrays.copyOf(in.elms, in.elms.length)
       |  private val idxs: Array[Int] = java.util.Arrays.copyOf(in.idxs, in.idxs.length)
       |  private val total = elms.length
       |  private var _hasNext = true
       |  def hasNext: Boolean = _hasNext
       |  def next(): Any = {
       |    if (!_hasNext) Iterator.empty.next()
       |    val out = java.util.Arrays.copyOf(elms, total)
       |    val res = $leafOut
       |    // next lexicographic permutation of idxs (with elms swapped in parallel) — mirrors Scala's PermutationsItr.next
       |    var i = total - 2
       |    while (i >= 0 && idxs(i) >= idxs(i + 1)) i -= 1
       |    if (i < 0) _hasNext = false
       |    else {
       |      var j = total - 1
       |      while (idxs(j) <= idxs(i)) j -= 1
       |      var ti = idxs(i); idxs(i) = idxs(j); idxs(j) = ti
       |      var te = elms(i); elms(i) = elms(j); elms(j) = te
       |      val half = (total - i) / 2; var kk = 1
       |      while (kk <= half) { val a1 = i + kk; val b1 = total - kk; ti = idxs(a1); idxs(a1) = idxs(b1); idxs(b1) = ti; te = elms(a1); elms(a1) = elms(b1); elms(b1) = te; kk += 1 }
       |    }
       |    res
       |  }
       |}""".stripMargin
  }

  private def atDef(k: Kind): String = {
    val rngCase = if k.name == "Int" then "\n       |    case rng: RangeNode => rng.start + i * rng.step" else ""
    val padBase = if k.name == "Ref" then "pad.base.applyBoxed(i)" else s"${k.lc}At(pad.base, i)"
    s"""  def ${k.lc}At(node: FBase, i: Int): ${k.arr} = node match {
       |    case leaf: ${k.name}Arr => leaf.arr(i)
       |    case o: ${k.name}One => o.elem
       |    case c: Concat => if (i < c.left.length) ${k.lc}At(c.left, i) else ${k.lc}At(c.right, i - c.left.length)
       |    case a: ${k.name}Append => if (i < a.base.length) ${k.lc}At(a.base, i) else a.elem
       |    case p: ${k.name}Prepend => if (i == 0) p.elem else ${k.lc}At(p.base, i - 1)
       |    case rev: ReverseNode => ${k.lc}At(rev.base, rev.base.length - 1 - i)
       |    case pad: ${k.name}Pad => if (i < pad.base.length) $padBase else pad.filler
       |    case u: ${k.name}Updated => if (i == u.index) u.elem else ${
        if k.name == "Ref" then "u.base.applyBoxed(i)" else s"${k.lc}At(u.base, i)"
      }
       |    case s: SliceNode => ${k.lc}At(s.base, s.offset + i)
       |    case fm: ${k.name}FlatMap => { var lo = 0; var hi = fm.segs.length - 1; while (lo < hi) { val mid = (lo + hi + 1) >>> 1; if (fm.offs(mid) <= i) lo = mid else hi = mid - 1 }; fm.segs(lo)(i - fm.offs(lo)) }$rngCase
       |    case _ => ${k.dflt}
       |  }""".stripMargin
  }

  private def farrayOps: String = {
    val dfsConsumers = opKinds.map(dfsConsumer).mkString("\n")
    val cursors = opKinds.map(cursorClass).mkString("\n")
    val groupBufs = opKinds.map(groupBufClass).mkString("\n")
    val combPerm = opKinds.map(combPermClasses).mkString("\n")
    val dfsC = opKinds.map(dfsCDef).mkString("\n")
    val ats = opKinds.map(atDef).mkString("\n")
    // Flatten a (possibly deep) Updated chain to a fresh leaf array, ONCE, so dfs can emit it tight via
    // onRunF instead of walking the chain per element. Recursion applies overrides bottom-up => outer wins.
    // Bare-leaf fast path: Arrays.copyOf (intrinsic memcpy) instead of the dfs + consumer object.
    // MUST stay a fresh copy — sortWith/sorted mutate the materialized array in place. copyOf also
    // trims slack (leaf.length may be < arr.length).
    val mat = opKinds
      .map(k =>
        s"""  def materialize${k.name}(node: FBase): Array[${k.arr}] = node match {
         |    case leaf: ${k.name}Arr => java.util.Arrays.copyOf(leaf.arr, leaf.length)
         |    case u: ${k.name}Updated => { val out = materialize${k.name}(u.base); out(u.index) = u.elem; out }
         |    case _ => { val out = new Array[${k.arr}](node.length); var o = 0; dfsC${k.name}(node, new ${k.name}Dfs { def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { System.arraycopy(a, start, out, o, count); o += count }; def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e) { out(o) = a(i); o += 1; i -= 1 } }; def onOne(v: ${k.arr}): Unit = { out(o) = v; o += 1 } }); out }
         |  }""".stripMargin
      )
      .mkString("\n")
    // Stable bottom-up mergesort directly on a kind array, ping-ponging between two buffers (no per-level
    // copy-back). `less(x, y)` = x sorts strictly before y. Returns the sorted buffer (may be the input or temp).
    // non-inline per-kind flatMap result builder: dfs+System.arraycopy each inner into one flat array (dfs
    // inlined ONCE here, not at every flatMap call site). Ref takes the ClassTag so the array is typed.
    // non-inline flatMap helpers (compiled once, not re-inlined at every call site): grow the output array,
    // and copy ONE non-leaf inner into out (dfs).
    val flatMapCopyOne = opKinds
      .map(k =>
        s"""  def ensureCap${k.name}(out: Array[${k.arr}], needed: Int): Array[${k.arr}] =
         |    if (needed <= out.length) out else { var nc = out.length * 2; if (nc < needed) nc = needed; java.util.Arrays.copyOf(out, nc) }
         |  def flatMapCopyOne${k.name}(node: FBase, out: Array[${k.arr}], off0: Int): Unit = {
         |    var o = off0; dfsC${k.name}(node, new ${k.name}Dfs { def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { System.arraycopy(a, start, out, o, count); o += count }; def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e) { out(o) = a(i); o += 1; i -= 1 } }; def onOne(v: ${k.arr}): Unit = { out(o) = v; o += 1 } })
         |  }""".stripMargin
      )
      .mkString("\n")
    // SHARED flatMap drivers, one per OUTPUT kind KB — the flatMap analogue of the reduceLeaf*/mapLeaf*
    // shared methods. The surface realizes ONE IntToRefFn[FBase] SAM `(i) => f(<unboxed source read>)`
    // (the user lambda + the unboxed `sa(i)` read spliced into the SAM body) and CALLS this; the whole
    // adaptive node/flatten loop compiles ONCE here instead of splicing at every call site. That splice
    // was not just bloat: with the loop inlined into a caller that ALSO calls a virtual FBase method on
    // the result (`.flatMap(f).take(n)` / `.reverse`), Graal's partial escape analysis of the per-element
    // inner FArray collapsed catastrophically — MEASURED 35x (FlatMapFilterTakeStr 0.055x of iarray;
    // ChainDiag fmTake 70 ops/s vs fm 2430 ops/s, alloc.rate.norm 7.2MB/op vs 3.2MB/op). Compiled in its
    // own method the PEA context never contains the trailing call, so the cliff cannot happen.
    // ADAPTIVE, KIND-AWARE threshold on the first inner's width (unchanged): node only where it is a
    // strict throughput win (>=8 ref / >=32 prim, measured crossovers), else flatten-copy into one leaf.
    val flatMapShared = {
      val ee = new Emit("  ")
      opKinds.foreach { kb =>
        val KB = kb.name
        val nodeMinWidth = if KB == "Ref" then 8 else 32
        val segT = if KB == "Ref" then "Array[Object]" else s"Array[${kb.arr}]"
        // tiny entry (~30 bytecodes, always inlinable) picking node vs flatten; each path is its OWN
        // single-loop method so the JIT can inline the hot loop (and re-monomorphize `fi.apply`) at
        // each caller — one big method measured a 33% loss on chained prim flatMaps (FuseProbe eager).
        ee.open(s"def flatMapShared${KB}(cnt: Int, fi: Traversers.IntToRefFn[FBase]): FBase =")
        ee.line("val inr0 = fi.apply(0)")
        ee.line("val l0 = inr0.length")
        ee.line(s"if (l0 >= $nodeMinWidth) flatMapSegs${KB}(cnt, inr0, l0, fi) else flatMapFlat${KB}(cnt, inr0, l0, fi)")
        ee.close()
        // node path: one backing array per inner (zero-copy for leaf inners), offs = prefix sums.
        ee.open(s"def flatMapSegs${KB}(cnt: Int, inr0: FBase, l0: Int, fi: Traversers.IntToRefFn[FBase]): FBase =")
        ee.line(s"val segs = new Array[$segT](cnt)")
        ee.line("val offs = new Array[Int](cnt + 1)")
        ee.line(s"inr0 match { case lf: ${KB}Arr => segs(0) = lf.arr; case _ => segs(0) = materialize${KB}(inr0) }")
        ee.line("var total = l0")
        ee.line("var i = 1")
        ee.open("while (i < cnt)")
        ee.line("offs(i) = total")
        ee.line("val inr = fi.apply(i)")
        ee.line(s"inr match { case lf: ${KB}Arr => { segs(i) = lf.arr; total += lf.length }; case _ => { val m = materialize${KB}(inr); segs(i) = m; total += m.length } }")
        ee.line("i += 1")
        ee.close()
        ee.line("offs(cnt) = total")
        ee.line("if (total == 0) Empty.INSTANCE")
        ee.line(s"else if (total == 1) { var j = 0; while (offs(j + 1) == offs(j)) j += 1; new ${KB}One(segs(j)(0)) }")
        ee.line(s"else new ${KB}FlatMap(segs, offs)")
        ee.close()
        // flatten path: size from the first inner (uniform flatMap never grows), ensureCap for the rest.
        // Tiny leaf inners (ln <= 8, the FArray(a, b) case) copy with a manual element loop — the
        // System.arraycopy stub call costs more than the copy at these lengths; ${KB}One inners write
        // the element directly (no dfs, no consumer).
        ee.open(s"def flatMapFlat${KB}(cnt: Int, inr0: FBase, l0: Int, fi: Traversers.IntToRefFn[FBase]): FBase =")
        ee.line("val est = { val e = cnt * l0; if (e < 8) 8 else e }")
        ee.line(s"var out: $segT = new $segT(est)")
        ee.line(s"inr0 match { case lf: ${KB}Arr => System.arraycopy(lf.arr, 0, out, 0, l0); case o: ${KB}One => out(0) = o.elem; case _ => flatMapCopyOne${KB}(inr0, out, 0) }")
        ee.line("var off = l0")
        ee.line("var i = 1")
        ee.open("while (i < cnt)")
        ee.line("val inr = fi.apply(i)")
        ee.line("val ln = inr.length")
        ee.line(s"out = ensureCap${KB}(out, off + ln)")
        ee.open("inr match")
        ee.open(s"case lf: ${KB}Arr =>")
        ee.line("if (ln <= 8) { val ia = lf.arr; var c = 0; while (c < ln) { out(off + c) = ia(c); c += 1 } }")
        ee.line("else System.arraycopy(lf.arr, 0, out, off, ln)")
        ee.close()
        ee.line(s"case o: ${KB}One => out(off) = o.elem")
        ee.line(s"case _ => flatMapCopyOne${KB}(inr, out, off)")
        ee.close()
        ee.line("off += ln")
        ee.line("i += 1")
        ee.close()
        ee.line(s"if (off == 0) Empty.INSTANCE else if (off == 1) new ${KB}One(out(0)) else new ${KB}Arr(out, off)")
        ee.close()
      }
      ee.result
    }
    // Natural (run-adaptive) bottom-up mergesort. NON-inline (compiled once, not dumped at every sort site,
    // so multiple sorts in one method can't blow past the JIT method-size limit) with an unboxed primitive
    // comparator (${k.name}Less SAM — no boxing). Detects ascending runs and reverses strictly-descending
    // ones in place (already/reverse-sorted -> O(n)), then merges runs pairwise. Stable (merge prefers left;
    // ascending detection non-strict, descending strict). Ref sorts use java.util.Arrays.sort instead.
    val sortKinds = opKinds.filter(_.name != "Ref")
    val lessTraits = sortKinds.map(k => s"trait ${k.name}Less { def lt(a: ${k.arr}, b: ${k.arr}): Boolean }").mkString("\n")
    // n <= 32: in-place binary-free insertion sort — the run-adaptive mergesort's setup (two run-index
    // arrays + dst buffer + run detection) is pure overhead at tiny n (SortInt sortWith@10 measured
    // 0.33x of iarray). Stable (strict less; equal elements never swap past each other).
    val sortArr = sortKinds
      .map(k =>
        s"""  def sort${k.name}(a: Array[${k.arr}], n: Int, less: ${k.name}Less): Array[${k.arr}] = {
         |    if (n < 2) a
         |    else if (n <= 32) {
         |      var i = 1
         |      while (i < n) {
         |        val v = a(i)
         |        var j = i - 1
         |        while (j >= 0 && less.lt(v, a(j))) { a(j + 1) = a(j); j -= 1 }
         |        a(j + 1) = v
         |        i += 1
         |      }
         |      a
         |    } else {
         |      var sruns = new Array[Int](n + 1); var nr = 0; var i = 0
         |      while (i < n) {
         |        sruns(nr) = i; nr += 1; var j = i + 1
         |        if (j < n) {
         |          if (less.lt(a(j), a(i))) { j += 1; while (j < n && less.lt(a(j), a(j - 1))) j += 1; var lo = i; var hi = j - 1; while (lo < hi) { val t = a(lo); a(lo) = a(hi); a(hi) = t; lo += 1; hi -= 1 } }
         |          else { j += 1; while (j < n && !less.lt(a(j), a(j - 1))) j += 1 }
         |        }
         |        i = j
         |      }
         |      sruns(nr) = n
         |      var src = a; var dst = new Array[${k.arr}](n); var druns = new Array[Int](nr + 1)
         |      while (nr > 1) {
         |        var r = 0; var w = 0
         |        while (r < nr) {
         |          druns(w) = sruns(r); w += 1
         |          if (r + 1 < nr) {
         |            val lo = sruns(r); val mid = sruns(r + 1); val hi = sruns(r + 2)
         |            var x = lo; var y = mid; var k = lo
         |            while (x < mid && y < hi) { if (less.lt(src(y), src(x))) { dst(k) = src(y); y += 1 } else { dst(k) = src(x); x += 1 }; k += 1 }
         |            while (x < mid) { dst(k) = src(x); x += 1; k += 1 }
         |            while (y < hi) { dst(k) = src(y); y += 1; k += 1 }
         |            r += 2
         |          } else { val lo = sruns(r); System.arraycopy(src, lo, dst, lo, n - lo); r += 1 }
         |        }
         |        druns(w) = n
         |        val ts = src; src = dst; dst = ts; val tr = sruns; sruns = druns; druns = tr; nr = w
         |      }
         |      src
         |    }
         |  }""".stripMargin
      )
      .mkString("\n")

    // Reference reads cast the leaf array to Array[A] (checkcast hoisted out of the loop at the
    // concrete inline call site; empty leaves never evaluate it). Primitive arrays can't cast to
    // Array[A] (erases to Object[]), so they read directly.
    // Ref reads cast the ELEMENT (Object -> A), valid for any backing array — an Array[A] (i.e. Array[String])
    // cast would CCE on an Object[] backing (which materialize/sort produce, having no ClassTag).
    def read(k: Kind): String = if k.name == "Ref" then "r.wrap(a(i).asInstanceOf[A])" else "r.wrap(a(i))"
    def readOne(k: Kind): String = if k.name == "Ref" then "r.wrap(v.asInstanceOf[A])" else "r.wrap(v)"
    def alloc(k: Kind, rv: String): String =
      if k.name == "Ref" then s"new Array[Object](n)" else s"new Array[${k.arr}](n)"
    // a ref write stores r.Prim (= A, runtime <: Object) into an Object[] — cast it (no-op); prims store directly.
    def wr(k: Kind, e: String): String = if k.name == "Ref" then s"($e).asInstanceOf[Object]" else e
    // wrap a raw kind-array element back to A (for an inline user comparator); plain kind-array alloc
    def readVal(k: Kind, e: String): String = if k.name == "Ref" then s"r.wrap($e.asInstanceOf[A])" else s"r.wrap($e)"
    def allocPlain(k: Kind): String = s"new Array[${k.arr}](n)"
    // Canonicalizing leaf builder: a result array `arr` of length `len` becomes Empty (0) / ${K}One (1) / a
    // ${K}Arr leaf (>=2), upholding the size-0/1 invariant at every Scala build site. `len` is evaluated once.
    def leaf(k: Kind, arr: String, len: String): String =
      s"{ val _l = $len; if (_l == 0) Empty.INSTANCE else if (_l == 1) new ${k.name}One($arr(0)) else new ${k.name}Arr($arr, _l) }"
    // ---- run-method builders: emit the onRunF/onRunB pair for a consumer from ONE per-element body. `body`
    // reads the wrapped element via `read(k)` (which references `a(i)`); `i` runs start..start+count (fwd) or
    // start..start-count (bwd, descending). Two constant-stride loops keep the JIT-friendly shape.
    // fullRun: process EVERY element (no early stop). rawA=true reads a(i) directly (sum/product), else read(k).
    def runMethods(k: Kind, body: String => String, rawA: Boolean = false): String = {
      val rd = if rawA then "a(i)" else read(k)
      val fwd =
        s"def onRunF(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start + count; while (i < e) { ${body(rd)}; i += 1 } }"
      val bwd =
        s"def onRunB(a: Array[${k.arr}], start: Int, count: Int): Unit = { var i = start; val e = start - count; while (i > e) { ${body(rd)}; i -= 1 } }"
      s"$fwd; $bwd"
    }
    // Friendly compile error for the no-Repr case (e.g. `FArray[Int | String]`, `FArray[Any]`,
    // `FArray[Boolean]`): instead of the cryptic "cannot reduce summonFrom", explain the cause and the
    // fix. `tp` names the offending type param ("A"/"B"). The element type itself can't be spliced into a
    // `compiletime.error` literal (it needs a macro), so we name the type param and give the recipe.
    def noReprErr(tp: String): String =
      "scala.compiletime.error(\"FArray: no element-kind specialization for type parameter " + tp +
        ". This op needs " + tp + " to be a known kind (Int/Long/Double or a reference type <: AnyRef). A union" +
        " like `Int | String`, `Any`, `AnyVal`, or a non-specialized primitive (e.g. Boolean) has no" +
        " unboxed representation. Fixes: (1) keep the element type homogeneous; (2) box the primitive" +
        " side to a reference first, e.g. `ints.map(java.lang.Integer.valueOf) ++ strings : FArray[Integer" +
        " | String]` (a reference union IS specializable); or (3) read via the boxed view `xs.toIndexedSeq`" +
        " / `xs.iterator` if you must keep the primitive union.\")"
    // Append a friendly-error catch-all to an already-built OUTER `summonFrom { … }` string. The outer
    // dispatch keys on the element type param `tp` (A / A1 / …); if no `${K}Repr[tp]` matches (a primitive
    // union / Any / Boolean), this `case _` fires `noReprErr` with the recipe instead of "cannot reduce
    // summonFrom". Only the OUTER dispatch needs it — inner B/C dispatches only run after the outer matched.
    def withErr(summonStr: String, tp: String): String =
      val close = summonStr.lastIndexOf("\n    }")
      if close < 0 then summonStr // defensive: shape changed, leave as-is
      else summonStr.substring(0, close) + s"\n      case _ => ${noReprErr(tp)}" + summonStr.substring(close)
    // start:kind-dispatch
    // emit a Scala-3 `summonFrom` that picks the per-kind unboxed body at the concrete call site:
    // a `case r: ${K}Repr[A]` per element kind (Int/Long/Double/Ref), resolved at COMPILE time.
    def dispatchA(body: Kind => String): String =
      "summonFrom {\n" + opKinds.map(k => s"      case r: ${k.name}Repr[A] => ${body(k)}").mkString("\n") + s"\n      case _ => ${noReprErr("A")}\n    }"
    def dispatchB(body: Kind => String): String =
      "summonFrom {\n" + opKinds.map(k => s"      case r: ${k.name}Repr[B] => ${body(k)}").mkString("\n") + s"\n      case _ => ${noReprErr("B")}\n    }"
    // stop:kind-dispatch

    // wrap an arbitrary RAW kind-array-typed expression `e` back to A (mirrors readOne, which hardcodes `v`).
    // `rawArray` ops (sum/product) read the prim directly; Ref always casts the Object element to A then wraps.
    def wrapRaw(k: Kind, e: String, rawArray: Boolean): String =
      if rawArray then e
      else if k.name == "Ref" then s"r.wrap(($e).asInstanceOf[A])"
      else s"r.wrap($e)"

    // ===== the PUSH emitter (design §B): splice dfsBody INLINE with the op body in its holes =====
    // Accumulators become plain `var` LOCALS (no consumer object, no heap field); the per-element body is
    // spliced straight into the leaf/run loops (no onRunF call, no megamorphism); the ReverseNode arm and the
    // deep-base Slice/Pad/Updated arms are handled self-contained (reverse-of-leaf inline, else materialize).
    // Wave 1: Exit.Never + Out.Scalar / Out.NoOut. Wave 3 adds Out.PrimArray / Out.ResultFArray (map/filter):
    // `out` is a LOCAL array, `o` a LOCAL cursor, the write spliced into the run loops — no ${K}Dfs, no heap field.
    def lower(spec: Trav): String = {
      val k = spec.k
      val backward = spec.dir == Dir.Bwd
      val rawA = spec.body.rawArray
      val rd = if rawA then "a(i)" else read(k) // the element read inside a constant-stride run loop (reads `a(i)`)
      spec.exit match {
        case Exit.Never =>
          // onRF/onRB = the runMethods full-run loops with `spec.body.perElem` spliced; onO = the single form.
          // The run array hole arrives as an arbitrary expression (`leaf.arr`, `lf.arr`, `la`, `mout`, …); bind
          // it to a local `a` so the `read(k)`/`a(i)` element fragment resolves to the right array.
          // `start`/`count` arrive as arbitrary expressions (e.g. `u.length - ui - 1`); parenthesize so the
          // `start + count` / `start - count` end-bound keeps the intended precedence (the old runMethods passed
          // them as plain method params `start`/`count`, so it never needed this).
          val onRF = (arr: String, start: String, count: String) =>
            s"{ val a = $arr; var i = $start; val e = ($start) + ($count); while (i < e) { ${spec.body.perElem(rd)}; i += 1 } }"
          val onRB = (arr: String, start: String, count: String) =>
            s"{ val a = $arr; var i = $start; val e = ($start) - ($count); while (i > e) { ${spec.body.perElem(rd)}; i -= 1 } }"
          // ALWAYS brace the single-element body: it splices into the pop loop's `if (isTail(sp)) <onO> else
          // cur = stack(sp)`, so a brace-less body (e.g. count's bare `if (p(..)) n += 1`) would steal that
          // trailing `else` (dangling-else) and drop the popped Concat child. Braces make the `else` bind right.
          val onO = (e: String) => s"{ ${spec.body.perElem(wrapRaw(k, e, rawA))} }"
          // ReverseNode arm, self-contained (design §B.6): the base is visited in the OPPOSITE direction.
          //   walk dir Fwd  -> base Bwd: reverse-of-leaf = a descending window; else materialize then run Bwd.
          //   walk dir Bwd  -> base Fwd: reverse-of-leaf = an ascending window;  else materialize then run Fwd.
          val onRev = (rf: (String, String, String) => String, rb: (String, String, String) => String, _o: String => String) =>
            if !backward then
              s"rev.base match { case lf: ${k.name}Arr => ${rb("lf.arr", "lf.length - 1", "lf.length")}; case _ => { val mout = materialize${k.name}(rev.base); ${rb("mout", "mout.length - 1", "mout.length")} } }"
            else
              s"rev.base match { case lf: ${k.name}Arr => ${rf("lf.arr", "0", "lf.length")}; case _ => { val mout = materialize${k.name}(rev.base); ${rf("mout", "0", "mout.length")} } }"
          val walk = dfsBody(k, backward, onRF, onRB, onO, onRev)
          // (3) accumulators as plain `var` locals, declared OUTSIDE the walk; (4) the Out epilogue.
          val accDecls = spec.acc.map(a => s"var ${a.name}: ${a.tpe} = ${a.init}").mkString("; ")
          // Out array allocation (Wave 3): `out` is a plain LOCAL declared BEFORE the walk, so the spliced
          // `out(oc) = …; oc += 1` writes touch a register-resident array reference, not a heap consumer field.
          // The write cursor `oc` is one of `spec.acc` (Acc("oc","Int","0")), so it's a local too. (It is named
          // `oc`, not `o`, because `dfsBody`'s ${K}One arm binds the pattern var `o` — a bare `o` would collide.)
          def sizeExpr(h: SizeHint): String = h match { case SizeHint.Exact(e) => e; case SizeHint.AtMost(e) => e }
          def allocOut(ek: Kind, h: SizeHint): String =
            if ek.name == "Ref" then s"val out = new Array[Object](${sizeExpr(h)})"
            else s"val out = new Array[${ek.arr}](${sizeExpr(h)})"
          val outDecl = spec.out match {
            case Out.PrimArray(ek, _)          => s"${allocOut(ek, SizeHint.Exact("n"))}\n"
            case Out.ResultFArray(ek, h, _, _) => s"${allocOut(ek, h)}\n"
            case _                             => ""
          }
          // The epilogue: scalar -> return the acc expr; ResultFArray -> canonicalize the built `out` via the
          // EXISTING leaf() builder (LeafExact: length == n) / the filter identity-then-leaf form (FilterIdentity).
          val epilogue = spec.out match {
            case Out.Scalar(res)                 => s"; $res"
            case Out.NoOut                       => ""
            case Out.PrimArray(_, _)             => "; out"
            case Out.ResultFArray(ek, _, _, fin) =>
              fin match {
                // LeafExact (map): output length is exactly `oc` (== n); route through the canonicalizer so a
                // length-0 walk yields Empty and length-1 yields ${K}One — upholding the size-0/1 invariant.
                case Finalize.LeafExact => s"\n${leaf(ek, "out", "oc")}"
                // LeafTrim (collect/…): `oc <= out.length`. SKIP the trim copy — hand the (possibly over-sized)
                // `out` to the leaf with logical length `oc` (slack = out.length - oc). Leaves iterate to `length`.
                case Finalize.LeafTrim => s"\n${leaf(ek, "out", "oc")}"
                // FilterIdentity: keep the `if (oc == n) xs` reference-identity shortcut; else canonicalize
                // size-0/1 and hand `out` straight through with logical length `oc` (no trim copy — slack).
                case Finalize.FilterIdentity =>
                  s"\nif (oc == n) xs else if (oc == 0) Empty.INSTANCE else if (oc == 1) new ${ek.name}One(out(0)) else new ${ek.name}Arr(out, oc)"
              }
          }
          val accPart = if accDecls.isEmpty then "" else s"$accDecls\n"
          // LEAF FAST-PATH: a top-level single leaf (the overwhelmingly common shape — every `tabulate(n)`/map
          // input is one `${K}Arr`) runs a STANDALONE constant-stride loop, NOT nested inside the `cur match` +
          // outer `while (cur != null)` walk. The nested leaf loop does not vectorize for trivial-`f` prim ops
          // (IntMap stuck at 0.90x); lifted out, it is byte-identical to iarray's loop. Tree-internal leaves
          // still flow through the walk's own leaf arm, so trees stay correct + consumer-free.
          val leafFast =
            if !backward then
              s"case leaf: ${k.name}Arr => { val a = leaf.arr; var i = 0; val e = leaf.length; while (i < e) { ${spec.body.perElem(rd)}; i += 1 } }"
            else s"case leaf: ${k.name}Arr => { val a = leaf.arr; var i = leaf.length - 1; while (i >= 0) { ${spec.body.perElem(rd)}; i -= 1 } }"
          s"{ $outDecl$accPart" + s"xs match { $leafFast; case _ => { val root: FBase = xs\n$walk } }" + s"$epilogue }"
        case other => sys.error(s"lower: Wave 1 does not support Exit $other")
      }
    }

    // ===== Wave 1 traversal specs (design §B.1). Each op delegates: dispatchA(k => lower(spec(k))). =====
    def foldLeftSpec(k: Kind): Trav =
      Trav(k, Dir.Fwd, Body(e => s"acc = op(acc, $e)"), List(Acc("acc", "Z", "z")), Exit.Never, Out.Scalar("acc"))
    // foldRight is the SAME spec with direction flipped and the op argument order swapped — a declaration, not a
    // hand-written mirror (the direction-mirroring dfsBody already encodes is now one field).
    def foldRightSpec(k: Kind): Trav =
      foldLeftSpec(k).copy(dir = Dir.Bwd, body = Body(e => s"acc = op($e, acc)"))
    def foreachSpec(k: Kind): Trav =
      Trav(k, Dir.Fwd, Body(e => s"f($e)"), Nil, Exit.Never, Out.NoOut)
    def countSpec(k: Kind): Trav =
      Trav(k, Dir.Fwd, Body(e => s"if (p($e)) n += 1"), List(Acc("n", "Int", "0")), Exit.Never, Out.Scalar("n"))
    def sumSpec(k: Kind): Trav =
      if k.prim.isDefined then
        Trav(k, Dir.Fwd, Body(e => s"acc += $e", rawArray = true), List(Acc("acc", k.arr, k.dflt)), Exit.Never, Out.Scalar("acc.asInstanceOf[B]"))
      else
        Trav(
          k,
          Dir.Fwd,
          Body(e => s"acc = num.plus(acc, ($e).asInstanceOf[B])", rawArray = true),
          List(Acc("acc", "B", "num.zero")),
          Exit.Never,
          Out.Scalar("acc")
        )
    def productSpec(k: Kind): Trav = {
      val one = k.name match { case "Long" => "1L"; case "Double" => "1.0"; case "Float" => "1.0f"; case _ => "1" }
      if k.prim.isDefined then
        Trav(k, Dir.Fwd, Body(e => s"acc *= $e", rawArray = true), List(Acc("acc", k.arr, one)), Exit.Never, Out.Scalar("acc.asInstanceOf[B]"))
      else
        Trav(
          k,
          Dir.Fwd,
          Body(e => s"acc = num.times(acc, ($e).asInstanceOf[B])", rawArray = true),
          List(Acc("acc", "B", "num.one")),
          Exit.Never,
          Out.Scalar("acc")
        )
    }

    // Wave 3 (design §B.2/§B.3): array-building ops. `ka` drives the walk (dispatchA); `kb` (the result element
    // kind, from dispatchB) drives the `out` array type + the write's unwrap. The write `out(o) = …; o += 1` is the
    // per-element body, spliced into the run loop — `out`/`o` are LOCALS, no consumer.
    // (map's OLD inlined-walk spec lived here — the Build shape now routes map/mapConserve through the shared
    //  mapLeaf${I}${O} leaf methods + the Java build${I}${O} traversers instead. filter/filterNot's OLD inlined-walk
    //  filterSpec lived here too — the Build-filtered shape now routes them through the shared filterLeaf${I} leaf
    //  methods + the Java buildFiltered${I} traversers.)

    // ===== HYBRID TRAVERSAL — foldLeft over ALL input kinds (design §5) =====
    // The surface peels the hot shapes inline (the user's `inline op` splices unboxed — the optimal small
    // loop) and realizes a SAM into the Java fold function type ONLY for genuine trees. Dispatch on Z's kind
    // (summonFrom rz) picks, for a PRIMITIVE input kind ${I}, the unboxed ${I}To${I}Fold (Z=${I}, `acc` a
    // primitive local) vs the generic ${I}ToRefFold<Z> (Z=Ref, `acc` already an object) — either way NO
    // boxing of the accumulator. A CROSS-prim Z (e.g. Long acc over Int input) keeps the OLD lowered walk
    // (which threads any-kind Z correctly); only the self-kind unboxed Z and Z=Ref get the new traverser.
    // foldRight stays on the OLD walk for now (it is the Bwd direction; the Java Bwd traverser exists only
    // because the Fwd traverser recurses into it at ReverseNode).

    // wrap the SAM lambda's `v` parameter (the raw element handed back by the Java fold) to A: for a prim
    // input v is already the prim element; for Ref input v is Object and must be cast to A before wrap.
    def wrapV(k: Kind): String = if k.name == "Ref" then "r.wrap(v.asInstanceOf[A])" else "r.wrap(v)"

    // HYBRID Foreach surface (design §2.1/§5): summon the input kind, realize the user's inline `f` into the
    // ${I}Consumer SAM, and CALL the shared leaf method foreachLeaf${I} (Empty/One/leaf inline, tree ->
    // Traversers.foreachFwd${I}). NO inlined leaf loop here. The SAM wraps the raw element `v` to A (wrapV).
    val foreach = dispatchA(k => s"foreachLeaf${k.name}(xs, (v) => f(${wrapV(k)}))")

    // Emit a fold surface (foldLeft when fwd, foldRight when bwd) for input kind `k`. ONE STATEMENT PER LINE at
    // real indentation (design §6). The two directions differ only in (1) the combine argument order — fwd
    // `op(acc, e)`, bwd `op(e, acc)` — and (2) which shared leaf method they route through (Fwd vs Bwd). The
    // surface ONLY realizes the user's inline `op` into the specialized fold SAM and CALLS the shared leaf
    // method (reduceLeaf${dir}…) — there is NO inlined leaf loop here; the leaf method peels Empty/One/leaf
    // and routes genuine trees through Traversers. Self-kind prim Z -> unboxed reduceLeaf${dir}${I}${I}; Ref Z
    // -> reduceLeaf${dir}${I}Ref; cross-prim/other Z -> the old lowered walk (which threads any-kind Z).
    def foldSurface(k: Kind, backward: Boolean): String = {
      val K = k.name
      val dir = if backward then "Bwd" else "Fwd"
      // combine an accumulator expr `acc` with an element expr `el`, in the op's required argument order.
      def comb(acc: String, el: String): String = if backward then s"op($el, $acc)" else s"op($acc, $el)"
      val ee = new Emit("  ")
      // --- the unboxed self-kind arm (prim input, Z == input kind): realize the `${K}To${K}Fold` SAM (acc a
      //     primitive — the body unwraps z/acc, applies `op` over wrapped values, unwraps back) and CALL the
      //     shared leaf method. NO inlined leaf loop here. ---
      def zPrimArm(): Unit =
        ee.line(s"rz.wrap(reduceLeaf${dir}${K}${K}(xs, rz.unwrap(z), (acc, v) => rz.unwrap(${comb("rz.wrap(acc)", wrapV(k))})))")
      // --- the generic ref-acc arm (Z = Ref): `acc` is already an object, no boxing. Realize the
      //     `${K}ToRefFold[Z & AnyRef]` SAM and CALL the shared leaf method. NO inlined leaf loop here. ---
      def zRefArm(): Unit =
        ee.line(
          s"reduceLeaf${dir}${K}Ref[Z & AnyRef](xs, z.asInstanceOf[Z & AnyRef], (acc, v) => ${comb("acc.asInstanceOf[Z]", wrapV(k))}.asInstanceOf[Z & AnyRef]).asInstanceOf[Z]"
        )
      // --- the cold cross-kind arm (cross-prim Z, e.g. a Long accumulator over Int input, or a prim Z over
      //     Ref input): route through the SAME generic Ref-acc shared leaf method with the accumulator BOXED
      //     to AnyRef. NO inlined leaf loop here. These cases are cold/rare, so the boxing is fine. ---
      def zBoxedArm(): Unit =
        ee.line(
          s"reduceLeaf${dir}${K}Ref[AnyRef](xs, z.asInstanceOf[AnyRef], (acc, v) => ${comb("acc.asInstanceOf[Z]", wrapV(k))}.asInstanceOf[AnyRef]).asInstanceOf[Z]"
        )
      // --- Ref input + PRIMITIVE accumulator (e.g. xs.foldLeft(0)((n, s) => n + s.length)): the acc stays an
      //     unboxed primitive `${P}`. Realize the `RefTo${P}Fold` SAM (the element `v` is Object) and CALL the
      //     shared unboxed leaf method. A COMMON reference workload, so it gets the unboxed acc (the leaf path
      //     never boxes; only a genuine tree boxes, inside the leaf method's cold arm). NO inlined leaf loop. ---
      def zPrimAccOverRefArm(p: Kind): Unit =
        ee.line(
          s"rz.wrap(reduceLeaf${dir}Ref${p.name}(xs, rz.unwrap(z), (acc, v) => rz.unwrap(${comb("rz.wrap(acc)", wrapV(k))})))"
        )
      ee.open("summonFrom")
      if k.isPrim then {
        ee.open(s"case rz: ${K}Repr[Z] =>")
        zPrimArm()
        ee.close()
      } else {
        // Ref input: unboxed prim-accumulator arms (Int/Long/Double) before the generic Ref-acc / boxed arms.
        primKinds.foreach { p =>
          ee.open(s"case rz: ${p.name}Repr[Z] =>")
          zPrimAccOverRefArm(p)
          ee.close()
        }
      }
      ee.open("case rz: RefRepr[Z] =>")
      zRefArm()
      ee.close()
      // CROSS-prim Z (a prim Z over a DIFFERENT prim input, e.g. Long acc over Int input): the shared Ref-acc
      // leaf method with a boxed accumulator. (Ref input's prim-acc cases are handled unboxed above.)
      ee.open("case _ =>")
      zBoxedArm()
      ee.close()
      ee.close()
      ee.result
    }
    // SHARED LEAF METHODS (design §5) — the whole Reduce family (foldLeft/foldRight/sum/product/count/
    // mkString) routes through these. Each is a SMALL NON-inline method in FArrayOps' own class: it peels
    // Empty/One/top-leaf with the unboxed leaf loop calling the realized SAM `f.apply`, and for a genuine
    // TREE makes ONE CALL to the Java traverser (Traversers.reduce{Fwd,Bwd}…). Because it is non-inline and
    // lives in FArrayOps, it references the `Traversers.*` statics directly with no `Traversers$` module
    // (the NoClassDefFoundError gotcha only afflicts inline def bodies) — so it REPLACES the old
    // foldTree* forwarders. Staying SMALL (the tree case is a CALL, not an inlined walk) lets the JIT
    // inline it for a lone monomorphic call site (single fold stays fast) AND share it across many sites
    // (no per-call bloat -> no @100k multi-fold cliff) — exactly how Array.map works. (Measured: the old
    // inline-the-leaf-loop surface craters to 0.29× of iarray at 8 folds @100k; this shared form ties
    // iarray everywhere, 1.01–1.07×.) The SAM wraps the raw element `v` to A itself (it closes over the
    // user `op`/`r`), so the leaf method is fully op-agnostic per (kind, direction, acc-shape).
    val reduceLeafMethods = {
      val ee = new Emit("  ")
      // emit ONE shared leaf method. `je` = the Scala element type of the leaf array (`k.arr`); `accT` = the
      // accumulator type; `tparam` = a leading type param clause (Ref-acc is generic); `foldT` = the SAM type;
      // `treeCall` = the Traversers.* tree forwarder.
      def emit(name: String, k: Kind, tparam: String, accT: String, foldT: String, treeCall: String, backward: Boolean): Unit = {
        ee.open(s"def $name$tparam(xs: FBase, z: $accT, f: $foldT): $accT = xs match")
        ee.line("case e: Empty => z")
        ee.line(s"case o: ${k.name}One => f.apply(z, o.elem)")
        ee.open(s"case leaf: ${k.name}Arr =>")
        // Loop on the LOGICAL length `leaf.length` (may be < arr.length — leaves can carry slack so structural
        // ops skip the trim copy). MEASURED (ArrLenFoldBenchmark, GraalVM + C2): a `leaf.length` field bound over
        // `a` ties the `a.length` baseline exactly — HotSpot loop-predication hoists the `leaf.length <= a.length`
        // check out once and the body stays a vectorized counted loop. (The compound `i < a.length && i < len`
        // form is what BREAKS vectorization — 0.48× on GraalVM — never use it.)
        ee.line("val a = leaf.arr")
        ee.line("val n = leaf.length")
        ee.line(s"var acc: $accT = z")
        if !backward then {
          ee.line("var i = 0")
          ee.open("while (i < n)")
          ee.line("acc = f.apply(acc, a(i))")
          ee.line("i += 1")
          ee.close()
        } else {
          ee.line("var i = n - 1")
          ee.open("while (i >= 0)")
          ee.line("acc = f.apply(acc, a(i))")
          ee.line("i -= 1")
          ee.close()
        }
        ee.line("acc")
        // SLICE FAST-PATH: a top-level SliceNode over a matching leaf (the take/drop/tail/slice result shape)
        // runs a STANDALONE constant-stride loop over [offset, offset+length), exactly like the bare-leaf arm,
        // NOT nested inside the Traversers walk (the `cur != null` + long instanceof chain). `take(n).drop(1)`
        // collapses to one such SliceNode, so a fold over a sliced array stays a flat lifted loop. Slices whose
        // base is NOT this leaf kind (nested trees) still route through the tree call below.
        ee.closeOpen(s"case s: SliceNode if s.base.isInstanceOf[${k.name}Arr] =>")
        ee.line(s"val a = s.base.asInstanceOf[${k.name}Arr].arr")
        ee.line("val so = s.offset")
        ee.line("val sn = s.length")
        ee.line(s"var acc: $accT = z")
        if !backward then {
          ee.line("var i = so")
          ee.line("val e = so + sn")
          ee.open("while (i < e)")
          ee.line("acc = f.apply(acc, a(i))")
          ee.line("i += 1")
          ee.close()
        } else {
          ee.line("var i = so + sn - 1")
          ee.open("while (i >= so)")
          ee.line("acc = f.apply(acc, a(i))")
          ee.line("i -= 1")
          ee.close()
        }
        ee.line("acc")
        ee.closeOpen("case _ =>")
        ee.line(treeCall)
        ee.close() // case _
        ee.close() // xs match
      }
      opKinds.foreach { k =>
        val K = k.name
        List(false, true).foreach { backward =>
          val dir = if backward then "Bwd" else "Fwd"
          // prim self-kind unboxed acc (only for primitive input kinds).
          if k.isPrim then emit(s"reduceLeaf${dir}${K}${K}", k, "", k.arr, s"Traversers.${K}To${K}Fold", s"Traversers.reduce${dir}${K}${K}(xs, z, f)", backward)
          // generic ref acc (every input kind).
          emit(s"reduceLeaf${dir}${K}Ref", k, "[Z <: AnyRef]", "Z", s"Traversers.${K}ToRefFold[Z]", s"Traversers.reduce${dir}${K}Ref[Z](xs, z, f)", backward)
        }
      }
      // CROSS-kind: Ref ELEMENT folded into a PRIMITIVE accumulator (sum/count over Strings etc.). The hot
      // Empty/One/leaf path keeps the acc UNBOXED (a primitive `${P}` local + the RefTo${P}Fold SAM). Only the
      // COLD genuine-tree path (`case _`) boxes: it routes through the existing generic Ref-acc Java traverser
      // (reduce${dir}RefRef) with the primitive acc boxed to its wrapper, then unboxes the result — boxing only
      // on the rare tree, never per-element on a flat leaf.
      primKinds.foreach { p =>
        val P = p.name
        val refK = opKinds.last // the Ref kind
        List(false, true).foreach { backward =>
          val dir = if backward then "Bwd" else "Fwd"
          val box = p.prim.get.wrapper // java.lang.Integer / Long / Double
          val treeCall =
            s"${p.unbox(s"Traversers.reduce${dir}RefRef[$box](xs, ${p.box("z")}, (acc, v) => ${p.box(s"f.apply(${p.unbox("acc")}, v)")})")}"
          emit(s"reduceLeaf${dir}Ref${P}", refK, "", p.arr, s"Traversers.RefTo${P}Fold", treeCall, backward)
        }
      }
      ee.result
    }
    // SHARED LEAF METHODS for the BUILD shape (map/mapConserve), one per covered (I, O) pair — the EXACT
    // analogue of the reduceLeaf* family. Each is a SMALL NON-inline method in FArrayOps: it peels
    // Empty/${I}One/top-${I}Arr with the unboxed leaf loop calling the realized Build SAM `f.apply`, and for a
    // genuine TREE makes ONE CALL to the Java build traverser (Traversers.buildFwd${I}${O}), then canonicalizes
    // the filled `out` via the leaf() builder (length-0 -> Empty, length-1 -> ${O}One). NO inlined leaf loop in
    // the inline surface — the surface only realizes the user lambda into the Build SAM and CALLS mapLeaf${I}${O}.
    // The Ref ends are type params (the SAM closes over the user's `f`/`r`/`rb`, so the leaf method is op-agnostic
    // per (I, O)). Output canonicalisation upholds the size-0/1 invariant.
    val mapLeafMethods = {
      val ee = new Emit("  ")
      // emit ONE shared Build leaf method for (ki, ko). `riT`/`roT` are the Scala-side input/output element
      // types bound to the Ref tparams; `fnScala` is the Scala reference to the (I,O) Build SAM type;
      // `treeCall` is the Java build traverser forwarder; `outAlloc`/`oneCtor`/`leafCanon` build the result.
      def emit(ki: Kind, ko: Kind): Unit = {
        val KI = ki.name
        val KO = ko.name
        // type params: only a Ref OUTPUT carries a tparam (RO <: AnyRef). A Ref input element is plain Object.
        val tps = if !ko.isPrim then List("RO <: AnyRef") else Nil
        val tpClause = if tps.isEmpty then "" else s"[${tps.mkString(", ")}]"
        // the Scala reference to the Java SAM (Ref OUTPUT tparam filled with RO).
        val fnScala = "Traversers." + buildFnType(ki, ko, "RO").replace('<', '[').replace('>', ']')
        // input element read in the leaf loop: prim arrays hold the prim; a Ref input leaf array is Object[] and
        // the SAM's apply takes Object, so `a(i)` (an Object) is passed straight through — no cast.
        val readElem = "a(i)"
        val readOneElem = "o.elem"
        // output array allocation + write: prim O writes the returned prim straight; a Ref O leaf array is
        // Object[] and the SAM returns RO (<: AnyRef), stored as an Object.
        val outAlloc = if ko.isPrim then s"new Array[${ko.arr}](n)" else "new Array[Object](n)"
        val writeOut = if ko.isPrim then "out(i) = f.apply(_RD_)" else "out(i) = f.apply(_RD_).asInstanceOf[Object]"
        // ${O}One ctor for the size-1 leaf: prim takes the prim, Ref takes Object.
        val oneArg = if ko.isPrim then s"f.apply($readOneElem)" else s"f.apply($readOneElem).asInstanceOf[Object]"
        ee.open(s"def mapLeaf${KI}${KO}$tpClause(xs: FBase, f: $fnScala): FBase = xs match")
        ee.line("case e: Empty => Empty.INSTANCE")
        ee.line(s"case o: ${KI}One => new ${KO}One($oneArg)")
        ee.open(s"case leaf: ${KI}Arr =>")
        ee.line("val a = leaf.arr")
        ee.line("val n = leaf.length")
        ee.line(s"val out = $outAlloc")
        ee.line("var i = 0")
        ee.open("while (i < n)")
        ee.line(writeOut.replace("_RD_", readElem))
        ee.line("i += 1")
        ee.close()
        ee.line(s"new ${KO}Arr(out, n)")
        ee.closeOpen("case _ =>")
        ee.line("val n = xs.length")
        ee.line(s"val out = $outAlloc")
        // the Java build traverser; a Ref output passes RO explicitly so Scala threads the tparam.
        val refClause = if !ko.isPrim then "[RO]" else ""
        ee.line(s"val k = Traversers.buildFwd${KI}${KO}$refClause(xs, out, 0, f)")
        ee.line(leaf(ko, "out", "k"))
        ee.close() // case _
        ee.close() // xs match
      }
      buildPairs.foreach { case (ki, ko) => emit(ki, ko) }
      ee.result
    }
    // SHARED LEAF METHODS for the BUILD-FILTERED shape (filter/filterNot), one per input kind — the EXACT analogue
    // of mapLeaf*. Each is a SMALL NON-inline method in FArrayOps: it allocates ONE max-size `out` of length n,
    // peels Empty (-> Empty), ${I}One (1-elt predicate test), top-${I}Arr (the unboxed leaf loop with a
    // predicate-GUARDED append `if (p) { out(o) = e; o += 1 }`), and routes a genuine TREE through the Java
    // buildFiltered${I} traverser (which returns the kept-count). Output is canonicalised on the kept-count k:
    // k == 0 -> Empty; k == 1 -> ${I}One(out(0)); else a ${I}Arr wrapping `out` directly with logical length k
    // (k <= n; NO trim copy — the leaf carries slack `n - k` and iterates to `length`). The
    // ${I}Pred SAM closes over the user predicate (does the A wrap), so the leaf method is op-agnostic per input
    // kind. NO inlined leaf loop in the inline surface — the surface only realizes the predicate and CALLS this.
    val filterLeafMethods = {
      val ee = new Emit("  ")
      opKinds.foreach { k =>
        val K = k.name
        // the size-1/0 canonicaliser: k == 0 -> Empty, k == 1 -> ${K}One, else `out` wrapped directly with logical
        // length k (k <= n; NO trim copy — slack `n - k`).
        ee.open(s"def filterLeaf${K}(xs: FBase, p: Traversers.${K}Pred): FBase =")
        ee.open("xs match")
        ee.line("case e: Empty => Empty.INSTANCE")
        ee.line(s"case o: ${K}One => if (p.apply(o.elem)) xs else Empty.INSTANCE")
        ee.open(s"case leaf: ${K}Arr =>")
        ee.line("val a = leaf.arr")
        ee.line("val n = leaf.length")
        ee.line(s"val out = new Array[${k.arr}](n)")
        ee.line("var i = 0")
        ee.line("var o = 0")
        ee.open("while (i < n)")
        ee.line("val e = a(i)")
        ee.line("if (p.apply(e)) { out(o) = e; o += 1 }")
        ee.line("i += 1")
        ee.close()
        // o <= n. o == n -> nothing filtered, reuse xs (reference identity). Else hand `out` through with logical
        // length `o` — SKIP the trim copy (slack = n - o); leaves iterate to `length`.
        ee.line(s"if (o == n) xs else if (o == 0) Empty.INSTANCE else if (o == 1) new ${K}One(out(0)) else new ${K}Arr(out, o)")
        ee.closeOpen("case _ =>")
        ee.line("val n = xs.length")
        ee.line(s"val out = new Array[${k.arr}](n)")
        ee.line(s"val o = Traversers.buildFiltered${K}(xs, out, 0, p)")
        ee.line(
          s"if (o == 0) Empty.INSTANCE else if (o == 1) new ${K}One(out(0)) else new ${K}Arr(out, o)"
        )
        ee.close()
        ee.close() // xs match
        ee.close() // def
      }
      ee.result
    }
    // SHARED LEAF METHODS for the PARTITION shape, one per input kind — the dual-output analogue of filterLeaf*.
    // ONE pass writes kept-true into outA[oa++] and kept-false into outB[ob++]; both outputs are trimmed leaves of
    // the SAME input kind. Empty -> (Empty, Empty); ${I}One -> (xs, Empty) or (Empty, xs) by the predicate; a
    // top-${I}Arr runs the unboxed leaf loop routing each element to outA/outB; a genuine TREE routes through the
    // Java partitionFwd${I} traverser, which PACKS the two cursors into a long (oa in the low 32 bits, ob in the
    // high 32 bits) — Java can't return two ints. Each output is canonicalised on its own count via leafTrim. The
    // ${I}Pred SAM closes over the user predicate (does the A wrap), so the leaf method is op-agnostic per input
    // kind. NO inlined leaf loop in the inline surface — the surface only realizes the predicate and CALLS this.
    val partitionLeafMethods = {
      val ee = new Emit("  ")
      opKinds.foreach { k =>
        val K = k.name
        // canonicalise ONE output buffer `b` filled to length `c` (c <= b.length) into a leaf — SKIP the trim
        // copy: hand `b` through with logical length `c` (slack = b.length - c); leaves iterate to `length`.
        def trim(b: String, c: String): String =
          s"if ($c == 0) Empty.INSTANCE else if ($c == 1) new ${K}One($b(0)) else new ${K}Arr($b, $c)"
        ee.open(s"def partitionLeaf${K}(xs: FBase, p: Traversers.${K}Pred): scala.Tuple2[FBase, FBase] =")
        ee.open("xs match")
        ee.line("case e: Empty => new scala.Tuple2(Empty.INSTANCE, Empty.INSTANCE)")
        ee.line(s"case o: ${K}One => if (p.apply(o.elem)) new scala.Tuple2(xs, Empty.INSTANCE) else new scala.Tuple2(Empty.INSTANCE, xs)")
        ee.open(s"case leaf: ${K}Arr =>")
        ee.line("val a = leaf.arr")
        ee.line("val n = leaf.length")
        ee.line(s"val outA = new Array[${k.arr}](n)")
        ee.line(s"val outB = new Array[${k.arr}](n)")
        ee.line("var i = 0")
        ee.line("var oa = 0")
        ee.line("var ob = 0")
        ee.open("while (i < n)")
        ee.line("val e = a(i)")
        ee.line("if (p.apply(e)) { outA(oa) = e; oa += 1 } else { outB(ob) = e; ob += 1 }")
        ee.line("i += 1")
        ee.close()
        // oa == n -> nothing went to B, reuse xs for the A side (reference identity); ob == n likewise for B.
        ee.line(s"val ra = if (oa == n) xs else ${trim("outA", "oa")}")
        ee.line(s"val rb = if (ob == n) xs else ${trim("outB", "ob")}")
        ee.line("new scala.Tuple2(ra, rb)")
        ee.closeOpen("case _ =>")
        ee.line("val n = xs.length")
        ee.line(s"val outA = new Array[${k.arr}](n)")
        ee.line(s"val outB = new Array[${k.arr}](n)")
        ee.line(s"val packed = Traversers.partitionFwd${K}(xs, outA, outB, 0L, 0L, p)")
        ee.line("val oa = (packed & 0xffffffffL).toInt")
        ee.line("val ob = (packed >>> 32).toInt")
        ee.line(s"new scala.Tuple2(${trim("outA", "oa")}, ${trim("outB", "ob")})")
        ee.close()
        ee.close() // xs match
        ee.close() // def
      }
      ee.result
    }
    // SHARED LEAF METHODS for the FOREACH shape, one per input kind — the EXACT analogue of reduceLeaf*. Each is a
    // SMALL NON-inline method in FArrayOps: it peels Empty (no-op), ${I}One (one f.apply), top-${I}Arr (the
    // unboxed leaf loop bounded on a.length) and routes a genuine TREE through Traversers.foreachFwd${I}. NO
    // inlined leaf loop in the surface — the surface only realizes the user lambda into the ${I}Consumer SAM and
    // CALLS foreachLeaf${I}. The SAM wraps the raw element `v` to A itself (closes over the user `f`/`r`), so the
    // leaf method is op-agnostic per input kind.
    val foreachLeafMethods = {
      val ee = new Emit("  ")
      opKinds.foreach { k =>
        val K = k.name
        ee.open(s"def foreachLeaf${K}(xs: FBase, f: Traversers.${K}Consumer): Unit = xs match")
        ee.line("case e: Empty => ()")
        ee.line(s"case o: ${K}One => f.apply(o.elem)")
        ee.open(s"case leaf: ${K}Arr =>")
        ee.line("val a = leaf.arr")
        ee.line("val n = leaf.length")
        ee.line("var i = 0")
        ee.open("while (i < n)")
        ee.line("f.apply(a(i))")
        ee.line("i += 1")
        ee.close()
        ee.closeOpen("case _ =>")
        ee.line(s"Traversers.foreachFwd${K}(xs, f)")
        ee.close()
        ee.close()
      }
      ee.result
    }
    // SHARED LEAF METHODS for the SCAN shape, one per covered (I, Z) pair × {Left, Right} — the EXACT analogue of
    // reduceLeaf*. The result is ALWAYS length n+1 (so Empty => ${Z}One(z), NEVER Empty). scanLeft writes the
    // running accumulator ascending with out[0] = z and out[i+1] = acc = f(acc, a(i)); scanRight writes
    // descending with out[n] = z. A genuine TREE makes ONE CALL to Traversers.scan{Fwd,Bwd}${I}${Z} (which reads
    // the running acc from out[o±1] — no acc threading), then wraps `out` into a ${Z}Arr (length n+1 >= 1, so it
    // is never Empty and the size-1 case is the One peeled above). NO inlined leaf loop in the surface.
    val scanLeafMethods = {
      val ee = new Emit("  ")
      // emit ONE shared scan leaf method. `zT` = the accumulator Scala type; `tparam` = a leading type-param
      // clause (Ref acc is generic); `foldT` = the SAM type; `outAlloc` builds the (n+1) output array; `store`
      // wraps an acc value for the Object[] of a Ref acc (no-op for a prim); `treeCall` = the Java tree forwarder.
      def emit(k: Kind, refAcc: Boolean, backward: Boolean): Unit = {
        val K = k.name
        val side = if backward then "Right" else "Left"
        val name = s"scan${side}Leaf${K}${if refAcc then "Ref" else K}"
        val zT = if refAcc then "Z" else k.arr
        val tparam = if refAcc then "[Z <: AnyRef]" else ""
        val foldT = if refAcc then s"Traversers.${K}ToRefFold[Z]" else s"Traversers.${K}To${K}Fold"
        val outArr = if refAcc then "Object" else k.arr
        val store = (e: String) => if refAcc then s"($e).asInstanceOf[Object]" else e
        // read the previously-written acc slot, cast to Z for a Ref acc.
        val readPrev = (slot: String) => if refAcc then s"out($slot).asInstanceOf[Z]" else s"out($slot)"
        // result wrapper: out has length n+1 (>= 1). size-1 (n == 0) was peeled to the ${K}One(z); a leaf with
        // n >= 1 yields n+1 >= 2, always a ${Z}Arr.
        val oneCtor = s"new ${if refAcc then "Ref" else K}One(${store("z")})"
        ee.open(s"def $name$tparam(xs: FBase, z: $zT, f: $foldT): FBase = xs match")
        ee.line(s"case e: Empty => $oneCtor")
        // ${K}One: n == 1 -> n+1 == 2 elements. scanLeft = [z, f(z, elem)]; scanRight = [f(elem, z), z].
        ee.open(s"case o: ${K}One =>")
        ee.line(s"val out = new Array[$outArr](2)")
        if !backward then {
          ee.line(s"out(0) = ${store("z")}")
          ee.line(s"out(1) = ${store(s"f.apply(${readPrev("0")}, o.elem)")}")
        } else {
          ee.line(s"out(1) = ${store("z")}")
          ee.line(s"out(0) = ${store(s"f.apply(${readPrev("1")}, o.elem)")}")
        }
        ee.line(s"new ${if refAcc then "Ref" else K}Arr(out, 2)")
        ee.closeOpen(s"case leaf: ${K}Arr =>")
        ee.line("val a = leaf.arr")
        ee.line("val n = leaf.length")
        ee.line(s"val out = new Array[$outArr](n + 1)")
        // The running acc is a REGISTER local, not a reload of out(i): scan is a serial dependency
        // chain, so `acc = f(out(i), a(i))` pays a store->load forward (~4-5 cycles) per element
        // where the register form pays the op alone (ScanIntBenchmark measured 0.22-0.40x of iarray
        // with the out(i) reload).
        if !backward then {
          ee.line(s"out(0) = ${store("z")}")
          ee.line(s"var acc: $zT = z")
          ee.line("var i = 0")
          ee.open("while (i < n)")
          ee.line("acc = f.apply(acc, a(i))")
          ee.line(s"out(i + 1) = ${store("acc")}")
          ee.line("i += 1")
          ee.close()
        } else {
          ee.line(s"out(n) = ${store("z")}")
          ee.line(s"var acc: $zT = z")
          ee.line("var i = n - 1")
          ee.open("while (i >= 0)")
          ee.line("acc = f.apply(acc, a(i))")
          ee.line(s"out(i) = ${store("acc")}")
          ee.line("i -= 1")
          ee.close()
        }
        ee.line(s"new ${if refAcc then "Ref" else K}Arr(out, n + 1)")
        ee.closeOpen("case _ =>")
        ee.line("val n = xs.length")
        ee.line(s"val out = new Array[$outArr](n + 1)")
        val refClause = if refAcc then "[Z]" else ""
        if !backward then {
          ee.line(s"out(0) = ${store("z")}")
          ee.line(s"Traversers.scanFwd${K}${if refAcc then "Ref" else K}$refClause(xs, out, 1, f)")
        } else {
          ee.line(s"out(n) = ${store("z")}")
          ee.line(s"Traversers.scanBwd${K}${if refAcc then "Ref" else K}$refClause(xs, out, n - 1, f)")
        }
        ee.line(s"new ${if refAcc then "Ref" else K}Arr(out, n + 1)")
        ee.close()
        ee.close()
      }
      opKinds.foreach { k =>
        List(false, true).foreach { backward =>
          if k.isPrim then emit(k, refAcc = false, backward)
          emit(k, refAcc = true, backward)
        }
      }
      ee.result
    }
    // SHARED LEAF METHODS for the SHORT-CIRCUIT shape, one PAIR (Fwd + Bwd) per INPUT kind. The EXACT analogue of
    // foreachLeaf*: peel Empty / ${I}One / top-${I}Arr inline (the unboxed clamped predicate-in-condition leaf
    // scan), and route a genuine TREE through Traversers.sc{Fwd,Bwd}${I}. Returns the FIRST-HIT GLOBAL index, or
    // `xs.length` (Fwd no-hit) / -1 (Bwd no-hit) — the 12 surface ops derive every answer from that index. The
    // ${I}Pred SAM takes the RAW element (prim/Object) and closes over the user predicate (it does the A wrap).
    // Fwd clamps with `from` (skip indices < from); Bwd clamps with `end` (skip indices > end).
    val scLeafMethods = {
      val ee = new Emit("  ")
      opKinds.foreach { k =>
        val K = k.name
        // ---- forward: scFwdLeaf${K}(xs, from, p): Int -- first index >= from where p holds, else length ----
        ee.open(s"def scFwdLeaf${K}(xs: FBase, from: Int, p: Traversers.${K}Pred): Int = xs match")
        ee.line("case e: Empty => 0")
        ee.line(s"case o: ${K}One => if (from <= 0 && p.apply(o.elem)) 0 else 1")
        ee.open(s"case leaf: ${K}Arr =>")
        ee.line("val a = leaf.arr")
        ee.line("val n = leaf.length")
        ee.line("var i = if (from < 0) 0 else from")
        ee.line("while (i < n && !p.apply(a(i))) i += 1")
        ee.line("i")
        ee.closeOpen("case _ =>")
        ee.line(s"val r = Traversers.scFwd${K}(xs, from, 0, p)")
        ee.line("if (r >= 0) r else ~r - 1")
        ee.close()
        ee.close()
        // ---- backward: scBwdLeaf${K}(xs, end, p): Int -- last index <= end where p holds, else -1 ----
        ee.open(s"def scBwdLeaf${K}(xs: FBase, end: Int, p: Traversers.${K}Pred): Int = xs match")
        ee.line("case e: Empty => -1")
        ee.line(s"case o: ${K}One => if (end >= 0 && p.apply(o.elem)) 0 else -1")
        ee.open(s"case leaf: ${K}Arr =>")
        ee.line("val a = leaf.arr")
        ee.line("var i = if (end > leaf.length - 1) leaf.length - 1 else end")
        ee.line("while (i >= 0 && !p.apply(a(i))) i -= 1")
        ee.line("if (i < 0) -1 else i")
        ee.closeOpen("case _ =>")
        ee.line(s"val r = Traversers.scBwd${K}(xs, end, xs.length - 1, p)")
        ee.line("if (r >= 0) r else -1")
        ee.close()
        ee.close()
      }
      ee.result
    }
    val foldLeft = {
      val ee = new Emit("  ")
      ee.open("summonFrom")
      opKinds.foreach { k =>
        ee.open(s"case r: ${k.name}Repr[A] =>")
        ee.lines(foldSurface(k, backward = false))
        ee.close()
      }
      ee.line(s"case _ => ${noReprErr("A")}")
      ee.close()
      ee.result
    }
    // SHORT-CIRCUIT shape (design §B.4): the 12 breakable ops are THIN inline-def wrappers over the shared leaf
    // methods sc{Fwd,Bwd}Leaf${I} (Empty/One/leaf peeled inline; genuine TREES -> Traversers.sc{Fwd,Bwd}${I},
    // which return the FIRST-HIT GLOBAL index and ABANDON the lazy stack via the method return). Each wrapper
    // summons the input kind I, realizes the user predicate into the ${I}Pred SAM (which wraps the raw element to
    // A), calls the leaf method, and derives its answer from the returned index. NO inlined walk here. `length` is
    // xs.length: scFwdLeaf returns < length on a hit / == length on no-hit; scBwdLeaf returns the index / -1.
    // `wrapV(k)` turns the raw SAM element `v` into A. forall/segmentLength/prefixLength/foreachWhile scan for the
    // FIRST element that FAILS the user predicate (the SAM is the negation). find/collectFirst do exactly ONE
    // applyAtImpl(hit) — one random access on the found index, never per-element.
    def predSAM(k: Kind, cond: String): String = s"(v) => $cond"
    val existsV = dispatchA(k => s"scFwdLeaf${k.name}(xs, 0, ${predSAM(k, s"p(${wrapV(k)})")}) < length")
    val forallV = dispatchA(k => s"scFwdLeaf${k.name}(xs, 0, ${predSAM(k, s"!p(${wrapV(k)})")}) == length")
    val findV =
      dispatchA(k => s"{ val i = scFwdLeaf${k.name}(xs, 0, ${predSAM(k, s"p(${wrapV(k)})")}); if (i < length) Some(applyAtImpl[A](xs, i)) else None }")
    val indexWhereV =
      dispatchA(k => s"{ val i = scFwdLeaf${k.name}(xs, if (from < 0) 0 else from, ${predSAM(k, s"p(${wrapV(k)})")}); if (i < length) i else -1 }")
    val indexOfV =
      dispatchA(k => s"{ val i = scFwdLeaf${k.name}(xs, if (from < 0) 0 else from, ${predSAM(k, s"${wrapV(k)} == elem")}); if (i < length) i else -1 }")
    val collectFirstV = dispatchA(k =>
      s"{ val i = scFwdLeaf${k.name}(xs, 0, ${predSAM(k, s"pf.isDefinedAt(${wrapV(k)})")}); if (i < length) Some(pf(applyAtImpl[A](xs, i))) else None }"
    )
    val segmentLenV = dispatchA(k =>
      s"{ val f0 = if (from < 0) 0 else if (from > length) length else from; scFwdLeaf${k.name}(xs, f0, ${predSAM(k, s"!p(${wrapV(k)})")}) - f0 }"
    )
    val prefixLenV = dispatchA(k => s"scFwdLeaf${k.name}(xs, 0, ${predSAM(k, s"!p(${wrapV(k)})")})")
    val lastIndexWhereV = dispatchA(k => s"scBwdLeaf${k.name}(xs, if (end > length - 1) length - 1 else end, ${predSAM(k, s"p(${wrapV(k)})")})")
    val lastIndexOfV = dispatchA(k => s"scBwdLeaf${k.name}(xs, if (end > length - 1) length - 1 else end, ${predSAM(k, s"${wrapV(k)} == elem")})")
    // count/sum/product: hybrid Reduce surfaces over the shared leaf method (design §2.1/§5). Each ONLY
    // realizes the combine SAM and CALLS reduceLeafFwd${I}${I} (which peels Empty/One/leaf and routes genuine
    // TREES through Traversers) — NO inlined leaf loop. The SAME shared leaf machinery as foldLeft. Prim inputs
    // stay unboxed (acc a primitive local); the Ref input (and, for count, any non-Int prim input — Z=Int would
    // be a cross-prim fold) keeps the old walk.
    //
    // sum/product: combine `acc + e` / `acc * e` (raw prim op == num.plus/num.times for Int/Long/Double); the
    // unboxed acc is boxed to B (== the input kind at runtime for a prim FArray) only at the boundary.
    def sumProductSurface(k: Kind, combine: (String, String) => String, seed: String, isProduct: Boolean): String = {
      val K = k.name
      // Non-NUMERIC kinds (Ref input, and the prim kinds with no Numeric — Char/Boolean): route through the
      // generic Numeric path (`num.plus`/`num.times` on B). The surface requires `Numeric[B]`, so a Char/Boolean
      // `sum` never actually compiles at the use site — but the codegen branch must still typecheck, and this
      // boxed-acc form does. (For Ref it's the real, cold path.)
      if !k.isPrim || !numericKinds.exists(_.name == K) then {
        val numOp = if isProduct then "num.times" else "num.plus"
        val refSeed = if isProduct then "num.one" else "num.zero"
        val toB = if k.isPrim then s"${k.box("v")}.asInstanceOf[B]" else s"r.wrap(v.asInstanceOf[A]).asInstanceOf[B]"
        return s"reduceLeafFwd${K}Ref[AnyRef](xs, ($refSeed).asInstanceOf[AnyRef], (acc, v) => $numOp(acc.asInstanceOf[B], $toB).asInstanceOf[AnyRef]).asInstanceOf[B]"
      }
      // NUMERIC prim: realize the unboxed `${K}To${K}Fold` SAM (acc stays a primitive) and CALL the shared leaf
      // method — NO inlined leaf loop. The leaf method peels Empty/One/leaf and routes trees through Traversers.
      // Short/Byte arithmetic WIDENS to Int in Scala (`short + short: Int`), so narrow the combine back to the
      // SAM's element type for those kinds.
      val narrow = (e: String) => K match { case "Short" => s"($e).toShort"; case "Byte" => s"($e).toByte"; case _ => e }
      s"reduceLeafFwd${K}${K}(xs, $seed, (acc, v) => ${narrow(combine("acc", "v"))}).asInstanceOf[B]"
    }
    // count: Reduce whose combine folds a predicate into an Int. Only the Int input kind has a usable unboxed
    // self-kind shared leaf method (reduceLeafFwdIntInt); other prim inputs (Long/Double) and Ref would need
    // an I->Int fold the engine doesn't emit (cross-prim), so they stay on the old lowered walk.
    def countSurface(k: Kind): String = {
      if k.name != "Int" then {
        // cold cross-kind count (Long/Double/Ref input would need an I->Int fold the engine doesn't emit):
        // route through the SAME generic Ref-acc shared leaf method with the Int acc BOXED to Integer —
        // NO inlined leaf loop. These cases are cold/rare, so the boxing is fine.
        return s"reduceLeafFwd${k.name}Ref[java.lang.Integer](xs, java.lang.Integer.valueOf(0), (acc, v) => java.lang.Integer.valueOf(if (p(${wrapV(k)})) acc.intValue + 1 else acc.intValue)).intValue"
      }
      // realize the unboxed IntToIntFold SAM (combine folds the predicate into an Int acc) and CALL the
      // shared leaf method — NO inlined leaf loop.
      "reduceLeafFwdIntInt(xs, 0, (acc, v) => if (p(r.wrap(v))) acc + 1 else acc)"
    }
    val countV = dispatchA(k => countSurface(k))
    val sumV = dispatchA(k => sumProductSurface(k, (acc, e) => s"$acc + $e", k.dflt, isProduct = false))
    val productV = {
      def one(k: Kind) = k.name match { case "Long" => "1L"; case "Double" => "1.0"; case "Float" => "1.0f"; case _ => "1" }
      dispatchA(k => sumProductSurface(k, (acc, e) => s"$acc * $e", one(k), isProduct = true))
    }
    // HYBRID Scan surface (design §2.1/§5): summon the input kind I (outer) AND the accumulator kind Z (= B,
    // inner) — exactly like foldLeft's Z-dispatch. Realize the user op into the ${I}To${Z}Fold SAM and CALL the
    // shared leaf method scan${Side}Leaf${I}${Z} (which peels Empty/One/leaf, result ALWAYS length n+1, tree ->
    // Traversers.scan{Fwd,Bwd}). Prim Z stays UNBOXED via the self-kind unboxed fold; Z=Ref uses the generic
    // ref-acc fold (acc already an object). A CROSS-prim (I, Z) (e.g. Long acc over Int input) is UNCOVERED —
    // the engine emits no I->Z fold — so it routes through the GENERIC per-index path (boxed applyAtImpl;
    // cold/rare, noted). scanLeft's op is (B,A)=>B == f.apply(acc, elem); scanRight's op is (A,B)=>B, so the
    // SAM applies op(elem, acc) — argument order swapped, same out[o±1] acc machinery.
    //
    // wrap the SAM's acc/elem params back to (Z=B)/A and combine in the op's required order.
    def scanComb(k: Kind, backward: Boolean, accE: String, vE: String): String =
      if backward then s"op($vE, $accE)" else s"op($accE, $vE)"
    // GENERIC fallback for an UNCOVERED (I, Z) cross-prim pair: allocate the Array[Z] of length n+1 and fill it
    // per-element with a boxed applyAtImpl read. scanLeft fills ascending (out(0)=z); scanRight fills descending
    // (out(n)=z). Length is ALWAYS n+1 -> always >= 1, leaf-canonicalised (Empty never; size-1 -> ${Z}One).
    def scanGeneric(kb: Kind, backward: Boolean): String = {
      val alloc = if kb.name == "Ref" then "new Array[Object](n + 1)" else s"new Array[${kb.arr}](n + 1)"
      if !backward then
        s"{ val out = $alloc; var acc: B = z; out(0) = ${wr(kb, "rb.unwrap(z)")}; var i = 0; while (i < n) { acc = op(acc, applyAtImpl[A](xs, i)); out(i + 1) = ${wr(kb, "rb.unwrap(acc)")}; i += 1 }; ${leaf(kb, "out", "n + 1")} }"
      else
        s"{ val out = $alloc; var acc: B = z; out(n) = ${wr(kb, "rb.unwrap(z)")}; var i = n - 1; while (i >= 0) { acc = op(applyAtImpl[A](xs, i), acc); out(i) = ${wr(kb, "rb.unwrap(acc)")}; i -= 1 }; ${leaf(kb, "out", "n + 1")} }"
    }
    // the set of (I, Z) pairs the scan shared-leaf path covers: prim self (I == Z) + every input kind paired with
    // a Ref acc. (Same coverage as Reduce.)
    val scanCovered: Set[(String, String)] = (opKinds.filter(_.isPrim).map(k => (k.name, k.name)) ++ opKinds.map(k => (k.name, "Ref"))).toSet
    // realize the SAM for a COVERED (ka, kb) and call scan${Side}Leaf${I}${Z}.
    def scanLeafCall(ka: Kind, kb: Kind, backward: Boolean): String = {
      val side = if backward then "Right" else "Left"
      val K = ka.name
      if kb.isPrim then // prim self-kind acc (kb == ka): unboxed ${K}To${K}Fold, acc a primitive.
        s"scan${side}Leaf${K}${K}(xs, rb.unwrap(z), (acc, v) => rb.unwrap(${scanComb(ka, backward, "rb.wrap(acc)", wrapV(ka))}))"
      else // Z = Ref: generic ${K}ToRefFold[B & AnyRef], acc already an object.
        s"scan${side}Leaf${K}Ref[B & AnyRef](xs, z.asInstanceOf[B & AnyRef], (acc, v) => ${scanComb(ka, backward, "acc.asInstanceOf[B]", wrapV(ka))}.asInstanceOf[B & AnyRef])"
    }
    def scanSurface(backward: Boolean): String =
      withErr(
        "summonFrom {\n" + opKinds
          .map { ka =>
            val inner = "summonFrom {\n" + opKinds
              .map { kb =>
                val body = if scanCovered((ka.name, kb.name)) then scanLeafCall(ka, kb, backward) else scanGeneric(kb, backward)
                s"          case rb: ${kb.name}Repr[B] => $body"
              }
              .mkString("\n") + "\n        }"
            s"      case r: ${ka.name}Repr[A] => $inner"
          }
          .mkString("\n") + "\n    }",
        "A"
      )
    val scanLeftV = scanSurface(backward = false)
    val scanRightV = scanSurface(backward = true)
    val iteratorV = dispatchA(k =>
      s"xs match { case leaf: ${k.name}Arr => new ${k.name}Cursor(leaf.arr, leaf.length); case _ => new ${k.name}Cursor(materialize${k.name}(xs), xs.length) }"
    )
    // foreachWhile: a short-circuit scan for the FIRST element where f returns false (the SAM is the negation);
    // the hit index is discarded — we only need the side effects f produced up to the stop. ONE leaf-method call.
    val foreachWhileV = dispatchA(k => s"{ scFwdLeaf${k.name}(xs, 0, ${predSAM(k, s"!f(${wrapV(k)})")}); () }")
    // foldRight: the hybrid Bwd reduce surface — the EXACT mirror of foldLeft, dir = Bwd. A BACKWARD walk
    // hands elements last-to-first (foldRight order), applying op(elem, acc). Self-kind prim Z + Ref Z ride
    // the new Bwd engine traversers; cross-prim Z falls to the old lowered walk.
    val foldRightV = {
      val ee = new Emit("  ")
      ee.open("summonFrom")
      opKinds.foreach { k =>
        ee.open(s"case r: ${k.name}Repr[A] =>")
        ee.lines(foldSurface(k, backward = true))
        ee.close()
      }
      ee.line(s"case _ => ${noReprErr("A")}")
      ee.close()
      ee.result
    }
    // Wave 3: map lowers the Trav spec inline — `out`/`o` are LOCALS (a register-resident array ref + cursor),
    // the write spliced into the run loop. NO ${KA}Dfs object (the heap-lifted `o` that cost map at large N is
    // gone), NO dfsC call, monomorphic per (ka, kb) site. The result is canonicalized via leaf() (Empty/One).
    // The inner dispatch binds the result-kind repr as `rb` (NOT `r`), so it never shadows the outer `A`-repr `r`
    // that the read side (read(ka)) needs. (dispatchB binds `r`, so map can't reuse it — the body needs both.)
    def dispatchBmap(body: Kind => String): String =
      "summonFrom {\n" + opKinds.map(k => s"          case rb: ${k.name}Repr[B] => ${body(k)}").mkString("\n") + "\n        }"
    // The set of (I, O) pairs the Build shared-leaf path covers (prim-self + every kind<->Ref).
    val buildCovered: Set[(String, String)] = buildPairs.map { case (ki, ko) => (ki.name, ko.name) }.toSet
    // Realize the user's inline `f: A => B` into the (ka, kb) Build SAM lambda, then CALL mapLeaf${I}${O}. The
    // SAM's element param `v` is the RAW input (prim, or — for Ref input — the RI element bound to A); the body
    // wraps it to A via `r.wrap`, applies `f`, then unwraps the B result to the output prim via `rb.unwrap`
    // (Ref output stays an object, no unwrap). NO inlined leaf loop here — the shared leaf method does the loop.
    // `outTpe` is the Scala result element type (the `B` of map; for mapConserve, where A == B, it is `A`); a
    // Ref output uses `outTpe & AnyRef` as RO. `rbName` is the output-kind Repr binder (rb for map; r for the
    // conserve case where input == output kind, so the single repr `r` serves both).
    def mapLeafCall(ka: Kind, kb: Kind, outTpe: String, rbName: String): String = {
      // wrap the SAM param `v` to A: prim input -> r.wrap(v); Ref input -> v is Object, cast to A then wrap.
      val toA = if ka.isPrim then "r.wrap(v)" else "r.wrap(v.asInstanceOf[A])"
      // the body: f over the wrapped element, then unwrap to the output element (prim) or keep as object (Ref).
      val body = if kb.isPrim then s"$rbName.unwrap(f($toA))" else s"f($toA).asInstanceOf[$outTpe & AnyRef]"
      // Only a Ref OUTPUT supplies a tparam (outTpe & AnyRef = RO); a Ref input element is Object, no tparam.
      val tpClause = if !kb.isPrim then s"[$outTpe & AnyRef]" else ""
      s"mapLeaf${ka.name}${kb.name}$tpClause(xs, (v) => $body)"
    }
    // GENERIC fallback for an UNCOVERED (I, O) — the cross-prim pairs (Int->Long, Long->Double, …): allocate the
    // O array and fill it per-element via applyAtImpl (boxed read), preserving the output kind O. NOTED: these
    // cross-prim pairs do NOT use the shared-leaf/Java-traverser fast path; they are cold (a prim FArray mapped
    // to a DIFFERENT prim kind is rare). Output canonicalised via leaf().
    def mapGeneric(kb: Kind): String = {
      val alloc = if kb.name == "Ref" then "new Array[Object](n)" else s"new Array[${kb.arr}](n)"
      val write =
        if kb.name == "Ref" then "out(i) = f(applyAtImpl[A](xs, i)).asInstanceOf[Object]" else s"out(i) = ${wr(kb, "rb.unwrap(f(applyAtImpl[A](xs, i)))")}"
      s"{ val out = $alloc; var i = 0; while (i < n) { $write; i += 1 }; ${leaf(kb, "out", "n")} }"
    }
    val mapM = withErr(
      "summonFrom {\n" + opKinds
        .map { ka =>
          val inner = dispatchBmap(kb => if buildCovered((ka.name, kb.name)) then mapLeafCall(ka, kb, "B", "rb") else mapGeneric(kb))
          s"      case r: ${ka.name}Repr[A] => $inner"
        }
        .mkString("\n") + "\n    }",
      "A"
    )
    // HYBRID Build-filtered surface (design §2.1/§5): filter/filterNot route through the shared leaf method
    // filterLeaf${I} (Empty/One/leaf peeled inline with the predicate-guarded append; genuine TREES ->
    // Traversers.buildFiltered${I}, which keeps the SAME elements in source order — visit flips at each
    // ReverseNode while the WRITE stays forward out[o++]). The surface only summons the input kind, realizes the
    // user predicate into the ${I}Pred SAM (which wraps the raw element to A), and CALLS filterLeaf${I}. NO
    // inlined leaf loop here. filterNotImpl passes the NEGATED predicate. The `if (o == n) xs` reference-identity
    // shortcut lives inside the leaf method.
    val filter = dispatchA(k => s"filterLeaf${k.name}(xs, ${predSAM(k, s"p(${wrapV(k)})")})")
    // partition: ONE dual-output pass through the shared partitionLeaf${I} (kept-true -> _1, kept-false -> _2).
    // Same slim surface as filter — summon the input kind, realize the user predicate into the ${I}Pred SAM, CALL
    // the shared leaf method. The result FBase pair is wrapped to (FArray[A], FArray[A]) in FArray.scala.
    val partition = dispatchA(k => s"partitionLeaf${k.name}(xs, ${predSAM(k, s"p(${wrapV(k)})")})")
    // collect: filter+map FUSED in ONE pass. Output kind O (= B's kind) may DIFFER from the input kind I, so this
    // is a BOXED-but-SHARED path (NOTED): the PartialFunction is over A => B, so the element is boxed to A and the
    // B result is boxed regardless of O — unboxing the *output* would need a per-(I, O) `${I}To${O}Collect` SAM
    // matrix, deferred. We still avoid any surface inline leaf loop: the structural one pass is the EXISTING shared
    // foreachLeaf${I} traverser. The PF is applied via isDefinedAt + apply (a clean sentinel-free applyOrElse is
    // not viable here: a primitive B forces applyOrElse's B result to UNBOX the AnyRef sentinel -> CCE). Each kept B
    // unwraps into a growable, unboxed ${O}Group buffer (dispatch on O), so the OUTPUT stays unboxed even though the
    // per-element A/B handoff boxes. buf.toLeaf canonicalises (Empty/One/Arr).
    val collect = withErr(
      "summonFrom {\n" + opKinds
        .map { ka =>
          val inner = "summonFrom {\n" + opKinds
            .map { kb =>
              val add = s"buf.add(${wr(kb, "rb.unwrap(pf(a))")})"
              s"          case rb: ${kb.name}Repr[B] => { val buf = new ${kb.name}Group(); foreachLeaf${ka.name}(xs, (v) => { val a = ${wrapV(ka)}; if (pf.isDefinedAt(a)) $add }); buf.toLeaf }"
            }
            .mkString("\n") + "\n        }"
          s"      case r: ${ka.name}Repr[A] => $inner"
        }
        .mkString("\n") + "\n    }",
      "A"
    )
    // partitionMap: ONE pass, TWO outputs of (possibly) DIFFERENT kinds — Lefts of B's kind to _1, Rights of C's
    // kind to _2. Like collect this is a BOXED-but-SHARED path (NOTED): f returns Either[A1, A2] (an allocated
    // Either, the values boxed), so the per-element handoff boxes regardless of the output kinds; we keep the
    // OUTPUTS unboxed via two growable ${B}Group / ${C}Group buffers and reuse the EXISTING shared foreachLeaf${I}
    // traverser (no surface inline leaf loop). Each f(a) is matched Left/Right ONCE; the value unwraps into its
    // buffer. Both buffers canonicalise via toLeaf.
    val partitionMap = withErr(
      "summonFrom {\n" + opKinds
        .map { ka =>
          val m2 = "summonFrom {\n" + opKinds
            .map { kb =>
              val m3 = "summonFrom {\n" + opKinds
                .map { kc =>
                  val addL = s"bl.add(${wr(kb, "rb.unwrap(l)")})"
                  val addR = s"br.add(${wr(kc, "rc.unwrap(r)")})"
                  s"              case rc: ${kc.name}Repr[A2] => { val bl = new ${kb.name}Group(); val br = new ${kc.name}Group(); foreachLeaf${ka.name}(xs, (v) => { f(${wrapV(ka)}) match { case scala.util.Left(l) => $addL; case scala.util.Right(r) => $addR } }); new scala.Tuple2(bl.toLeaf, br.toLeaf) }"
                }
                .mkString("\n") + "\n            }"
              s"          case rb: ${kb.name}Repr[A1] => $m3"
            }
            .mkString("\n") + "\n        }"
          s"      case r: ${ka.name}Repr[A] => $m2"
        }
        .mkString("\n") + "\n    }",
      "A"
    )
    // fused short-circuiting contains (prim compares unboxed against the unwrapped elem). Empty-body scan.
    // contains: a short-circuit forward scan for the first element == elem (the ${I}Pred wraps the raw element to
    // A and compares to elem). One leaf-method call; hit -> index < length.
    val contains = dispatchA(k => s"scFwdLeaf${k.name}(xs, 0, ${predSAM(k, s"${wrapV(k)} == elem")}) < length")
    val applyAt = dispatchA(k => if k.name == "Ref" then s"r.wrap(${k.lc}At(xs, i).asInstanceOf[A])" else s"r.wrap(${k.lc}At(xs, i))")
    // mapConserve: I == O == k (always a COVERED pair — prim-self + Ref->Ref). SAME slim surface as map, plus
    // the identity-check: scan for the first element f actually changes (reference !eq); if NONE, return xs with
    // NO allocation. On a change, build the result through the SHARED leaf method mapLeaf${K}${K} (re-applying f
    // over the whole array — the conserve optimisation is the no-change fast path, not prefix-sharing). NO
    // inlined leaf build loop here. (Prims always "change" — boxed eq — so they always rebuild like map, which
    // is correct: a primitive can't be conserved.)
    val mapConserve = dispatchA { k =>
      val readEl = if k.name == "Ref" then "sa(i).asInstanceOf[A]" else "sa(i)"
      val readElNode = if k.name == "Ref" then s"${k.lc}At(xs, i).asInstanceOf[A]" else s"${k.lc}At(xs, i)"
      val build = mapLeafCall(k, k, "A", "r")
      val scanLeaf =
        s"{ val sa = leaf.arr; val n = leaf.length; var i = 0; var changed = false; while (i < n && !changed) { val a = ${readVal(k, readEl)}; if (!(f(a).asInstanceOf[AnyRef] eq a.asInstanceOf[AnyRef])) changed = true; i += 1 }; if (!changed) xs else $build }"
      val scanNode =
        s"{ val n = xs.length; var i = 0; var changed = false; while (i < n && !changed) { val a = ${readVal(k, readElNode)}; if (!(f(a).asInstanceOf[AnyRef] eq a.asInstanceOf[AnyRef])) changed = true; i += 1 }; if (!changed) xs else $build }"
      s"xs match { case leaf: ${k.name}Arr => $scanLeaf; case _ => $scanNode }"
    }
    // ONE pass, no inner buffer: dfs the source, and for each element append f(x)'s elements straight into a
    // growing output array (inline grow). The inner FArray is created, matched, and arraycopied within the
    // loop body, never stored -> escape analysis drops its wrapper, so each inner costs only its int[] (like
    // Array). dispatchA x dispatchB resolves to ONE case per concrete site (1 dfs inlined). Non-leaf inners
    // fall back to the non-inline flatMapCopyOne.
    // One pass: size the output from the FIRST inner (cnt * firstLen) so a uniform flatMap (the common case)
    // never grows or trims — only non-uniform inner sizes fall back to ensureCap growth.
    val flatMapOne = {
      // HYBRID flatMap surface (design §5, same shape as map/filter/reduce): the surface only extracts the
      // source backing array (leaf array directly, or materialize ONCE), realizes ONE IntToRefFn[FBase] SAM whose body is
      // the unboxed source read + the user's inline `f`, and CALLS the shared flatMapShared${KB} driver —
      // NO inlined node/flatten loop at the call site. (The old spliced loop measured a 35x PEA collapse
      // when the caller also invoked a virtual FBase method on the result — see flatMapShared's comment.)
      withErr(
        "summonFrom {\n" + opKinds
          .map { ka =>
            val inner = "summonFrom {\n" + opKinds
              .map { kb =>
                val body =
                  s"{ val sa = (xs match { case leaf: ${ka.name}Arr => leaf.arr; case _ => materialize${ka.name}(xs) }); " +
                    s"flatMapShared${kb.name}(cnt, (i) => f(${readVal(ka, "sa(i)")})) }"
                s"          case rb: ${kb.name}Repr[B] => { if (cnt == 0) Empty.INSTANCE else $body }"
              }
              .mkString("\n") + "\n        }"
            s"      case r: ${ka.name}Repr[A] => $inner"
          }
          .mkString("\n") + "\n    }",
        "A"
      )
    }
    // single-pass unzip/unzip3: read each tuple ONCE, fill the 2/3 output arrays unboxed. Dispatch on the
    // component kinds (resolves to one case per concrete site). Source leaf fast-path / applyBoxed.
    def allocFor(k: Kind, rv: String): String = if k.name == "Ref" then s"new Array[Object](n)" else s"new Array[${k.arr}](n)"
    val refK = opKinds.find(_.name == "Ref").get
    // a node's contents as ONE flat backing array: a leaf hands its array straight in, anything else materializes
    // via the single dfs ONCE. Lets zip/unzip/matchAll2 loop over a flat array on every shape — never kindAt.
    def flatOf(k: Kind, node: String): String = s"($node match { case lf: ${k.name}Arr => lf.arr; case _ => materialize${k.name}($node) })"
    val unzipV = withErr(
      "summonFrom {\n" + opKinds
        .map { k1 =>
          val inner = "summonFrom {\n" + opKinds
            .map { k2 =>
              val fill = s"o1(i) = ${wr(k1, "r1.unwrap(t._1)")}; o2(i) = ${wr(k2, "r2.unwrap(t._2)")}"
              s"          case r2: ${k2.name}Repr[A2] => { val o1 = ${allocFor(k1, "r1")}; val o2 = ${allocFor(k2, "r2")}; val sa = ${flatOf(refK, "xs")}; var i = 0; while (i < n) { val t = ev(sa(i).asInstanceOf[A]); $fill; i += 1 }; (${leaf(k1, "o1", "n")}, ${leaf(k2, "o2", "n")}) }"
            }
            .mkString("\n") + "\n        }"
          s"      case r1: ${k1.name}Repr[A1] => $inner"
        }
        .mkString("\n") + "\n    }",
      "A1"
    )
    val unzip3V = withErr(
      "summonFrom {\n" + opKinds
        .map { k1 =>
          val m2 = "summonFrom {\n" + opKinds
            .map { k2 =>
              val m3 = "summonFrom {\n" + opKinds
                .map { k3 =>
                  val fill = s"o1(i) = ${wr(k1, "r1.unwrap(t._1)")}; o2(i) = ${wr(k2, "r2.unwrap(t._2)")}; o3(i) = ${wr(k3, "r3.unwrap(t._3)")}"
                  s"              case r3: ${k3.name}Repr[A3] => { val o1 = ${allocFor(k1, "r1")}; val o2 = ${allocFor(k2, "r2")}; val o3 = ${allocFor(k3, "r3")}; val sa = ${flatOf(refK, "xs")}; var i = 0; while (i < n) { val t = ev(sa(i).asInstanceOf[A]); $fill; i += 1 }; (${leaf(k1, "o1", "n")}, ${leaf(k2, "o2", "n")}, ${leaf(k3, "o3", "n")}) }"
                }
                .mkString("\n") + "\n            }"
              s"          case r2: ${k2.name}Repr[A2] => $m3"
            }
            .mkString("\n") + "\n        }"
          s"      case r1: ${k1.name}Repr[A1] => $m2"
        }
        .mkString("\n") + "\n    }",
      "A1"
    )
    // zip / zipWithIndex: read both operands UNBOXED (leaf fast-path + <kind>At fallback) into a RefArr of
    // tuples. For primitive×primitive we name the stdlib @specialized Tuple2 directly (Tuple2$mcXY$sp), which
    // stores the two values unboxed — Scala 3 no longer picks it itself, so a uniform-int zip never boxes the
    // elements into Integers; only the tuple object is allocated.
    // Scala only @specializes Tuple2 over Int/Long/Double/Char/Boolean (I/J/D/C/Z); Short/Byte/Float (S/B/F)
