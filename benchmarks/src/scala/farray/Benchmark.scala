// Holistic harness: run the whole Seq-like op surface over many inputs for List vs FArray vs IArray,
// and time each collection across all ops. Each scenario is a thunk (() => Any) closing over shared
// inputs; the three per-collection lists are evaluated by a single pass (ops.foreach(_())). Keeping the
// scenarios as separate lambdas (rather than one giant List of case-class instances) keeps every method
// well under the JVM 64KB limit even though each FArray op inlines to node construction.
object Benchmark:
  def foo(str: String): Either[String, String] = if (str.length > 2) Left(str) else Right(str)

  // Outer container is a plain List (stdlib, non-inline foreach) so each FArray op inlines exactly once
  // inside its own scenario lambda — not once per element of an inline FArray.foreach in every thunk.
  def inputs(base: String, nonEmpty: Boolean = false): (List[List[String]], List[FArray[String]], List[IArray[String]]) =
    val all = (base * 2).toArray.map(_.toString)
    val indices = if nonEmpty then all.indices.drop(1) else all.indices
    val allList = indices.map(all.take).map(_.toList).toList
    val allFArray = indices.map(all.take).map(FArray.fromArray(_)).toList
    val allIArray = indices.map(all.take).map[IArray[String]](IArray.unsafeFromArray).toList
    (allList, allFArray, allIArray)

