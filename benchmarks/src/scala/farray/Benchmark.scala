  def timed(title: String)(t: => Unit): Unit =
    val t0 = System.currentTimeMillis()
    var k = 0
    while k < 100 do { t; k += 1 }
    println(s"$title: ${System.currentTimeMillis() - t0} ms")

  // evaluate every scenario in a list in a single pass
  def runAll(ops: List[() => Any]): Unit = ops.foreach(_())

  @main def flaff(): Unit =
    val all = listOps ::: farrayOps ::: iarrayOps
    var w = 0
    while w < 200 do { runAll(all); w += 1 } // warmup
    timed("warm FArray")(runAll(farrayOps))
    timed("warm List")(runAll(listOps))
    timed("warm IArray")(runAll(iarrayOps))
